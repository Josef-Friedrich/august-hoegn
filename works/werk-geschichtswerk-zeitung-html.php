      <h2>III. Das Wallfahrtskirchlein Osterbr&uuml;nnl bei Ruhmannsfelden<a
  name="iii"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a><br>
        <font
  size="-1">&quot;Durch G&auml;u und Wald&quot;, Beilage zum
        Deggendorfer Donauboten, Nr. 23 / 1927</font> </h2>
      <p><em>Einige erg&auml;nzende Ausf&uuml;hrungen zum gleichnamigen
        Artikel in „G&auml;u und Wald“ Nr. 15 / 1926.<br>
        </em><strong><em>Quellenangabe: </em></strong><em> Ostbairische
        Grenzmarken 1924 Nr. 9/10 – Das ehemalige Zisterzienser-Kloster
        Gotteszell und das Wallfahrtskirchlein Osterbr&uuml;nnl von Domvikar C.
        A. Loibl – Regensburger Marienkalender 1902 – Pers&ouml;nliche
        Aufzeichnungen eines Ruhmannsfeldeners – Pfarrarchiv Ruhmannsfelden </em></p>
      <p>Ein sch&ouml;nes Pl&auml;tzchen in unserem Heimatgau ist das schmale
        Wiesental an der Teisnach, in dessen Mittelpunkt das reizende
        Osterbr&uuml;nnl liegt, dessen idyllische Lage wiederholt in Poesie und
        Prosa geschildert wurde, mit dem sich die Heimatforscher immer gerne
        besch&auml;ftigt hatten, weil stets die Forschungsergebnisse die Arbeit
        gekr&ouml;nt haben. Allerdings ist man sich &uuml;ber die Entstehung des
        Osterbr&uuml;nnls nicht im Klaren.<br>
        An die Entstehung des
        Osterbr&uuml;nnls kn&uuml;pft sich 1. die Sage, 2. die Vermutung. </p>
      <p>Der Inhalt der wiederholt ver&ouml;ffentlichen Sage von der
        Entstehung des Osterbr&uuml;nnls weicht ab, ist etwas verworren.<br>
        <strong>So
        schreibt Waltinger in der Ostbairischen Grenzmarken 1924 Nr. 9 / 10:<br>
        </strong><strong>Am Nachmittage des Ostersonntags 1660 …<br>
        </strong><em>Und der Regensburger Marienkalender 1902 schreibt:<br>
        </em><em>Vor mehr als 100 Jahren …<br>
        </em><strong>Ein Viehhirte des Bruckhofbauern …<br>
        </strong><em>Ein Hirtenknabe ...<br>
        </em><strong>In der Teisnach ein liebliches Muttergottesbild auf dem
        Wasser daher schwimmen ...<br>
        </strong>Hier: <em> ein Muttergottesbild in der Teisnach
        flussaufw&auml;rts schwimmen …<br>
        </em>W&auml;hrend H. Pfarrer Vietl in dem sch&ouml;nen Gedicht &uuml;ber
        das Osterbr&uuml;nnl das Wunderbild gar aus dem Morgenlande Donau
        aufw&auml;rts &uuml;ber Pressburg, Wien, Regensburg, Regen, Teisnach
        aufw&auml;rts hierher kommen l&auml;sst.<br>
        <strong>Er st&uuml;lpte die
        Hose auf und watete nach dem Bilde. Zu seiner gr&ouml;&szlig;ten
        Verwunderung aber konnte er es nicht erreichen usw. Da dachte der
        Bruckhofer an den Pfarrer und holte ihn. Der konnte das Bild ohne
        besondere M&uuml;he aus den Fluten holen …<br>
        </strong>Hier: <em> erstaunt &uuml;ber die seltsame Erscheinung holte
        der Knabe das Bild aus dem Bache …<br>
        </em><strong>Als er jedoch an die Stelle kam, wo heute das Kirchlein
        Osterbr&uuml;nnl steht, wurde das Bild auf einmal so schwer, dass er es
        ihm unm&ouml;glich wurde, es weiter zu tragen. Daher lehnte er es an
        einen Erlenstamm am Wege …<br>
        </strong>Hier: <em> und befestigte es schlie&szlig;lich an einem Baum,
        wo er h&auml;ufig seine kindliche Andacht zu Unser Lieben Frau
        verrichtete.<br>
        </em><strong>O Wunder! Kaum hatte das Bild den Boden ber&uuml;hrt,
        sprang eine murmelnde Quelle daraus hervor …<br>
        </strong>Hier: <em> Als er l&auml;ngere Zeit so gebetet hatte,
        f&uuml;hlte er einen brennenden Durst. Allein es war ihm unm&ouml;glich
        zu einer Quelle zu gelangen. Da erneuerte er sein vertrauungsvolles
        Gebet zur Gottesmutter und siehe – pl&ouml;tzlich entdeckte er eine
        Quelle neben dem Baum, an welchem das Bild hing. – Er stillte seinen
        Durst und erlangte zugleich die Heilung f&uuml;r seine kranken
        F&uuml;&szlig;e …<br>
        </em><strong>Daran erkannte der Pfarrer, dass es der Wunsch der
        Gottesmutter sei, hier ein Heim zu besitzen und erh&auml;ngte daher das
        Bild an die Erle. Der Bruckhofer errichtete alsbald eine h&uuml;bsche
        Holzkapelle, die sp&auml;ter aus Stein erbaut wurde …<br>
        </strong>Hier: <em>auf die Kunde von dieser Gebetserh&ouml;rung hin,
        kamen Ungl&uuml;ckliche und Bedr&auml;ngte aus nah und fern, um Unser
        Lieben Frau um Hilfe anzuflehen. Die Kapelle, urspr&uuml;nglich aus Holz
        gezimmert, wurde sp&auml;ter aus Mauerwerk hergestellt.<br>
        </em>Es liegen also hier tats&auml;chlich Verschiedenheiten vor, die bei
        genauerer Vergleichung Veranlassung geben, zu sagen, dass der
        Hirtenknabe, arm, krank an beiden F&uuml;&szlig;en, f&uuml;r seinen
        Gebetseifer und sein felsenfestes Vertrauen auf die Muttergotteshilfe
        reichlich belohnt wurde, dass uns die Sage im Regensburger
        Marienkalender f&uuml;r die Einf&uuml;hrung in die Entstehungsgeschichte
        des Osterbr&uuml;nnls besser erscheint, weil diese Sage die Entstehung
        des Wallfahrtskirchleins schon mit dem Motiv verbindet: „Maria hat
        geholfen.“ </p>
      <p>An die Entstehung des Osterbr&uuml;nnls kn&uuml;pft sich 2. die
        Vermutung.<br>
        Akten, die uns genauen Aufschluss geben w&uuml;rden
        &uuml;ber die Entstehung der Osterbr&uuml;nnls, sind bis jetzt nicht
        hinterlegt. Es ist nicht ausgeschlossen, dass vielleicht doch noch in
        irgend einem Archiv ein diesbez&uuml;gliches Dokument vorgefunden wird,
        insbesonders, wenn einmal die Pfarr- und Gemeindearchive geordnet und
        zug&auml;nglich gemacht werden.<br>
        H. Pfarrer Lukas, ein geborener
        Ruhmannsfeldener vermutet, die Osterbr&uuml;nnlkapelle sei die ins Tal
        herab ger&uuml;ckte alte Schlosskapelle. Da m&uuml;sste also auf der
        H&ouml;he ein Schloss gestanden sein, was gar nicht zutrifft, da ein
        Schloss hier niemals existierte und die einstens hier vorhandene Burg
        Hrothmars (Rumars) eine Weiherburg war. Also kann das Osterbr&uuml;nnl
        nicht die ins Tal herab ger&uuml;ckte Schlosskapelle sein. &Uuml;berdies
        sind diese Wallfahrtskirchlein mit der Bezeichnung „Br&uuml;nnl“
        erst sp&auml;ter entstanden und die Burgherren von anno dazumal, als es
        in Ruhmannsfelden noch eine Burg gab, 12. und 13. Jahrhundert, kannten
        eine Marienverehrung in dem Umfange, dass von ihnen allerorts
        Marienkapellen errichtet worden w&auml;ren, noch nicht, vielmehr
        m&uuml;ssen wir unsere Kapellchen von <em>den „14 Nothelfern,
        Sebastiani-, Florianikapelle und dergleich“ </em> in dieser Zeit
        entstehen lassen.<br>
        F&uuml;r die Entstehung des Osterbr&uuml;nnls gibt
        es zwei Vermutungen – entweder waren es religi&ouml;se oder
        wirtschaftliche Bed&uuml;rfnisse, die die Errichtung dieser
        Wallfahrtsst&auml;tte notwendig machten. Da entsteht eine Kapelle, um
        die Dorfbewohner gemeinsam zum Gebete vereinigen zu k&ouml;nnen, dort,
        wegen der weiten Entfernung von der Pfarrkirche, dort, um die Maiandacht
        abhalten oder seine Bittgebete zu irgendeinem Heiligen verrichten zu
        k&ouml;nnen. Alles das war f&uuml;r die Entstehung des Osterbr&uuml;nnls
        kaum ma&szlig;gebend. Sondern wir m&uuml;ssen uns zur&uuml;ckversetzen
        in die Zeit, als zwischen den Ruhmannsfeldenern und Gotteszellern eine
        tiefe Kluft war. Ja, der ewige Streit und Zank zwischen Gotteszell und
        Ruhmannsfelden wirkte sich neben anderem auch auf religi&ouml;sem
        Gebiete aus, zumal, wenn wir uns zur&uuml;ckversetzen in die
        Nachreformationszeit, in die Nachkriegszeit in der Mitte des 17.
        Jahrhunderts, wo es &uuml;berall, so auch im Kloster Gotteszell ziemlich
        spukte. Dort das verhasste Zisterzienserkloster mit dem gestrengen
        Klosterkonvent – hier das nach Ruhe und Rettung aus seiner misslichen
        Lage d&uuml;rstende gl&auml;ubige Volk. Dort die Pilger in gro&szlig;en
        Wallfahrtsz&uuml;gen infolge der Gebetserh&ouml;rung und
        Krankheitsheilung auf die F&uuml;rbitte hin zur Heiligen Mutter Anna –
        hier nichts als Not und Elend und Bedr&uuml;ckung und die leeren
        Taschen. Da pl&ouml;tzlich erschien das Wunderbild Mariens als
        „Helferin und Retterin“ in diesem Wirrwarr. Die Kunde von der
        wunderbaren Heilung des armen, kranken Hirtenknaben drang durch alle
        Lande. Die erwachende Erkenntnis, dass die F&uuml;rbitte zu Maria
        „Heilung und Rettung“ bringen kann, f&uuml;hrte zur Errichtung einer
        St&auml;tte, an der man ohne Beisein der unbeliebten Gotteszeller
        Klosterherren dem stillen Gebete zu Maria obliegen konnte. Und Maria hat
        die F&uuml;rbitten erh&ouml;rt und reichlich geholfen. Das sagen uns die
        vielen Votivtafeln, die einstens das Kirchlein schm&uuml;ckten. Auf
        diese Weise k&ouml;nnen religi&ouml;se Momente zur Entstehung des
        Osterbr&uuml;nnls mitgespielt haben. Es k&ouml;nnen aber auch
        wirtschaftliche Verh&auml;ltnisse mit hereingespielt haben, zumal, wenn
        wir uns in die Mitte des 17. Jahrhunderts zur&uuml;ckversetzen, wo
        Verw&uuml;stung, Elend, Verarmung vorherrschte, da war auch hier in
        Ruhmannsfelden, in dem Markt, der sich einstens unter Aldersbacher
        Klosterherrschaft so rasch emporgeschwungen hatte, der Drang und
        Bestreben sich wieder emporzuarbeiten. Mit Neid musste der
        Ruhmannsfeldener sehen, wie ganze Wallfahrtsz&uuml;ge durch
        Ruhmannsfelden hindurch gezogen hin&uuml;ber zur Mutter Anna in der
        Klosterkirche Gotteszell, was sich dort nach den Gottesdiensten und
        Andachts&uuml;bungen f&uuml;r ein Gesch&auml;ft abwickelte. Und
        Ruhmannsfelden wollte auch empor. Aber wie? Da kommt der Retter in
        wirtschaftlicher Not. Die Kunde von der wunderbaren Heilung des an
        beiden F&uuml;&szlig;en erkranken Hirtenknaben verbreitete sich
        blitzschnell in der ganzen weiten Umgebung. Ein Kapellchen aus Holz
        wurde gezimmert. Das Muttergottesbild, das flussaufw&auml;rts aus fernem
        Lande hierher gekommen und jetzt das schmucke Alt&auml;rchen zierte,
        wollte von jedermann gesehen sein. In dichten Scharen str&ouml;mten sie
        herbei, die bei Maria Hilfe und in dem heilkr&auml;ftigen
        Osterbr&uuml;nnlwasser ihre Heilung suchten. Die frommen Pilger zogen
        nicht mehr nach Gotteszell, sie blieben in Ruhmannsfelden. Das brachte
        den Ruhmannsfeldenern gro&szlig;e Einnahmen, war f&uuml;r die weitere
        Entwicklung des Marktes von gro&szlig;er Bedeutung war. Gottes
        F&uuml;gung war auch hier der Retter aus wirtschaftlicher Not. </p>
      <h2>IV. Das Wallfahrtskirchlein Osterbr&uuml;nnlein bei Ruhmannsfelden<a
  name="iv"></a> &nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a><br>
        <font
  size="-1">&quot;Durch G&auml;u und Wald&quot;, Beilage zum
        Deggendorfer Donauboten, Nr. 2 / 1928 </font> </h2>
      <p><em>Einige erg&auml;nzende Ausf&uuml;hrungen zum gleichnamigen
        Artikel in „G&auml;u und Wald“ Nr. 15 / 1926</em></p>
      <p>Wenn die Entstehung des Osterbr&uuml;nnls zeitlich bestimmt werden
        soll, so lassen sich genaue Angaben vorerst nicht machen; aber es wird
        vermutet, dass das Osterbr&uuml;nnl entstanden ist unmittelbar nach dem
        30j&auml;hrigen Krieg, in jener Zeit, in der das Volk in tiefster
        religi&ouml;ser und wirtschaftlicher Not war. Waltinger schreibt: <em>„Am
        Nachmittage des Ostersonntags 1660 …..“<br>
        </em>In dieser Zeit war die Stelle, an der heute das Wallfahrtskirchlein
        steht, noch mit m&auml;chtigen Fichten bewachsen. Wir finden also das
        Kapellchen urspr&uuml;nglich im dunklen Hochwald, bei einem
        Bergbr&uuml;nnlein mit seinem klaren, frischen Wasser. Ganz versteckt!
        Nicht auf der H&ouml;he, nicht an der Stra&szlig;e. Es durfte ja von den
        Gotteszellern nicht gesehen werden, die mit der Entstehung des
        Osterbr&uuml;nnls nicht einverstanden waren. Bedeutete doch das
        Osterbr&uuml;nnl f&uuml;r Gotteszell eine schwere Einbu&szlig;e auf
        religi&ouml;sem und wirtschaftlichem Gebiete, zumal der Besuch des
        Osterbr&uuml;nnls und die Verehrung des Marienbildes damals sehr
        gro&szlig; gewesen sein muss. Abt Wilhelm II. (1716 - 1760) erblickte in
        dem Osterbr&uuml;nnl Trotz und Auflehnung der Ruhmannsfeldener gegen
        ihre Obrigkeit, sah darin eine Beeintr&auml;chtigung der Wallfahrt zu
        St. Anna in Gotteszell und befahl als Vogt und Grundherr anno 1724 die
        h&ouml;lzerne Kapelle samt den darin befindlichen Votivtafeln
        niederzubrennen, was auch geschah. Alle Bem&uuml;hungen der
        Ruhmannsfeldener, eine neue Kapelle aufrichten zu d&uuml;rfen, waren
        vergebens. Es wurde sogar mit Strafe gedroht. Abt Wilhelm II. verstand
        es auch die bisch&ouml;fliche Beh&ouml;rde in diesem Sinne zu
        beeinflussen, sodass die Ruhmannsfeldener auch bei dieser Stelle mit
        ihrer Bitte um Wiederaufrichtung der Kapelle streng abgewiesen wurden.
        Das dauerte solange, bis das Kloster aufgel&ouml;st wurde. Im Jahre 1813
        erneuerten die Ruhmannsfeldener ihre Bitte beim Kgl. Kreiskommissariate
        des Unterdonaukreises, die niedergebrannte Kapelle wieder aufbauen zu
        d&uuml;rfen. Das Kreiskommissariat erteilte die Genehmigung und durch
        das eifrige Zusammenarbeiten aller Ruhmannsfeldener entstand wieder das
        Wallfahrtskirchlein „Osterbr&uuml;nnl“ und die Wallfahrt bl&uuml;hte
        so rasch auf, dass ein Ruhmannsfeldener schreibt: <em>„1813 kam die
        Wallfahrt im so genannten Osterbr&uuml;nnl so stark auf, dass man vom
        Einnahm Geld oder Einleg Geld der Wallfahrter, welche in selbiger Zeit
        meistens Unterlehner waren, so viel zusammen kam, das sich Josef Baumann
        und Anton Schl&ouml;gl, derzeit ledigen Mannspersonen vom Markte
        annahmen und eine Kapelle gegen Untergang der Sonne von Holz bauen
        lie&szlig;en und lie&szlig;en ein gro&szlig;es Mariahilfsbild in einer
        Tafel samt einem Glaskasten machen (per 30 fl. kostet) und am
        Charfreitag 1814 hinuntertrugen betend. Jetzt kam die Wallfahrt noch
        gr&ouml;&szlig;er auf, obwohl es schon vor 100 Jahren war. Sie
        lie&szlig;en auch einen Opferstock machen und wo viel Geld einging. Aber
        trauriger Tag: Am 18. Oktober 1814 lie&szlig; H. Landrichter Bayerlein
        von Viechtach die Kapelle in der Nacht zusammenrei&szlig;en. Jetzt
        nahmen die zwei oben genannten Mannspersonen das Muttergottesbild samt
        Kasten und trugens noch bei der Nacht in das Gotteshaus Laurentius
        hinauf, wo man es unter die Kanzel setzte und mit vielen
        Silberanh&auml;ngen beehrt wird. Die Kapelle blieb wie zuvor. Bis 1820
        wirkte sie die gr&ouml;&szlig;ten Wunderwerke und es ging soviel Geld
        ein, dass es ohne dem, was man zur Kirche verwendet, noch bis 1100 fl.
        da waren.<br>
        </em><em>Aber trauriger Tag: 1. Juli 1820. Da brannten wir samt der
        sch&ouml;nen Mutter Pfarrkirche und elf Mitnachbarn, samt Brothaus ab,
        wo das Feuer beim Berger auskam. Jetzt verbrannte auch das sch&ouml;ne
        Mariahilfsbild in der Kirche und als das die Herrschaft h&ouml;rte, dass
        soviel Osterbr&uuml;nnl Einleggeld da war, welches der Herr Marian
        (Anm.: P. Marian Priendorfer, geb. 19. Okt. 1775 zu Haidlfing, bei der
        S&auml;kularisation des Gotteszeller Klosters vertrieben, hielt sich
        zuletzt in Viechtach und in Ruhmannsfelden auf) Fr&uuml;hmetzer untern
        H&auml;nden hat, lie&szlig; es H. Marian gleich nehmen und nach
        Viechtach bringen, wo leider bisher noch kein Pfennig ausgefallen ist.
        – Allein, jetzt hei&szlig;t es, man soll eine Kapelle in
        Osterbr&uuml;nnl aufmauern lassen und das n&auml;mliche Mariahilfsbild,
        wie zuvor, dass wiederum ein Geld einging. Aber was war&acute;s? Die
        Kapelle konnte nicht ausgebaut werden, weil kein Geld da war und auch
        eine Genehmigung hiezu nicht erfolgte.“<br>
        </em>Ja man befahl sogar das beim Osterbr&uuml;nnl angefahrene
        Baumaterial solle zum Wiederaufbau der Pfarrkirche verwendet werden. Die
        Arbeiten bei der abgebrannten Pfarrkirche gingen aber nur sehr langsam
        vorw&auml;rts, <em>„weil die Arbeitsleute lieber bei den
        Abbr&auml;ndlern arbeiteten, wo sie bare Bezahlung erhalten.“ –
        „Die Messopfer unserer Priester geschehen daher auf offenem
        Marktplatze an einem Tisch, &uuml;ber welchen einige Bretter geschlagen
        sind, w&auml;hrend rund herum die Bauleute h&auml;mmern und l&auml;rmen,
        Fuhrleute vor&uuml;ber toben, etc.“<br>
        </em>Die Pfarrei und Marktgemeinde Ruhmannsfelden hat sich im September
        1820 an den K&ouml;nig mit einem Bittgesuch, das Osterbr&uuml;nnlein
        ausbauen zu d&uuml;rfen, gewandt, unter Hinweis darauf, dass die Kapelle
        am Osterbr&uuml;nnl unsch&auml;dlich, ja sogar n&uuml;tzlich und
        notwendig sei. Immer wieder erfolgte die Abweisung der Bittgesuche, da
        Landgericht und Rentamt Viechtach gegen den Wiederaufbau des
        Osterbr&uuml;nnls waren. Endlich am 24. Mai 1821 gab das Landgericht
        Viechtach seine Zustimmung. <em>„Mit Beihilfe in und au&szlig;er der
        Pfarr wurde eine sch&ouml;ne Filialkirche aus dem Osterbr&uuml;nnl
        erbaut.“ </em> Am 23. Juli 1821 erhielt H. Pfarrprovisor Deischl vom
        Ordinariate Regensburg die Erlaubnis, die neu erbaute Kapelle auf dem so
        genannten Osterbr&uuml;nnl nach Vorschrift benedizieren zu d&uuml;rfen,
        was auch geschah.<br>
        Am 4. August 1821 bekundet der Markt Magistrat
        Ruhmannsfelden von H. Pfarrprovisor Deischl 21 fl. als mild
        Beitr&auml;ge zum Bau des Osterbr&uuml;nnls verwenden zu d&uuml;rfen –
        18 fl. f&uuml;r den eben dorthin gekauften Altar. 1822 wurde der
        Hochaltar gemacht. In pers&ouml;nlichen Aufzeichnungen hei&szlig;t es
        weiter: <em>„Am 24. M&auml;rz 1822 wurde das Mariahilfsbild bei einem
        sch&ouml;nen Wetter nach der Frauenlitanei nachmittags 5 Uhr an einem
        Sonntag am Tag vor Maria Verk&uuml;ndigung mit dem ganzen Gel&auml;ute,
        wo H. Pfarrer Kaplan und H. Marian selbst dabei war, unter sch&ouml;nen
        Ges&auml;ngen und Kruzifix und betend, von vier wei&szlig;gekleideten
        Jungfrauen ins Osterbr&uuml;nnl am Hochaltar hinunter getragen und
        eingesetzt, wo recht viele Leute in und au&szlig;er der Pfarrei dabei
        waren.“ </em> 1842 wurde die Wallfahrtskirche restauriert und von H.
        Pfarrer Wagner benediziert. 1857 wurde ein Einbruch in die
        Wallfahrtskirche ver&uuml;bt, worauf der Untersuchungsrichter von
        K&ouml;tzing zur Augenscheineinnahme ins Osterbr&uuml;nnl kam.<br>
        1869
        macht die Kirchenverwaltung Ruhmannsfelden an das Bezirksamt Viechtach
        ein Gesuch, den bereits sehr schadhaften Teil der
        Schneidschindelbedachung auf der Osterbr&uuml;nnlkapelle mit gleichem
        Material eindecken zu d&uuml;rfen.<br>
        1877 hat sich Schmiedmeister
        Josef Baumann von Ruhmannsfelden bereit erkl&auml;rt zur Herstellung
        eines Kreuzweges beim Osterbr&uuml;nnl. Das Ordinariat Regensburg
        genehmigte die Einweihung der vollendeten Stationsbilder respektive der
        h&ouml;lzernen Kreuze. Die Einweihung geschah durch Franziskanerpater
        Berard Zierer unter H. Pfarrer R&ouml;tzer.<br>
        Im M&auml;rz 1880 hat
        eine B&auml;uerin einen Kreuzweg zum Osterbr&uuml;nnl schenken wollen,
        was vom Ordinariate Regensburg abgelehnt wurde, unter Hinweis darauf,
        dass es nicht gestattet werden k&ouml;nne, einen derartigen Kreuzweg in
        einer Wallfahrtskirche aufzustellen und dass nicht &auml;hnliche von den
        Gl&auml;ubigen gekaufte Bilder und Bildchen ohne vorg&auml;ngige
        Pr&uuml;fung des H. Pfarrers und dessen Zustimmung aufgehangen werden.<br>
        Im Jahre 1891 erfolgte eine gr&ouml;&szlig;ere Reparatur mit einem
        Kostenaufwand von 2471 Mark und auch in sp&auml;teren Jahren hat man
        nicht unterlassen dem Wallfahrtskirchlein die Sorgfalt angedeihen zu
        lassen, die ihm geb&uuml;hrt. Und so macht das Kirchlein schon von
        au&szlig;en einen netten Eindruck, frisch get&uuml;ncht, mit seinem
        grauen Schindeldach und den kleinen T&uuml;rmchen, das ein Altertum in
        sich birgt – eine Glocke, ungef&auml;hr 1 Ztr. schwer, mit dem Ton a
        – welche die Inschrift tr&auml;gt: </p>
      <p align="center"><em>„Hans Durnknopf zu Regenspurg 1550.“ </em></p>
      <p>Das Werk eines seinerzeit hochber&uuml;hmt gewesenen und noch jetzt
        mit zahlreichen Glocken in weitestem Umkreise vertretenen
        Glockengie&szlig;ermeisters von Regensburg (nach H. H. Pfr. Oberschmid:
        Straubing Stadtturm 1535 – Amselfing 1524 – M&uuml;hlhausen
        (Abensberg) 1529 – Haibach 1518 – Hirschkofen (Atting) 1550 usw.)
        Die alten Votivt&auml;felchen und sonstigen alten Sachen wurden vor
        geraumer Zeit aus dem Kirchlein entfernt. Schade, da der Ersatz
        hief&uuml;r dem Kirchlein nicht zur Zierde gereicht und dem
        Heimatforscher nichts sagend ist.<br>
        In unmittelbarer N&auml;he des
        Osterbr&uuml;nnl Wallfahrtskirchleins ist der Osterbr&uuml;nnlkeller.
        Dort suchen die Pilger, die alle Jahre kommen, von Zwiesel, Langdorf,
        Bischofsmais, Gotteszell, Patersdorf usw. bei gutem Tropfen aus der
        Brauerei Amberger-Ruhmannsfelden St&auml;rkung f&uuml;r den Heimmarsch
        und f&uuml;r die Einheimischen und Touristen ist der
        Osterbr&uuml;nnlkeller ein gern besuchtes Ruhepl&auml;tzchen. </p>
      <h2><b>V.</b> Was Ruhmannsfelden f&uuml;r Jubil&auml;en feiern
        k&ouml;nnte?<a name="v" id="v"></a>&nbsp;&nbsp;<a href="#oben"><img
