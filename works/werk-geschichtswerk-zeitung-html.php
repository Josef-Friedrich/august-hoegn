        eingesch&auml;tzt. Diese Statue ist seinerzeit nach dem Kauf sofort von
        dem b&uuml;rgerlichen Maler, und Vergolder F. S. Merz in Regensburg
        renoviert und gefasst worden.<br>
        Am 5. Juni 1837 fand unter H. Hr. Pfarrer Lienhard die bisch&ouml;fliche
        KonsekratIon der Pfarrkirche statt. 1855 wurde ein neuer &Ouml;lberg bei
        der Pfarrkirche nach einem vom k&ouml;nigliche Kreisb&uuml;ro
        angefertigten Plan errichtet. 1862 wurden Reparaturen am Glockenturm
        vorgenommen, da dieser bauf&auml;llig war; au&szlig;erdem wurde der Turm
        mit Wei&szlig;blech von A. Prigelmayer in Viechtach eingedeckt. Im
        gleichen Jahre wurde aus dem Tabernakel eine wertvolle Monstranz
        gestohlen. 1866/67 wurden die Seitenalt&auml;re, 1868 der Hochaltar neu
        gefasst und am 6. Juli 1869 erfolgte die Konsekration der Alt&auml;re
        unter H. Hr. Pfarrer Uschalt. 1878 erbaute die Sepulturgemeinde
        Ruhmannsfelden den neuen Friedhof. Leider hat man dabei die alten
        Grabsteine entfernt, die uns so viel erz&auml;hlen k&ouml;nnten.<br>
        Eine Urkunde erz&auml;hlt von folgender Begebenheit: <i>&quot;Am
        17.3.1879 abends beging der ledige Michael Eisenrichter, 50 Jahre alt,
        einen blutigen Selbstmordversuch auf den Stufen des Hochaltares in der
        Laurentius-Pfarrkirche, indem er sich an diesem Abend ungesehen in die
        Kirche einsperren lie&szlig;. Pl&ouml;tzlich h&ouml;rte man Rufe aus der
        Kirche und das Schellen mit dem Ministrantengl&ouml;cklein. Dem Mesner
        Plank und seiner Magd, die sich sofort in die Kirche begaben, bot sich
        ein entsetzlicher Anblick. Auf der obersten Altarstufe lag ein Mann in
        Hemd&auml;rmeln, die Schuhe abgezogen, seine Joppe als Kopfkissen zu
        Recht gerichtet. Er rief: &quot;Plank Alois, mich friert so, wo ist mein
        Rasiermesser, ich habs verloren.&quot; Die Altarstufen, ja selbst der
        Altartisch waren mit Blut befleckt. Am Halse trug der Mann zwei
        m&auml;chtige Schnittwunden. Die Mesnermagd Anna Sch&ouml;nberger,
        welche die Laterne trug, fand das Rasiermesser am linken Ende der
        unteren Altarstufe. Am rechten Ende lag das Taschemesser. Dieses scheint
        zu stumpf gewesen zu sein. Inzwischen trafen viele Mannspersonen in der
        Kirche ein. Der damalige Sergeant lie&szlig; den Mann in das Krankenhaus
        verbringen. Die Wunden waren nicht t&ouml;dlich. Den Beistand des
        Pfarrers wies er zur&uuml;ck. Der Mann hatte diese Tat mit Absicht und
        Vorbedacht begangen. 40 Jahre war er von Ruhmannsfelden fort und war
        erst kurze Zeit wieder hier. Er war Lederergeselle und hielt sich
        gr&ouml;&szlig;tenteils in &Ouml;sterreich auf. Er hat viel verdient
        aber auch alles wieder angebracht. Er war ein Religionshasser und
        Gottesl&auml;sterer. Die Armenpflege nahm sich seiner an und besorgte
        ihm eine Wohnung und ein Bett. Damit f&uuml;r seine Verk&ouml;stigung
        gesorgt war, wurde die Umzeche f&uuml;r ihn angeordnet.
        B&uuml;rgermeister Lederer Lukas verk&ouml;stigte ihn gut. Trotzdem
        wollte er die Umzeche nicht. Weil diese Angelegenheit nicht nach seinem
        Wunsche geregelt wurde, beging er die grausige Tat. Nachdem Eisenrichter
        ins Krankenhaus gebracht war, wurde das Allerheiligste sofort in den
        Pfarrhof gebracht. Alle kirchlichen Funktionen unterblieben. Der hochw.
        Hr. Bischof wurde telegraphisch verst&auml;ndigt. Am 19.3.1879 traf auch
        schon das gregorianische Wasser ein, Altar und Kirche wurden sofort
        konsekriert. Mit einem feierlichen Gottesdienste und Predigt wurden die
        kirchlichen Funktionen in der Laurentius-Pfarrkirche wieder aufgenommen.
        Inzwischen sind die kirchlichen Verrichtungen im Osterbr&uuml;nnl
        abgehalten worden.&quot;</i><br>
        Zweimal brannte die Pfarrkirche ab 1574 und 1820. Der 30. April 1889
        w&auml;re derselben bald wieder zum Verh&auml;ngnis geworden. Um die
        Mitternachtstunde des genannten Tages brannten sieben Anwesen im oberen
        Markte, Dietrich, Sixl, Weinzierl, Meindl, Hirtreiter, Reisinger und
        Baumann, die ihre Anwesen in n&auml;chster N&auml;he um die Pfarrkirche
        herum hatten, vollst&auml;ndig nieder. Zum gr&ouml;&szlig;ten Gl&uuml;ck
        hatte die Pfarrkirche um diese Zeit schon eine harte Bedachung
        (Platten). Trotzdem fing der Dachstuhl des Presbyteriums schon zu
        brennen an. Das Feuer konnte aber gl&uuml;cklicherweise noch
        bek&auml;mpft werden, sodass es nicht weiter greifen konnte, sonst
        w&auml;re die Laurentiuskirche sicherlich zum dritten Male ein Raub der
        Flammen geworden.<br>
        In ihrem jetzigen Gewande ist die Laurentius-Pfarrkirche in
        Ruhmannsfelden ein wahres Schmuckk&auml;stchen unter den Kirchen des
        bayerischen Waldes. Wer in die Kirche tritt, dem f&auml;llt sofort der
        eigent&uuml;mliche Baustil auf. In der Zeit, als die abgebrannte
        Laurentius-Pfarrkirche wieder aufgebaut wurde, hat man unter dem
        bestimmenden Einfluss K&ouml;nig Ludwig I. die Vorbilder zu den
        Neubauten der Antike entnommen und dementsprechend wurde auch die
        Laurentius-Pfarrkirche unter Kreisbauingenieur Hochstetters Leitung im
        klassizistischen Baustil ausgef&uuml;hrt. Die Seitenschiffe mit den
        waagrechten Decken sind im griechischen (klassischen) Baustil. Das
        Mittelschiff mit seinem Deckengew&ouml;lbe ist in
        r&ouml;misch-griechischem Baustil. Das ganze Innere der Kirche mit den
        griechischen S&auml;ulen zu beiden Seiten des Hauptschiffes und dem
        antiken Aufbau der Alt&auml;re klassizistisch. <br>
        Der Hochaltar, in elfenbeinwei&szlig; gehalten, hat drei Stufen, den
        Altartisch, den Altaraufbau mit dem Tabernakel. Zwei jonische, glatte,
        nicht kanelierte S&auml;ulen mit den Kapit&auml;len tragen ein in Gold
        reich verziertes Fries, das mit einem Zahnschnitt nach oben
        abschlie&szlig;t, auf dem der dreischenkelige Giebel ruht. Im Giebelfeld
        ist das strahlende Auge Gottes. Auf den zwei, oberen Giebelschenkeln
        sitzen wachende Engel und die Giebelspitze tr&auml;gt ein goldenes
        Kreuz. So sind auch die Seitenalt&auml;re, nur tragen diese
        gew&ouml;lbte Giebel. Die Altarbilder vom Hochaltar und den zwei
        Seitenalt&auml;ren haben gro&szlig;en Kunstwert. Das Hochaltarbild ist
        Eigentum des Staates. Sehr wertvoll ist auch der Kreuzweg in der
        Pfarrkirche. Derselbe ist (siehe Gg. Aichinger) nach Entw&uuml;rfen von
        F&uuml;hrich (ein &ouml;sterreichischer Maler, 1800 bis 1876) auf
        Messinggewebe von einem Sohn des Marktes, Leopold Baumann, gemalt.
        Dieser malte auch das Altarbild vom Corporis-Christi-Bruderschaftsaltar,
        im Presbyterium.<br>
        1903 wurde die Pfarrkirche einer gr&uuml;ndlichen Restaurierung
        unterzogen, die Sakristei umgebaut und oberhalb der Sakristei ein
        Oratorium geschaffen.<br>
        Die Deckengem&auml;lde, Szenen aus dem Leben des hl. Laurentius
        darstellend, und Restaurierung der Seitenw&auml;nde schuf Malermeister
        Weber von Amberg. Die Fassung der Alt&auml;re und Statuen f&uuml;hrte
        Malermeister Boroviska von Regensburg aus. Die St&uuml;hle und
        T&uuml;ren sowie die Sakristeischr&auml;nke machte Schreinermeister
        Kappl von Linden.<br>
        Das Reliefbild oberhalb der Sakristeit&uuml;re, die Anbetung Jesu
        darstellend, wurde vom verstorbenen Hr. Kammerer M&uuml;hlbauer
        angekauft. Katharina Bielmeier, Bauerstochter in Schwarzen stiftete den
        Betrag zur Beschaffung des Pflasters im Presbyterium. Durch vier
        Kirchenfenster hinter dem Hochaltar f&auml;llt Licht in das
        Presbyterium, an dessen Decke die Worte in gro&szlig;en Lettern stehen: <i>&quot;Hic aula Dei &quot;</i> d. h. das ist die Wohnung Gottes. Das
        linke von diesen vier Kirchenfenstern, Herz Jesu darstellend, ist vom
        Paktistenbund Ruhmannsfelden, das rechte, Herz Maria darstellen, vom
        Jungfrauenbund Ruhmannsfelden 1903 gestiftet worden. Durch drei
        Kirchenfenster f&auml;llt Licht in das rechte Seitenschiff, das eine
        davon, den hl. Isidor darstellend vom Bauernverein Ruhmannsfelden 1903
        und das andere, den hl. Nepomuk darstellend, ist vom B&uuml;rgerverein
        Ruhmannsfelden 1903 gestiftet worden. Leider ist dieses Kirchenfenster
        von einem Kirchenr&auml;uber durch Eindr&uuml;cken besch&auml;digt
        worden. Im rechten Seitenschiff sind 2 Beichtst&uuml;hle, 9
        Seitenst&uuml;hle, 2 Weihwasserkessel, 2 Opferst&ouml;cke und der
        breite, bequeme Stiegenaufgang zur Empore und zum Chor.<br>
        Von den drei Kirchenfenstern, durch die das Licht in das linke
        Seitenschiff f&auml;llt, ist das eine, den hl. Franziskus darstellend
        vom 3. Orden Ruhmannsfelden 1903 gestiftet und das andere, den hl. Georg
        darstellend, vom Krieger- und Veteranenverein Ruhmannsfelden 1903
        gestiftet. Die s&auml;mtlichen Fenster stammen aus der Kunstglaserei
        Schneider in Regensburg. Im linken Seitenschiff sind ebenfalls 2
        Beichtst&uuml;hle, 9 Seitenst&uuml;hle, 2 Weihwasserkessel, ein
        Opferstock und der Stiegenaufgang zur Empore. W&auml;hrend des
        Weihnachtsfestkreises ist am linken Seitenaltar eine sehr nette Krippe
        angebracht, die von Jung und Alt gerne besucht wird.<br>
        An den Seitenw&auml;nden des Hauptschiffes sind in den breiten Feldern
        zwischen den S&auml;ulen in kreisrunden mit Verzierungen versehenen
        Fl&auml;chen Heiligenbilder. In den Feldern der Gew&ouml;lbest&uuml;tzen
        sind die sieben Sakramente bildlich dargestellt. Durch je vier
        gro&szlig;e Halbfenster zu beiden Seiten des Hauptschiffes f&auml;llt
        gen&uuml;gend Licht in den gew&ouml;lbten Raum. Links vom Hochaltar ist
        die Statue des hl. Stephanus, rechts davon die Statue des hl.
        Laurentius, der zwei Erzdiakone. An der linken Seite im Presbyterium
        steht die Statue &quot;Herz-Jesu&quot;, an der rechten Seitenwand die
        Statue &quot;Herz Maria.&quot; Diese beiden Statuen sind erst
        sp&auml;ter von H. Hr. Pfarrer Fahrmeier nachgeschafft worden und
        weichen von den Altarstatuen in Farbe und Gr&ouml;&szlig;e ab. Links vom
        Corporis-Christi-Bruderschaftsaltar im Presbyterium steht die Statue der
        &quot;St. Barbara&quot;, rechts die Statue des &quot;St. Johannes.&quot;
        Links vom Laurentiusaltar im rechten Seitenschiff steht die Statue
        &quot;Herz Maria&quot; und rechts die Statue &quot;Herz Jesu&quot;.
        Links vom Martini-Altar im linken Seitenschiff ist die Statue der
        &quot;St. Philomena, rechts ist die Statue des &quot;St. Joseph.&quot;
        Auf dem Kanzeldach steht die Statue des &quot;Petrus&quot;. Die
        s&auml;mtlichen Statuen sind &uuml;berlebensgro&szlig; und
        elfenbeinwei&szlig;. In der Mitte der Kirche h&auml;ngt vom
        Deckengew&ouml;lbe herab inmitten der zwei Bilder &quot;Zacharias&quot;
        und &quot;Ezechiel&quot; das Kreuz mit dem gekreuzigten Heiland.<br>
        3 Gedenktafeln k&uuml;nden uns von verdienstvollen, ber&uuml;hmten
        Ruhmannsfeldenern. Die Inschrift der einen Gedenktafel lautet: <i>&quot;Denkmal
        des Hochw. Herrn Franz Lorenz Grassl, Missionar aus Ruhmannsfelden, geb.
        18.8.1753 als Sohn des hiesigen Lederermeisters. In seinen Studienjahren
        z&auml;hlte ihn der hochselige Bischof Sailer zu seinem innigsten
        Freunde. Nach wenigen Jahren priesterlicher T&auml;tigkeit verlie&szlig;
        er mit gr&ouml;&szlig;tem Schmerze seine liebe Heimat und kam am
        10.10.1887 in Amerika an, wo er als eifriger Missionar recht segensreich
        wirkte. Wegen seiner ganz vorz&uuml;glichen Natur- und Geistesgaben
        wurde er auf der ersten Synode der neuen Republik zum Coadjutor-Bischof
        von Baltimore gew&auml;hlt. W&auml;hrend die Tatsache seiner Wahl zur
        Best&auml;tigung nach Rom geschickt wurde, versah er immer noch zu
        Philadelphia das Amt des unerm&uuml;dlichen Missionars um eben diese
        Zeit, als dort die schreckliche Pest w&uuml;tete, bis endlich auch er
        als Opfer der Liebe und des Seeleneifers, Opfer dieser t&uuml;ckischen
        Krankheit erliegen mu&szlig;te am Ende des Jahres 1793. Sein Andenken
        bleibt in Segen.&quot;</i><br>
        Die Inschrift der zweiten Gedenktafel lautet: <i>&quot;Zur frommen
        Erinnerung an den hochw. Herrn Franz Xaver Fromholzer, Pfarrer der
        Vierzehnnothelferkirche in Gardenwille, Di&ouml;zese Buffalo,
        Nordamerika, geboren zu Ruhmannsfelden, zum Priester geweiht am 25. Juli
        1875 in Brixen, Tirol, wirkte &uuml;beraus segensreich 16 ½ Jahre in
        Springwille, Uschford, Scheldorn und Gardenwille, wo er am 4.3.1893
        wohlvorbereitet und ergeben in den Willen Gottes im 42. Lebensjahre
        verschied. R.I.P.&quot;</i><br>
        Die Inschrift der dritten Gedenktafel lautet<i>: &quot;Andenken an die
        ehrw. Missionsschwester Mr. Agnes Holler; Metzgermeisterstochter von
        Ruhmannsfelden, die am 13. August 1904 bei einem t&uuml;ckischen
        &Uuml;berfall der Missionsstation Baining auf Neupommern, wo sie mit
        einigen Br&uuml;dern und Schwestern zur Erholung und zur Feier der
        Einweihung der neuen Kapelle weilte, durch Keulenhiebe get&ouml;tet
        wurde und so als jugendliches Opfer von 23 Jahren f&uuml;r das Reich
        Gottes, dessen Ausbreitung ihr als sch&ouml;nste Lebensaufgabe galt, zur
        unverwelklichen Krone der Herrlichkeit gelangte. R.I.P.&quot;</i><br>
        <i> </i>Im Jahre 1910 wurde von dem Orgelbaumeister Herrn Ludwig
        Edenhofer in Deggendorf eine neue pneumatische Orgel mit 2 Manualen, 22
        klingenden Registern, 3 Bassregistern, 2 Koppelungen und 3
        Druckkn&ouml;pfen aufgestellt. Die Prospektzinnpfeifen wurden 1917
        w&auml;hrend des Krieges an den Staat abgeliefert, sind aber durch die
        Bem&uuml;hungen des derzeitigen Pfarrvorstandes, H. Hr. Pfarrer und
        Schuldekan Fahrmeier wieder neu beschafft. Am 17.8.1925 wurden die 43
        Prospektzinnpfeifen von der Orgelbauanstalt Weise in Plattling geliefert
        und in die Orgel eingebaut. Ebenso wurden die zwei kleineren Glocken von
        den vier Kirchenglocken w&auml;hrend des Krieges abgeliefert. Durch das
        unerm&uuml;dliche Bestreben des H. Hr. Pfarrers Fahrmeier wurde es
        erm&ouml;glicht, dass auch die beiden abgelieferten Glocken bald wieder
        nach beschafft werden konnten. Die dritte Glocke kam am 12. Dezember
        1924 und die vierte am 1. Juli 1926 wieder auf den Turm. Die beiden
        Glocken stammen aus der Glockengie&szlig;erei Gugg in Straubing und sind
        10 und 5 Zentner schwer, w&auml;hrend die zwei gro&szlig;en Glocken ein
        Gewicht von 28 und 20 Zentnern haben.<br>
        In Jahre 1911 wurde die Friedhofmauer unter Leitung des Baumeisters
        Gegenfurtner neu aufgef&uuml;hrt. 1916 kam eine neue Uhr auf den Turm,
        geliefert von der Turmuhrenfabrik E. Strobl, Regensburg. 1920 bekam die
        Laurentius-Pfarrkirche elektrische Innenbeleuchtung. Am 1. Mai 1920
        erstrahlte die &quot;Maienk&ouml;nigin&quot; auf dem Hochaltar in der
        Laurentius-Pfarrkirche zum ersten Male im elektrischen Lichterkranze.
        1920 und 1927 wurden Kuppel, Turm und Au&szlig;enmauern der Kirche
        renoviert. Im April 1928 wurde die Pfarrkirche durch die
        Entstaubungsfirma M&uuml;ller, M&uuml;nchen entstaubt. Seitdem erscheint
        das Innere der Kirche wie in ganz neuem Gewande.<br>
        Am 31. Oktober, 1. und 2. November 1928 wurde das 100-j&auml;hrige
        Bestehen der jetzigen Laurentius-Pfarrkirche durch ein Triduum festlich
        begangen. Die zwei Redemptoristenpatres H. Hr. P. Braig und H. Hr. P.
        Waldinger waren eigens von Deggendorf hierher gekommen. Bei den
        Predigten und Gottesdiensten war die festlich geschm&uuml;ckte Kirche
        immer voll von Gl&auml;ubigen.<br>
        In herrlichen Worten legten die beiden H. Hr. Festprediger Wert und
        Bedeutung der katholische Kirche und ihrer Gnadenmittel dar.
        &Uuml;berzeugend eiferten sie die Zuh&ouml;rer an immer treue Katholiken
        zu sein, die nicht blo&szlig; selbst den Glauben bet&auml;tigen, sondern
        auch stets f&uuml;r ihre kath. Kirche eintreten und f&uuml;r sie
        streiten und k&auml;mpfen. &Uuml;beraus gro&szlig; war der Andrang beim
        Sakramentenempfang. Am Schluss des Triduums zog eine feierliche,
        imposante Prozession durch den Markt, bei der sich s&auml;mtliche
        Beh&ouml;rden, alle Vereine und die Pfarrangeh&ouml;rigen in sehr
        gro&szlig;er Zahl beteiligten. Sollte doch diese Prozession der
        sichtbare, &auml;u&szlig;erliche Ausdruck des gro&szlig;en, innigen
        Dankes dem lieben Gott gegen&uuml;ber daf&uuml;r sein, dass die Pfarrei
        Ruhmannsfelden ein so sch&ouml;nes Gotteshaus bekommen hat, wie es die
        Laurentius-Pfarrkirche Ruhmannsfelden in ihrem jetzigen, wohl gepflegten
        Zustande ist. Am 10. August jeden Jahres begeht die Pfarrei
        Ruhmannsfelden das Fest ihres Kirchenpatrons, des hl. Laurentius.<br>
        M&ouml;chten die Gl&auml;ubigen der Pfarrei Ruhmannsfelden niemals
        vergessen, was unsere Vorfahren f&uuml;r gro&szlig;e Opfer gebracht
        haben, um ein sch&ouml;nes und w&uuml;rdiges Gotteshaus ihr Eigen nennen
        zu k&ouml;nnen. M&ouml;chten sie allezeit daran denken, wie viele
        Wohltaten und Segnungen dieses Gotteshaus schon vermittelt hat, dann
        werden sie nicht blo&szlig; zu danken wissen, sondern alles daransetzen,
        dass die sch&ouml;ne Laurentius-Pfarrkirche in Ruhmannsfelden f&uuml;r
        alle Zeiten wahrhaftig ist <i>„domus et porta coeli et vocabitur aula
        die“ </i>das Haus Gottes und die Pforte des Himmels und sein Name ist <i>„aula die“</i>, die Wohnung Gottes.</p>
