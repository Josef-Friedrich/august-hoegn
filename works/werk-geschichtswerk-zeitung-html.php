      <h3>HAUPTTEIL</h3>
      <h2><b>I.</b> Geschichtliches vom Markt Ruhmannsfelden,<a name="i"></a><br>
        <font size="-1">&quot;Durch G&auml;u und Wald&quot;, Beilage zum
        Deggendorfer Donauboten, 6.11.1926 und 15.12.1926</font>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a> </h2>
      <h3><b>1.</b> Der Name &quot;Ruhmannsfelden&quot;<a name="i1"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h3>
      <p>In einer der fr&uuml;heren Nummern dieser Beilage lesen wir in einer
        geschichtlichen Abhandlung vom Markte Ruhmannsfelden, dass man sich noch
        &uuml;ber die Herkunft des Namens Ruhmannsfelden im Unklaren ist. Ich
        fragte meinen Tischnachbarn, einen alten Ruhmannsfeldner B&uuml;rger, ob
        er den nicht wisse, woher der Name Ruhmannsfelden kommen m&ouml;ge.
        &quot;Das ist doch sehr einfach. Ruhmannsfelden - ruht der Mann im
        Felde,&quot; war die schlagfertige Auskunft. Nun musste ich ihm doch zu
        bedenken geben, dass er den Namen anders ausspricht, als er ihn deutet,
        denn er m&uuml;sste konsequenter Weise dann Ruhtmannsfeldn oder
        Ruhemannsfeld aussprechen. So aber spricht jung und alt den Namen ganz
        richtig, gem&auml;&szlig; seiner Herkunft, Rumarsfeldn aus. In einer
        Oberalteicher Urkunde (1184) erscheint der Name Rumarsfelden, in einer
        Niederalteicher Urkunde Rudmarsfelden. Im Jahre 1394 schrieb man
        Rumatzfelden. Und nun kommt f&uuml;r Ruhmannsfelden die schwere
        Zerw&uuml;rfniszeit mit dem Kloster Gotteszell.<br>
        Die Ruhmannsfeldner durften nicht mehr ihr bisheriges eigenes Siegel
        f&uuml;hren. Nur was das Kloster Gotteszell mit seinem Siegel
        best&auml;tigte, war g&uuml;ltig. Da fertigten sich die schlauen
        Ruhmannsfeldner ein anderes Siegel an. Zwischen zwei Krummst&auml;ben
        war ein Kissen und auf diesem lag eine R&uuml;be. In einer Urkunde von
        1448 erscheint der Name Ruebmannsfelden (R&uuml;bmannsfelden). Das
        Siegel musste selbstverst&auml;ndlich auf Befehl des damaligen Abtes
        sofort verschwinden. Aber die Schreibweise des Namens
        R&uuml;bmannsfelden und die Erkl&auml;rung, dass der Name Ruhmannsfelden
        von R&uuml;be, Symbol des Ackerbaues, des Feldbaues, herstamme, pflanzte
        sich noch lange fort. Es ist anzunehmen, dass man sp&auml;ter ein
        weiters Siegel angefertigt hat und zwar einen im Felde ruhenden Mann. In
        einer Finkschen Karte aus der 2. H&auml;lfte des 17. Jahrhunderts
        erscheint pl&ouml;tzlich der Name Ruemannsfelden. Seit dieser Zeit mag
        wohl auch die Namensdeutung &quot;ruht der Mann im Felde&quot;
        traditionell geworden sein. Auch H. Hr. Pfarrer Castenauer hat, diese
        Deutung des Namens vom H&ouml;rensagen in seine Beschreibung vom Markte
        Ruhmannsfelden aufgenommen. Die Formen Ruhmarsfelden, Rudmarsfelden, die
        der Volksmund best&auml;tigt, die tragen den Stempel der
        Originalit&auml;t fort.<br>
        Die erste H&auml;lfte des Namens ist der Personenname Hrothmar
        (Hruothmar= Romar) Hrot(i) = Ruhm, Sieg; mar(u) = ber&uuml;hmt. Hrothmar
        = also: der Siegber&uuml;hmte. Ruhmarsfelden = das Feld (Bereich,
        Bezirk) des Siegber&uuml;hmten.<br>
        Mein lieber Tischnachbar! Die Namen Ruebmannsfelden, Ruhemannsfelden,
        sind l&auml;ngst verwischt, weil aus Konfliktsstoffen, aus Zank und
        Streit heraus geboren. Die R&uuml;be sollte den gestrengen Klosterherren
        zeigen, dass der unbeugsame Wille der trutzigen Ruhmannsfeldner noch
        lange nicht gebrochen, wenn auch die Krummst&auml;be drohend &uuml;ber
        ihrem Haupte sich regten. Und es wurde der Trotz der Ruhmannsfeldner
        doch gebrochen. Ruhmannsfelden und seine B&uuml;rgerschaft musste die
        Widerspenstigkeit gegen das Kloster Gotteszell sehr stark
        b&uuml;&szlig;en. 30-jahriger Krieg und &ouml;sterreichischer
        Erbfolgekrieg schlugen Ruhmannsfelden schwere Wunden. Erst in der 2.
        H&auml;lfte des 17. Jahrhunderts gewannen die B&uuml;rger, wieder das
        Vertrauen zu ihrem geistlichen Oberherrn. Es kehrte Ruhe und Friede ein
        (ruht der Mann im Feld), nachdem Jahrhunderte lang Unruhe und Unfriede
        die Mauern des einst unter Aldersbacher Klosterherrschaft
        aufbl&uuml;henden Marktes Ruhmannsfelden erf&uuml;llte. Die Deutung des
        Namens k&ouml;nnen wir also unm&ouml;glich aus dem 14. 15. oder 17.
        Jahrhundert herleiten, nachdem der Ort Ruhmannsfelden seit dem Jahre
        1100 besteht und der Name Rumarsfelden sich aus der Mitte des 12.
        Jahrhunderts urkundlich nachweisen l&auml;sst.<br>
        Sind wir froh, dass der Name Rumarsfelden auch keine &auml;ndere Deutung
        zul&auml;sst. Unser Stolz ist der Name unseres Heimatortes
        Ruhmannsfelden, welch letzterer einstens der Wohnsitz eines
        siegber&uuml;hmten Helden war, der im internationalen Wettkampf auf
        Schweizer Boden den Sieg erringen und mit der Qualifikation
        &quot;Siegber&uuml;hmter&quot; an den Ort zur&uuml;ck konnte, der nach
        ihm den Namen &quot;Ruhmannsfelden&quot;, das ist &quot;Feld des
        Siegber&uuml;hmten&quot; erhielt.</p>
      <h3><b>2.</b> Die Bezeichnung „Markt“<a name="i2"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h3>
      <p>Aichinger schreibt in &quot;Kloster Metten und seine Umgebung&quot;,
        dass sich in der letzten Zeit der Regierung Aldersbachs Ruhmannsfelden
        zum Markt emporgeschwungen hat. Wann die Markttitelverleihung und von
        wem sie stattgefunden hat, ist urkundlich nicht nachzuweisen, wie eben
        an so vielen anderen Pl&auml;tzen auch. Pl&ouml;tzlich taucht dann in
        der einen oder anderen Urkunde der Titel „Markt&quot; oder
        „Stadt&quot; auf.<br>
        Jakob der Rueerer stellte am 26. April 1416 eine Urkunde aus, in welcher
        er sich <i>„dy czeitt Richter dez Markehtz zue Ruedmansfelden&quot;</i> nennt. Es d&uuml;rfte kaum ein Zweifel bestehen, dass der
        Urkundenaussteller als landesherrlicher Richter &uuml;ber die
        Markteigenschaft seines Wirkungsortes Bescheid wusste. Wir sind deshalb
        berechtigt, die Markteigenschaft zu Ruhmannsfelden schon seit dem
        Beginne des 15. Jahrhunderts in Anspruch zu nehmen. In einem
        Aldersbacher Codex vom Jahre 1452 ist die Rede von dem forum
        Rudmansfelden, also Markt, w&auml;hrend in einer Urkunde vom 2. April
        1475 das opidum Rudmansfelden erscheint, was mehr an die befestigte
        Siedlung als an den Markt gemahnt. Auf Bitten der &quot;Burger unnsers
        Margkts zu Rudmansfelden&quot; tut ihnen Herzog Albrecht IV. von
        Bayern-M&uuml;nchen die Gnade: &quot;...Freyen sie auch wissenlich in
        crafft des briefs, Also das sy und all Ir nachkomen, sich aller der
        gnaden vnd freihait geprauchen vud nyessen sollen, In allerma&szlig; als
        annder vnser M&auml;rkt, In Nidern Baiern, von unsern vordern gefreyt
        sein.&quot;<br>
        Das Privileg ist nur in Abschrift erhalten und undatiert, steht aber
        zwischen zwei Urkunden desselben Jahres 1469 und darf daher als aus
        diesem Jahre stammend angenommen werden. In einem Literale des Klosters
        Gotteszell vom Jahre 1566 - 1602 kommen vor die „Geschworenen des Rats
        und Markts R.&quot;, &quot;Rat und Gemein des Markts R.&quot;, „Die
        Geschworenen des Rats und ganze B&uuml;rgerschaft des Markts R.&quot;,
        wie &uuml;berhaupt seit der Begnadigung von 1496 keinerlei Zweifel an
        dem Marktrechte Ruhmannsfeldens mehr aufkommen kann.</p>
      <h3><b>3.</b> Das Wappen von Ruhmannsfelden<a name="i3"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h3>
      <p>Im Laufe der verschiedenen Zeiten hat man in Ruhmannsfelden nicht
        immer ein und dasselbe Wappen oder Siegel gef&uuml;hrt. &Uuml;ber das
        urspr&uuml;ngliche Wappen, das Ruhmannsfelden zur Ritterszeit, also im
        12. Jahrhundert, f&uuml;hrte, ist uns gar nichts mehr bekannt.
        Sp&auml;ter hatte man das Wappen, das Muelich, Apian und das Wappenbuch
        der Landschaft bringen, n&auml;mlich: &quot;in Blau unter zwei
        schr&auml;g gekreuzten silbernen Hirtenst&auml;ben eine wei&szlig;e
        R&uuml;be mit gr&uuml;nen Bl&auml;ttern.&quot; Dieses Wappen ist ein
        sogenanntes redendes Wappen, welches ohne jede Autorisierung
        l&auml;ngere Zeit gebraucht worden zu sein scheint. Die Hirtenst&auml;be
        w&uuml;rden lediglich als Dekoration anzusehen sein. Die R&uuml;be hat
        man als Wappenbild angenommen und man hat lange Zeit Rub = Rueb,
        R&uuml;b = R&uuml;ebmannsfelden geschrieben. Dieses Wappen ist
        abgebildet als Nr. 541 im Philipp Apians Wappensammlung der
        altbayerischen Landschaft, wie des zu seiner Zeit abgegangenen Adels
        (Oberbayerisches Archiv XXXIX 471- 498). Als es um 1650 an der Kirche zu
        Ruhmannsfelden ohne jede Genehmigung angebracht worden war, erhob
        dagegen P. Gerard Abt bei dem Kloster Gotteszell Protest: <i>„Die
        Ruedtmansfelder haben mit ihrer Unvernuenfftiegen Rueben auff dem
        hh&uuml;&szlig; on Verstandt, ganz Verme&szlig;lich und das Closter
        gehandlet … Wer hat ihnen ainmall ain wappen zue fueren erlaubt? Vnd
        wan sie gleich Wappenmessig waren, Wer hat ihnen erlaubt solches auf der
        Kirchen spesa auff ainem offnen thurm mallen zuelassen, alwo ihnen
        Ainiges Recht und herschaft nit zuestehet und gebiertt? Vnd was ist das
        f&uuml;r ain Verstandt ia phantastische Einbildund ain Rueben auf einem
        hh&uuml;ss?“ </i>Schade, dass eine Fortsetzung der Korrespondenz
        &uuml;ber das Wappen und seine Anbringung am Kirchenturm nicht vorhanden
        ist. &Uuml;ber das andere Wappen, das einen im Felde ruhenden Mann
        dargestellt soll haben, kann kein Aufschluss erholt werden.<br>
        Das heutige Wappen des Marktes Ruhmannsfelden weist „in Rot ein von
        Silber und blau in 2 Reihen geweckten Schr&auml;grechtsbalken&quot; auf.
        Zu dieser von 0. Hupp (Die Wappen und Siegel der deutschen St&auml;dte,
        Flecken und D&ouml;rfer, Frankfurt a. M. 1912, S.84) gegebenen
        Beschreibung f&uuml;gt der Verfasser noch erl&auml;uternd hinzu: <i>„Es
        ist gar kein altes Siegel bekannt geworden, sodass es fraglich ist, ob
        das beschriebene Wappen, das die B&uuml;rgermeistermedaille und ein nach
        dieser gefertigtes Magistratssiegel zeigen, das ist, das Ruhmannsfelden
        fr&uuml;her gef&uuml;hrt hat.&quot; </i>Die wei&szlig;-blauen Rauten im
        Schr&auml;grechtsbalken sagen uns, dass das Wappen, den Ruhmannsfeldnern
        von einem bayerischen Herzog (wei&szlig;-blau) verliehen wurde f&uuml;r
        besondere Tapferkeit auf blutgetr&auml;nktem Schlachtfelde (roter
        Untergrund im Wappen.)</p>
      <h3><b>4.</b> Schloss und Schlossberg Ruhmannsfelden<a name="i4"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h3>
      <p>In fr&uuml;hesten Zeiten waren die H&ouml;hen hiesiger Gegend mit
        Urwald bedeckt und die T&auml;ler mit Moosen, S&uuml;mpfen und Seen
        ausgef&uuml;llt, Avaren drangen durch dieses unwegsame Gebiet aus
        B&ouml;hmen heraus bis an die Donau vor, um die Kl&ouml;ster auf dem
        linksseitigen Donauufer auszurauben. Die bayerischen Volksherz&ouml;ge
        trieben diese Avaren wiederholt zur&uuml;ck. Erst Karl d. Gr. trat
        diesem R&auml;ubergesindel wirksamer entgegen. Zugleich schenkte er das
        ganze Gebiet und Ruhmannsfelden herum (Grenzline Kohlbach, Voglsang,
        K&ouml;ckersried, Eckersberg, Unterauerkiel, Asbachm&uuml;ndung,
        Altnu&szlig;berg, Seigersdorf, Fernsdorf, Frankenried, Hornberg,
        Einweging, Schusterstein, &Ouml;dwies, Hirschenstein, Kalteck, Voglsang)
        dem Kloster Metten. Allerdings war damit dem Kloster Metten eine riesige
        Arbeitslast aufgeb&uuml;rdet (Rodungsarbeit) und die Christianisierung
        der Bewohner die Urwaldgebietes mag keine leichte gewesen sein.
        Unverdrossene M&ouml;ncharbeit des Rodungsklosters Metten hat aber trotz
        aller Hindernisse hier in unserem Heimatgebiet, damals
        &quot;Nordgau&quot; genannt, die M&ouml;glichkeit zur ersten Besiedlung
        dieses Gebietes geschaffen. Da pl&ouml;tzlich kommt der Bayernherzog
        Arnulf, mit dem Beinamen &quot;der B&ouml;se&quot; und nimmt das ganze,
        dem Kloster Metten geh&ouml;rige Gebiet, diesem ab (S&auml;kularisation)
        und schenkt es seinem Getreuen, dem Grafen von Bogen. Graf Aswin, ein
        Sohn des Grafen Hartwig von Bogen und dessen Gemahlin Bertha, eine
        Tochter des Ungarnk&ouml;nigs Bela I., erbte nach dem Tode seines Vaters
        die G&uuml;ter im Nordgau, also auch das ganze Gebiet um Ruhmannsfelden
        herum. Es wurden in dieser Zeit auf den H&ouml;hen Burgen gebaut, die
        den Grafen (Schirmv&ouml;gten, Adeligen, Edelleuten) als Wohnsitz
        dienten. Unten in den T&auml;lern wurden auch Burgen gebaut, abseits vom
        Wege, ganz versteckt, von Weihern umgeben, die den Dienstmannen der
        Grafen als Wohnsitz dienten und die als Verbindungslinie zwischen den
        einzelnen den H&ouml;henburgen zu denken sind. (Nussberg = Linden,
        Ruhmannsfelden = Wei&szlig;enstein). Bis jetzt hat man geschrieben und
        gesprochen, es sei auf der sogenannten Leithanh&ouml;he bei
        Ruhmannsfelden ein Schloss gestanden und man bezeichnet immer noch den
        Berg als Schlossberg, ja man glaubt mit Bestimmtheit die Stelle zu
        wissen, wo einstens das vermeintliche Schloss gest&auml;nden sei. Es
        sind aber nicht die mindesten Anhaltspunkte vorhanden, annehmen zu
        m&uuml;ssen, dass hier in Ruhmannsfelden eine Burg, ein Schloss, auf
        genannter H&ouml;he gestanden habe. Wir lesen, dass die etlichen Ritter,
        die hier ihren Wohnsitz hatten, Dienstmannen, Ministeriale der Grafen
        von Bogen waren und diesen in Allem unterstanden. Diese Ministerialen
        werden nicht freie Auswahl gehabt haben, sie werden vielmehr ihren
        Wohnsitz in ihr Arbeitsgebiet verlegt haben und die Bauart ihres
        Wohnsitzes wird sich von der des Wohnsitzes des Grafen, des Adeligen,
        wesentlich unterschieden haben. Die Sippe, aus der der Ministeriale
        stammte, oder der er ihr vorstand, wird ihre Wohnungen in unmittelbarer
        N&auml;he der Wohnung ihres Ministerialen gehabt haben. Wenn das richtig
        ist, was Aichinger, Kiendl schreiben, <i>&quot;da&szlig; um die Burg
        Ruhmannsfelden herum eine kleine Ortschaft entstanden ist&quot;</i>, so
        k&ouml;nnen wir unm&ouml;glich weiterhin auf der sogenannten
        Leithenh&ouml;he bei Ruhmannsfelden ein Schloss suchen und finden
        wollen, wo niemals eines gestanden. Vielmehr m&uuml;ssen wir aus der
        baulichen Entwicklung des Ortes Ruhmannsfelden schlie&szlig;en auf den
        fr&uuml;heren Standort der Burg. Die ganze Anlage des Ortes
        Ruhmannsfelden ist augenf&auml;llig nicht von oben (Leithenh&ouml;he)
        nach unten, sondern von unten (Bachgasse) nach oben entstanden. Die
        tadellos erkennbare Vierecksanlage des Bachgassen-Viertels weist uns
        augenscheinlich darauf hin, dass das einstens <i>&quot;die kleine
        Ortschaft war, die um die Burg herum entstanden ist.&quot;</i> (Aichinger, Kiendl). Die &auml;ltesten Gewerbe - Schmid, Wagner, Gerber,
        Stricker, Seifensieder usw. - und die &auml;ltesten H&auml;user finden
        wir in diesem Teil des heutigen Marktes Ruhmannsfelden. Also m&uuml;ssen
        wir doch da, wo die ersten Spuren der baulichen Entwicklung des Ortes
        und die Anf&auml;nge des gesellschaftlichen und gesch&auml;ftlichen
        Lebens und Treibens der ersten Ansiedler unseres Heimatortes zu suchen
        sind, auch den Wohnsitz des Ministerialen des Grafen von Bogen die Burg-
        und die H&auml;useranlage der Sippe um die Burg herum unm&ouml;glich auf
        der Leithenh&ouml;he, sondern im Bachgassen-Viertel, suchen.
        Irregef&uuml;hrt hat man wiederholt auf der Leithenh&ouml;he
        Nachforschungen nach dem Schlosse angestellt. Besonders Eifrige haben
        sogar mit Pickel und Schaufel Grabungen vorgenommen. Was dabei gefunden
        wurde, das waren einige verroste S&auml;beln, Hufeisen, Geldst&uuml;cke,
        Sachen, die w&auml;hrend der Kriegsjahre sp&auml;terer Jahrhunderte
        versteckt wurden und die an anderen Pl&auml;tzen auch zu finden sind. Es
        ist weder eine bauliche Anlage eines fr&uuml;heren Schlossen zu
        erkennen, noch sind sonstige Anhaltspunkte zu finden, die ein ehemaliges
        Vorhandensein eines Schlosses auf der vermeintlichen Stelle
        best&auml;tigen k&ouml;nnten. Der tiefe Brunnen, der unterirdische Gang,
        der Schlosskeller mit dem uralten Wein, die eiserne Kiste voll Gold usw.
        das geh&ouml;rt alles in das Reich der Sage. Sogar im Gemeindekataster
        finden wir auf Grund irrt&uuml;mlicher Angaben (1835-43) den Eintrag
        &quot;am Haus&quot; an der Stelle, an der der Eintrag gar keine
        Berechtigung hat. Die Heimatforschung hat sich niemals mit der Frage:
        Gab’s ein Schloss Ruhmannsfelden und wo stand dieses? ernstlich
        besch&auml;ftigt, sonst h&auml;tte man schon l&auml;ngst darauf kommen
        m&uuml;ssen, dass die Benennungen „Schloss Ruhmannsfelden,
        Schlossberg&quot; keine Berechtigung haben, dass die Burg Ruhmannsfelden
        im heutigen Bachgassen-Viertel stand und sich die Ortschaft
        Ruhmannsfelden von hier aus entwickelt hat.</p>
      <h2><b>II.</b> Geschichtliches vom Markt Ruhmannsfelden,<a name="ii"></a><br>
        <font size="-1">„Durch G&auml;u und Wald“, Beilage zum Deggendorfer
        Donauboten, 20.8.1927, Chronik der Grundschule Ruhmannsfelden</font>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a> </h2>
      <h3><b>1.</b> Von der Schule in Ruhmannsfelden in fr&uuml;herer Zeit bis
        1835<a name="ii1"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <p>M&auml;rkte und St&auml;dte haben sich im 13. und 14. Jahrhundert
        durch Gewerbeflei&szlig; und Handel m&auml;chtig empor geschwungen und
        diese Regsamkeit in damaliger Zeit hatte Wohlhabenheit zu Folge und die
        &Uuml;berlegenheit im Gesch&auml;fte und im Wohlstande des Einzelnen vor
        dem anderen macht auch geistige &Uuml;berlegenheit zur Notwendigkeit.
        Das Lernen wuchs aus dem wirtschaftlichen Aufschwunge der damaligen von
        sich selbst heraus und macht ohne weiteres das Bed&uuml;rfnis nach
        Schulen geltend. Solche Schulen gab es zun&auml;chst nur in den
        St&auml;dten. Auf dem Land gab es nur den wandernden Volksschullehrer.
        Ob ein solcher sich hier oder in n&auml;chster Umgebung aufgehalten hat,
        wissen wir nicht.</p>
      <h4><b>a.)</b> Die ersten Schullehrer in Ruhmannsfelden<a name="ii1a"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h4>
      <p>Bei Aichinger „Kloster Metten und seine Umgebung“ lesen wir Seite
        328, dass 1503 Ruhmannsfelden in den Besitz des Klosters Gotteszell kam
        und dass von der incorpierten Pfarrei Geiersthal ein Expositus nach
        Ruhmannsfelden geschickt wurde. Dieser Expositus hat hier wohl auch den
        Kindern den Religionsunterricht erteilt. 1558/59 fanden auf Anordnung
        Herzog Albrecht V. &uuml;berall im Lande Schulvisitationen statt. Aus
        Trellingers „Beitr&auml;ge zur Geschichte des Schulwesens im
        Bezirke“, Bayerwald 1925, Seite 105, entnehmen wir, dass um jene Zeit
        in Ruhmannsfelden schon eine Schule bestanden hat. Aus den
        Visitationsprotokollen schreibt Trellinger in genannter Abhandlung
        &uuml;ber Schule Ruhmannsfelden folgendes: <i>„Pfarr Geirstall. Die
        von Ruebfelden (Ruhmannsfelden) haben einen Schulmeister, den nennen sie
        f&uuml;r sich selbst auf. Ist ain Niernberger; hab (der Pfarrer) mit Im
        nichts zu thun.--  Zu Ruebenfelden (ist) ain vacierende me&szlig;, von
        dem einkommen wird der Schulmeister bes&ouml;ldt.“ </i>Die
        Zechpr&ouml;bste von Sankt Lorenz in <i>„Ramffelden Markht, Filialgen
        Geirstall“</i> geben an: <i>„Vaciert ein Me&szlig;, welche einkommen
        bei 4 Jarlang 18 fl. davon hatlten sy ain Schuelmeister, geben ime
        j&auml;hrlich 4 Pfund Regensburger. Schuelmeister zu Ramanffelden,
        Casparn Stralnberger von Niernberg p&uuml;rtig, 6 Jahre allda gewest. Zu
        Leipzig studiert, hat mit testimonium. Hat 10 schueler, darunter je
        zwen, so gute Ingenia haten, lernen gemeinlich erst lesen. Singt zu
        Chor. Ist der alten Religion. Underweist der knaben zur Peicht und
        Empfahung des Sakraments catholice, desgleichen zu andern Sacrament,
        Gottesdienst, Predigt. Hat kein Superattendenten. Besoldung hat er aus
        der Bruderschaft 4 Pfund Regensburger. Am Rath daselbst Ime aufzunemen.
        Petten alle Morgen das Vaterunser, den grues und glauben. Singen keinen
        neuen rueff. Die Knaben seien eines geringen Verstandes, derhalben sy
        kain comedi oder declamation halten“<br>
        </i>In einer Urkunde von 1550 kommt als Zeuge vor ein Andr&auml;
        Wei&szlig;peckh, Schulmeister in Ruhmannsfelden und auch im 17. und 18.
        Jahrhundert berichten Urkunden von Schulmeistern in Ruhmannsfelden, die
        zugleich auch Mesner waren. Diese Schulmeister hatten meist selbst ein
        Besitztum und unterrichteten die wenigen Kinder die freiwillig lesen,
        schreiben und rechen lernen wollten in ihrer Behausung. So lesen wir in
        einer Urkunde: <i>„Unserm 5.11.1658 verkauften Georg Pitter,
        B&uuml;rger und gewester Schulmeister zu Ruhmannsfelden und dessen Frau
        Eva ihre Leibgedingsgerechtigkeit auf einem Lehen zu Ruhmannsfelden mit
        Br&auml;ugerechtigkeit dem Hans Eybeck, B&uuml;rger und Metzger daselbst
        und seiner Frau Margareta um 365 fl.“<br>
        </i>Am 5.7.1702 &uuml;bergibt Rosina Hinderholzer, verwitwete
        Schulmeisterin zu Ruhmannsfelden ihre Markbehausung am Kalteck an ihren
        Tochtermann Martin Staudenberger, B&uuml;rger und Schneider in
        Ruhmannsfelden.<br>
        Am 26. April 1779 starb hier Schullehrer Bernhard Hochreiter kunstvoller
        Orgelspieler, 52 Jahre alt.<br>
        1784 wirkte hier ein Schullehrer Adalbert Hermann und gepr&uuml;fter
        Eremit Franz Pitsch bei Gotteszell als Adstant. Eremit Pitsch hatte auf
        dem Kalvarienberge in Gotteszell ein kleines H&auml;uschen und in diesem
        erteilte er Unterricht an die Gotteszeller und Ruhmannsfeldner Jugend.
        Da aber im Laufe der Zeit den Ruhmannsfeldner Kindern der Weg nach
        Gotteszell, zumal bei schlechtem Wetter, zu beschwerlich war, ging der
        Eremit Pitsch nach Ruhmannsfelden und erteilte hier mit Hermann den
        Unterricht. Pitsch war noch 1776 Schulhalter zu Walchsee, Gericht
        Kufstein. Nach seinem Tode &uuml;bernahm der damalige Abt Amad&auml;us
        selbst im Gotteszell die Kinder unterrichtet.<br>
        Handschriftliche Aufzeichnungen im hiesigen gemeindlichen Archiv sagen
        folgendes: <i>„bey Aufhebung des Klosters Gotteszell hat man die
        Wohnung des zeitlichen Pfarr-Vikars, als auch des Schullehrers und
        Messners nicht f&uuml;r zweckm&auml;&szlig;ig befunden. So kam man auf
        den ungl&uuml;cklichen Gedanken beyde Wohngeb&auml;ude zu verkaufen und
        daf&uuml;r ein einziges, zwar gr&ouml;&szlig;ers, aber auch schon sehr
        bauf&auml;lliges Haus anzukaufen und darin den Pfarrer samt Kooperator,
        Lehrer, Gehilfen und 200 Schulkinder und den Messner, - im
        buchst&auml;blichen Sinne des Wortes – zusammen zu pressen.“</i><br>
        Laut landesgerichtlichen Protokolle vom 27. Juni 1803 wurden die
        Geb&auml;ude von Max Freiherrn von Antritzky gegen das alte Schulhaus
        und das Pfarr-Vikariatsh&auml;usl und eine bare Daraufgabe von 1 800 fl
        erworben und hierauf im Jahre 1804 zur Pfarr-, Schullehrers- und
        Messnerwohnung, dann zum Schullokale hergestellt. Der f&uuml;r die
        Schullehrers- und Mesnerwohnung, dann Schullokal bestimmte Anteil
        infolge Entschlie&szlig;ung das Staatsministerium des Innern vom 10.
        April 1832 um 500 fl. erworben.<br>
        Die &uuml;brigen Realit&auml;ten sind bei der S&auml;kularisation des
        Klosters Gotteszell infolge der Verordnung vom 9. September 1803 an den
        Staat als Eigent&uuml;mer &uuml;bergegangen und bei der Organisation der
        Pfarrei Ruhmannsfelden als Pfarr-Widdums Realit&auml;ten bestimmt
        worden. (1803 war Ruhmannsfelden noch keine Pfarrei. Pfarrprovisor war
        vom 21. M&auml;rz 1803 bis 1. Oktober 1905 Franz Joseph Haindl. F&uuml;r
        einen Hilfspriester hatte er keine Wohnung. Infolgedessen halten die
        Patres von Gotteszell aus und zwar: Hr. P. Stivard Sartor 21. April 1803
        bis Ende Oktober 1803. Hr. P. Maria Triendorfer vom November 1803 bis
        Ende Februar 1804. Hr. P. Guido Berger M&auml;rz 1804 bis Ende September
        1805. Haindl musste diese Priesteraushilfen selbst bezahlen.)<br>
        Das sogenannte alte Schulhaus (fr&uuml;her zum Freiherrn von
        Antritzky´schen Anwesen geh&ouml;rig) wurde nach dem Tausch zum 27.
        Juni 1803 von der Berg´schen Familie erworben. Heinrich Berger,
        Bierbr&auml;u von Ruhmannsfelden, der dieses alte Schulhaus
        schenkungsweise von seiner Mutter an sich gebracht hat, verkaufte dieses
        alte Schulhaus an Georg Wurzer, Bauerssohn von March, gelernter
        Br&auml;ubursch nach Zeugnis des Patrimonial-Gerichts.</p>
      <h4><b>b.)</b> Die Schule unter Lehrer Andreas Stern<a name="ii1b"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h4>
      <p>1804 erscheint in den Akten der noch vom Kloster Gotteszell
        aufgestellte Schullehrer Andreas Stern, ein <i>„sehr w&uuml;rdiger
        Schullehrer“,</i> wie es 1810 hei&szlig;t. Anl&auml;&szlig;lich des
        Verkaufes des sogenannten Schulhauses gab es sp&auml;ter einen
        &ouml;fteren Schriftwechsel mit dem Rentenamte Viechtach betreffs
        Andr&auml; Stern. Eine diesbez&uuml;gliche Urkunde sagt:<br>
        <i>„Vom K&ouml;niglichen Rentamte Viechtach:</i><br>
        <i>Andr&auml; Stern, Schullehrer von Ruhmannsfelden hat im Jahre 1804
        bay. Gelegenheit des Verkaufs vom alten Pfarrhof und Schulhaus f&uuml;r
        seine eigent&uuml;mlich auf Erbrecht besessen Stadl und Garten als
        Entsch&auml;digung hief&uuml;r das Mutternackerl Pl.-Nr. 952 und das
        zwaym&auml;dige Weiher- oder Pfarrweisel Pl.-Nr. 447 erhalten und solche
        bis zum Jahre 1833 in Privat Besitz gehabt.</i><br>
        <i>Die Schulstiftung wird hiemit aufgefordert, herkommen zu lassen, auf
        welche Art und gem&auml;&szlig; welchen Vertrag selbe die der vormaligen
        Kloster Gotteszell erbrechtsweise grundbaren Objekte erhalten hat und
        warum zu dessen Ankunfts Titl der grundherrliche Konsens nicht erholt
        wurde, da doch das Kloster Gotteszell und nun der Staat auf den ganzen
        Unfang der Burgfriedens von Ruhmannsfelden die Grundbarkeit zu gaudieren
        hatt.“</i><br>
        Im Jahre 1804 wurden die Schullokale im neu erworbenen Schulhaus neu
        eingerichtet. Laut Ausweis der diesbez&uuml;glichen Rechnungen, die von
        Amad&auml;us Bauer, Abt und kurf&uuml;rstlicher Schulinspektor in
        Gotteszell unterzeichnet und gesiegelt sind, wurden 5 Spundladen, 8
        mittlere Bretter, 7 d&uuml;nne Bretter, vom Hafner Lorenz Pl&ouml;tz ein
        kleiner Ofen, 60 kleine Tintenhaferl, von Johann Reisinger, Schreiner,
        10 B&auml;nke f&uuml;r Kinder, 2 Tafel zum Z&auml;hlen, eine Kanzel mit
        einer Schublade, ein kleines Schamerl geliefert und vom Wolfgang Geiger,
        b&uuml;rgerlicher Glaser- und Zimmerarbeiter-Meister 6 Winterfenster
        eingeglast.<br>
        Dass es zu damaliger Zeit schon Schulpr&uuml;fungen gab und dass bei
        diesen Pr&uuml;fungen an die flei&szlig;igsten Sch&uuml;ler auch Preise
        ausgeteilt wurden, besagt eine Urkunde im gemeindlichen Archiv mit
        folgenden Worten: <i>„Da&szlig; ich die zum Beytrag f&uuml;r die bey
        der Schulpr&uuml;fung der Kinder zu Ruhmannsfelden zu verteilenden
        Preise gn&auml;digst bewilligten 12 fl. von dem Bruderschaftsverwalter
        Baumann richtig erhalten habe, wird hiermit bescheinigt. </i> <i> </i><i>R
        u h m a n n s f e l d e n, den 26. </i><i>Sept. 1804</i><br>
        <i>Frans Joseph Haindl Pfarrprovisor“.</i> Mitteilung vom Bayerischen
        Staatsarchiv Landshut.<br>
        1812 wird Andr&auml; Stern ein Adjunkt beigegeben, den er selbst
        bezahlen soll. Er ist dar&uuml;ber sehr aufgebracht und anscheinend zog
        man den Plan wieder zur&uuml;ck. 1814 ist Stern an Lungenentz&uuml;ndung
        schwer krank, der Adjunkt Georg Lippl von B&ouml;brach wird zur Aushilfe
        herbeigerufen. Lehrer und Gehilfe vertragen sich schlecht. Letzterer
        muss mit der Stallmagd aus einer Sch&uuml;ssel essen.<br>
        In dieser Zeit (1814) war die Industrie-Lehrerin Renate Wuzelhofer und
        die Stricklehrerin Magdalena Schweigerin, Schlossermeisterin von hier,
        an der Schule.<br>
        1816 wird ein neuer Gehilfe, Schuldienst-Expektant Alois Rokinger, von
        Unterviechtach nach Ruhmannsfelden versetzt. Er ist aber nicht
        auffindbar. Daf&uuml;r kommt der Schulpr&auml;parand Andreas Dreseli aus
        Passau. Sein Nachfolger ist 1820 der Expektant Josef Krieger von
        Deggendorf, dann ein gewisser Pichlmeier, 1823 ein Thadd&auml;us Esterl,
        bisher in Langdorf, 1825 folgt Johann Nepomuk W&auml;nninger,
        Aushilfslehrer von Langdorf.<br>
        1829 finden wir Stern noch als Lehrer in Ruhmannsfelden. In der
        Volksschulbeschreibung von 1822 hei&szlig;t es bei Ruhmannsfelden: <i>„Andreas
        Stern, geb. 1766, angestellt 1791, Bef&auml;higung: I. – Einkommen:
        6533 fl. 56 kr.“</i></p>
      <h4><b>c.)</b> Das Schul- und Mesnerhaus<a name="ii1c"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h4>
      <p>1819/20: Die am hiesigen Schul- und Mesnerhause notwendigen
        Bauf&auml;lle mussten unumg&auml;nglich gewendet werden. Nach der hier
        in Abschrift vorliegenden Genehmigung des k&ouml;niglichen Landgerichts
        Viechtach vom 2. Juni 1820 sind hierauf 59 fl. 42 kr. bestimmt worden.
        Die H&auml;lfte hiervon ist nach obiger Genehmigung von den
        Schulgemeinden zu bestreiten. Von der letzten H&auml;lfte hat die
        Marktgemeinde wieder die Halbscheide und die Gemeinde Zachenberg und
        Patersdorf auch die Halbscheide zu bezahlen. Da nun diese Bauf&auml;lle
        f&uuml;r 1819/20 Kosten von 32 fl. 23 kr. erforderten, wor&uuml;ber die
        Bescheinigungen der Kirchen Rechnung f&uuml;r 1819/20 angelegt werden,
        so hat hierzu die Marktkasse den 4. Teilkostenbeitrag bezahlt mit 8 fl.
        5 kr.<br>
        Wie es nun in diesem neu angekauften, aus schon sehr bauf&auml;lligen
        Pfarr-, Schul- und Mesnerhaus ausgesehen haben mag, besagt uns eine
        Mauerer Kostenrechnung vom 22. Mai 1830:<br>
        „<i>Bemerkungen.</i><br>
        <i> Das Mesnerhaus befindet sich im Pfarrhofe linker Hand zu ebener
        Erde und besteht:</i><br>
        <i>1.&nbsp; </i> <i>In einem Vorschlage f&uuml;r eine Dienstmagd<br>
        </i><i>2.&nbsp; </i> <i>In der Wohnstube und Schlafkammer des Mesners</i><br>
        <i>3.&nbsp; </i> <i>In einem Nebenzimmer von welchen man in die Speise
        k&ouml;mmt.</i><br>
        <i>4.&nbsp; </i> <i>Unter den letzteren Breden befindet sich der Keller
        des Me&szlig;ners.</i><br>
        <i>5.&nbsp; </i> <i>Rechter Hand am Ende der Schlafkammer des
        Schulgehilfen (das Lehrerzimmer k&ouml;mmt zuvor) ist endlich die
        K&uuml;che des Mesners.</i> <br>
        <i>6.&nbsp; </i> <i>Der Getreidekoben des Mesners, welcher sich oberhalb
        der Baustube des Pfarrers befindet.</i><br>
        <i>7.&nbsp; </i> <i>Die Stallung, nebst einem ansto&szlig;enden
        Schweinestall.</i> <br>
        <i>8.&nbsp; </i> <i>Die Scheune  zur H&auml;lfe bis an den Dreschtennd.
        Da&szlig; der Messner auf eben dieser Tenne sein Getraid auszutreschen
        berechtig dey, wird nich beweifelt werden.</i><br>
        <i>9.&nbsp; </i> <i>Die Holzschupfe, welche an die Scheune des Pfarrers
        ansto&szlig;t.</i><br>
        <i>10.&nbsp;&nbsp;&nbsp; </i> <i>Der Abtritt, welcher an jenen der
        Schule ansto&szlig;t.</i><br>
        <i>11.&nbsp;&nbsp;&nbsp; </i> <i>Die Dungst&auml;lle n&auml;chst jener
        des Pfarrers ist.</i><br>
        <i>12.&nbsp;&nbsp;&nbsp; </i> <i>Das Wasch– und Backhaus, welches
        f&uuml;r beide gemeinschaftlich geh&ouml;rt.</i><br>
        <i>13.&nbsp;&nbsp;&nbsp; </i> <i>Das kleine Baumg&auml;rtlein, welches
        an die Scheune des Pfarrers ansto&szlig;t und mit einer Blanke versehen
        ist..“</i><br>
        Die Zust&auml;nde in dem Pfarr-, Schul- und Mesnerhaus veranlassten die
        Schulgemeinde an die Erbauung eines eigenen Schul- und Mesnerhauses
        heranzutreten. Im Jahre 1828 besichtigten Vertreter der Schulgemeinde
        das Schulhaus und kamen am 2. Juli des gleichen Jahres zu den Beschluss,
        dass <i>„nachdem die Schulgemeinde Ruhmannsfelden vor ung. 20 Jahren
        das damalige Pfarr- und Schulhaus aus eigenen Mitteln gekauft hat um als
        Schul- und Mesnerhaus verwendet zu werden, welches einen Kostenaufwand
        von ung. 2500 fl. verursacht und das Staats Aerar hiezu einen
        verh&auml;ltnism&auml;&szlig;igen Beitrag leistet, weil das
        n&auml;mliche Geb&auml;ude zugleich zur Pfarrerwohnung bestimmt war,
        sich das Staats&auml;rar gefallen lasse 1800 fl. zur Erbauung eines
        neuen Schul- und Mesnerhauses beizutragen, wogegen das bisherige
        Schulhaus die Pfarrerwohnung allein werden w&uuml;rde, folglich dieses
        Geb&auml;ude ungeteilt dem Staats-Aerar anheim fiel. Wenn nun obiger
        Beitrag von Seite des Kirche der Bruderschaft und des Staates geleistet
        sein werde, so verpflichtet sich die Schulgemeinde nebst der Herlassung
        des zum Schulhaus erforderlichen Platzes, das Defizit der Baulasten zu
        decken. Zur Bestimmung des Beitragsquantums nach Abzug des beantragten
        Aearialbeitrags zu 1800 fl. soll festgesetzt werden, was im Namen der
        Kirchen die genannte Bruderschaft, die H&auml;lfte des Defizits und die
        Schulgemeinde die 2. H&auml;lfte zu tragen habe.“</i><br>
        Zun&auml;chst setzte der Streit ein, welcher Platz f&uuml;r das zu
        erbauende Schul- und Mesnerhaus der geeignetste w&auml;re. Der Erfolg
        dieser langwierigen Auseinandersetzungen war, dass man auf dem Gedanken
        kam, das alte Geb&auml;ude zu belassen und ein neues Pfarrhaus zu bauen
        mit dem zur Erbauung des Schulhauses treffenden Bau Anteil zu 1650 fl.
        Laut Protokoll vom 7. August 1830 haben sich gleichzeitig die Vertreter
        der Schulgemeinde erheischig gemacht, bei dem neuen Pfarrhausbau samt
        n&ouml;tigen Nebengeb&auml;uden die erforderlichen Hand- und
        Spannf&uuml;hren zu leisten. Rentamt, Lokalschulinspektor und
        s&auml;mtliche Beigezogene waren mit diesen Antrag einverstanden.
        Mauermeister Achatz gab die Erkl&auml;rung ab, dass er das in Frage
        stehende Geb&auml;ude f&uuml;r ein Pfarr- und zugleich Schulhaus
        keineswegs geeignet finde, doch zum Zwecke einer Wohnung f&uuml;r den
        Schullehrer und zugleich Mesner samt Schulgehilfen und f&uuml;r die
        erforderlichen Lehrzimmer geeigenschaftet finde. Maurermeister Achatz
        hat laut Protokoll vom 3. Juni 1831 eine Erkl&auml;rung abgegeben,
        wonach das bisherige Pfarr- und Schulhaus um die Summe von ungef&auml;hr
        400 fl. zu alleinigen Zweck eines k&uuml;nftigen Schulhauses
        hergerichtet werden k&ouml;nne. Bei n&auml;herer Pr&uuml;fung hat sich
        aber gezeigt, dass die Kosten auf eine bei weitem h&ouml;here Summe sich
        belaufen w&uuml;rden. So konnte die Schulgemeinde das angebotene
        Geb&auml;ude als k&uuml;nftiges Schulhaus nicht &uuml;bernehmen, weil
        ihr die Erbauung eines neuen Schulhauses auch nicht h&ouml;her zu stehen
        gekommen w&auml;re. Die langwierigen Verhandlungen haben, nachdem der
        Gemeindeanteil an dem gemeinschaftlichen Pfarr und Schulhause um 500 fl.
        an den Statt verkauft war, ergeben, dass ein neues Geb&auml;ude als
        Schul- und Mesnerhaus ausgef&uuml;hrt werde.<br>
        <i>„Es ist im Jahre 1834 aus Bruch- und Ziegelsteinen gebaut worden,
        liegt im s&uuml;dlichen Endes des Marktes, hart an der nach
        Gotteszell-Deggendorf f&uuml;hrenden Stra&szlig;e und hat zwei
        Stockwerke. Im ersten, das ist zu ebener Erde, befindet sich die Wohnung
        des Schullehrers und des Schulgehilfen, ein Gew&ouml;lbe vertritt die
        Stelle des Kellers, weil ein solcher wegen zu feuchter Grundlage nicht
        erbaut werden konnte.</i><br>
        <i>Im zweiten Stockwerke befinden sich die zwei Schulzimmer und zuvor
        eines f&uuml;r Vorbereitung- und I-, dann eines f&uuml;r II. und III.
        Klasse. Der Dachboden ordentlich gelegt und das Dach mit
        Schneidschindeln gedeckt. Die neben dem Schulhaus in s&uuml;dlicher
        Richtung stehenden &Ouml;konomiegeb&auml;ude sind gemauert und befinden
        sich im gutbaulichen Zustande, was aber von der am Stalle stehenden
        Holzlege ebenso wenig wie vom Schulhause gesagt werden kann.</i> (Beschreibung vom 12.5.1866 von Raymund Schinagl, Schullehrer.)<br>
        Mit der Erbauung des neuen Schulhauses und seiner Einweihung und
        Er&ouml;ffnung 1835 beginnt eine neue Zeitepoche in der aufw&auml;rts
        sehr reifenden Entwicklung des Schulwesens im Markte Ruhmannsfelden.<br>
        Am 26. Oktober 1830 starb Andreas Stern, Elementarlehrer in
        Ruhmannsfelden, 63 Jahre und 11 Monate alt. Bemerkung in der
        Pfarrmatrikel <i>„Dieser w&uuml;rdige Mann war gegen 43 Jahre lang in
