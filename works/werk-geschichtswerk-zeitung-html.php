      <p>Die erste Ansiedlung in hiesiger Gegend erfolgte am Fu&szlig;e des
        Nordabhanges vom Voglsang. Hier hatte das den Agilolfingern
        gleichberechtigte Grafengeschlecht der &quot;Drozza&quot; (nach Hr.
        Geheimrat Dr. Eberl) einen Hof genannt &quot;Dro&szlig;lach&quot;. In
        der N&auml;he davon befanden sich noch zwei Ansiedelungen, n&auml;mlich
        Achslach und Irlach. Da wir nirgends um Ruhmannsfelden herum Ortsnamen
        finden, die auf eine Entstehung der betreffenden Ortschaft vor dem 8.
        Jahrhundert schlie&szlig;en lie&szlig;en, so m&uuml;ssen wir annehmen,
        dass die Besiedelung unserer Gegend in der Richtung von Westen nach
        Osten, von der Donau her &uuml;ber Metten, Achslach, Dro&szlig;lach
        erfolgte. Die hiesige Gegend, damals noch Urwald, kam sp&auml;ter in den
        Besitz Karl des Gro&szlig;en. Dieser schenkte einen Teil des sogenannten
        Nordgaues dem Kloster Metten. Die Grenze dieses Besitztums lief von
        Metten &uuml;ber den Voglsang nach K&ouml;ckersried, L&auml;mmersdorf,
        Fratersdorf, Heum&uuml;hle, Frankenried, Hornwald, Schusterstein,
        Kalteneck, Metten. Das Kloster Metten war aber nicht allzu lange im
        Besitze des genannten Teiles des Nordgaues. Ein Arnulf, der B&ouml;se
        genannt, hat die Kl&ouml;ster s&auml;kularisiert, d. h. er hat ihnen das
        Besitztum wieder abgenommen und dieses dann Grafen gegeben. Und so kommt
        die hiesige Gegend in den Besitz des Grafen Aswin von Bogen,
        W&auml;hrend die Mettner Patres ihre Hauptt&auml;tigkeit im
        Christianisierungswerk erblickten, versuchten die weltlichen Machthaber,
        die Grafen, das Land so rasch als m&ouml;glich urbar und dadurch
        ertragreich zu machen. Unter der Mettner Herrschaft entstanden die
        Ortsnamen mit &quot;dorf&quot;, L&auml;mmersdorf, Fratersdorf,
