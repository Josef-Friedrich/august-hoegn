      <p>Auf diese Weise kam auch hierher ein solcher Ministerialer, der sich
        der sich hier ans&auml;ssig machen musste, genannt Hrothimar (sprich
        Rumar). Er baute sich eine turmf&ouml;rmige Weiherburg, kein Schloss,
        denn er war kein Adeliger, sondern nur Angestellter des Grafen Aswin von
        Bogen und kurze Zeit darauf entstand dann um die Burg herum (siehe
        Aichinger: &quot;Metten und seine Umgebung&quot;) eine Ortschaft,
        genannt Hrothimarsfeld, Rumarsfeld, das heutige Ruhmannsfelden. Nach
        Ansicht der in Frage kommenden Autorit&auml;ten war dies um das Jahr
        1100 herum, sodass Ruhmannsfelden auf ein 800-j&auml;hriges Bestehen
        zur&uuml;ckblicken kann und sein 800-j&auml;hriges
        Geburtstagsjubil&auml;um feiern k&ouml;nnte.<br>
        Um die Mitte des 14. Jahrhunderts herum herrschte hier in Ruhmannsfelden
        die Pest (nach Schmid, M&uuml;nchen i. J.1340). Es war die indische
        Beulenpest, die von Italien nach Bayern und &Ouml;sterreich
        eingeschleppt wurde. Diese Pest forderte hier so viele Opfer, dass zwei
        neue Begr&auml;bnispl&auml;tze errichtet werden mussten, Grab und
        Siechet, um die Toten beerdigen zu k&ouml;nnen. Wenn nun die Geschichte
        schreibt, dass die Pest hier f&uuml;rchterlich gew&uuml;tet hat, so
        besteht sicherlich Veranlassung genug zu &uuml;berlegen, ob man nicht
        diese beiden 600-j&auml;hrigen Begr&auml;bnisst&auml;tten von so vielen
        von der Pest dahingerafften Ruhmannsfeldnern als solche auf irgend eine
        Weise w&uuml;rdigen k&ouml;nnte und sei es nur eine kurze Inschrift in
        Stein. Wie ehren damit die Toten, die vor 600 Jahren dort ihre
        Ruhest&auml;tte fanden und ehren uns damit selbst. Denn es macht immer
        einen guten Eindruck, wenn wohl gepflegt der heimatgeschichtlichen
        Begebenheiten eines Ortes auch offensichtlich in Inschriften, auf
        Tafeln, in S&auml;ulen, Denkm&auml;lern etc. gedacht wird.<br>
        In der letzten Zeit der Aldersbacher Regierung dahier (siehe Aichinger)
        hat sich Ruhmannsfelden zum Markt emporgeschwungen. Am 26. April 1416
        stellte Jakob der Rueerer eine Urkunde aus, in welcher er sich <i>&quot;dyczeit
        Richter dez Markehtz zue Ruedmansfelden&quot;</i> nennt. Es d&uuml;rfte
        kaum ein Zweifel bestehen, dass der Urkundenausteller als
        landesherrlicher Richter &uuml;ber die Markteigenschaft seines
        Wirkungsortes Bescheid wusste. Es ist nicht genau festzustellen, wann
        und von wem Ruhmannsfelden zum Markt erhoben wurde. Aber man kann
        behaupten auf Grund der 1416 ausgestellten Urkunde, dass Ruhmannsfelden
        bei Beginn des 15. Jahrhunderts, also am das Jahr 1400 herum, zum Markt
        erhoben wurde, demnach 500 Jahre Markt ist und sein 500-j&auml;hriges
        Marktjubil&auml;um feiern k&ouml;nnte.<br>
        Im Jahre 1574 brannte das Pfarrgotteshaus nieder und es k&ouml;nnte
        m&ouml;glich sein, dass sich die Ruhmannsfeldner dadurch zu helfen
        wussten, dass sie sich bis zum Wiederaufbau der Pfarrkirche ein
        Kapellchen bauten, auf einem ganz versteckten Platz, damit sie nicht zum
        Gottesdienst bis nach Gotteszell gehen brauchten.<br>
        Vielleicht w&auml;re das in Zusammenhang zu bringen mit der Entstehung
        des Osterbr&uuml;nnls. Wenn das zutreffen w&uuml;rde, dann st&uuml;nde
        das Osterbr&uuml;nnl schon 350 Jahre. Wenn nicht, dann ist aber das
        Osterbr&uuml;nnl sicher in der Mitte des 17. Jahrhunderts entstanden, um
        1660, sodass das Osterbr&uuml;nnl sein 250-j&auml;hriges Bestehen feiern
        kann.<br>
        Fr&uuml;her war es der Brauch, dass man das Holz im Walde verbrannte und
        die Asche heimfuhr, da das Holz nur einen ganz geringen Wert hatte und
        f&uuml;r die Asche, die an die einheimischen Seifensieder oder an
        ausw&auml;rtige H&auml;ndler verkauft wurde, mehr Geld einnahm als
        f&uuml;r das Holz. So kam es auch, dass hier beim Berger Br&auml;u ein
        gro&szlig;er Aschenhaufen aufgeschichtet war im Hof. In der Nacht zum 1.
        Juli 1820 kam ein heftiger Wind, der die Glut im Aschenhaufen anfachte
        und das Tannenreisig brennend machte. In wenigen Minuten stand der
        Stadel vom Berger Br&auml;u in Flammen. Die halbe Marktgasse von der
        Bachgasse bis zum oberen Markt samt der sch&ouml;nen gotischen
        Laurentius-Pfarrkirche wurde ein Raub der Flammen. Erst im Jahre 1828
        konnte die neu aufgebaute Kirche, fertig gestellt und eingeweiht werden.
        Demnach kann die hiesige Pfarrkirche, klassizistisch in ihrem Innern
        durch Entstauben jugendfrisch gestaltet, als ein wahres
        Schmuckk&auml;stlein unter den Kirchen des oberen bayerischen Waldes,
        ihr 100-j&auml;hriges Jubil&auml;um feiern.<br>
        Es k&ouml;nnte noch eine Reihe kleinerer Begebenheiten angef&uuml;hrt
        werden, die auch in den Jubil&auml;umskranz eingeflochten werden
        k&ouml;nnten. Aber die oben angef&uuml;hrten reichen wohl aus
        &uuml;berzeugen zu d&uuml;rfen, dass es sich f&uuml;r Ruhmannsfelden
        lohnen d&uuml;rfte, auch einmal ein Jubil&auml;umsjahr zu begehen, wie
        es vor kurzem erst Osterhofen getan.</p>
      <h2><b>VI.</b> Wie hat es um Ruhmannsfelden herum ausgesehen vor seiner
        Entstehung?<a name="vi" id="vi"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a><br>
        <font size="-1">Viechtacher Tagblatt, 25.10.1928</font></h2>
      <p>Kaiser Karl der Gro&szlig;e beabsichtigte ein gro&szlig;es
        christliches Weltreich unter der Herrschaft der Franken zu gr&uuml;nden.
        Dazu brauchte er erstens Soldaten, um die damals bekannten V&ouml;lker
        unterwerfen zu k&ouml;nnen, und zweitens die Glaubensboten, um diese
        unterworfenen V&ouml;lker, soweit diese noch heidnisch waren, zu
        christianisieren. So lernen wir Kaiser Karl kennen als bedeutenden
        Kriegsf&uuml;rsten mit ausgedehnter Machtstellung, die ihm den Beinamen
        &quot;der Gro&szlig;e&quot; eintr&auml;gt, und lernen ihn kennen als
        besonderen Freund der Kirche, sodass ihm Papst Leo III, am
        Weihnachtsabend im Jahre 800 als Zeichen der Dankbarkeit die Kaiserkrone
        auf das Haupt setzte.<br>
        Karl der Gro&szlig;e hatte fast alle damals bekannten V&ouml;lker
        unterworfen. Da h&ouml;rte er auch von dem frechen Eindringen der Avaten
        und Slawen der Donau entlang. Diese Eindringlinge pl&uuml;nderten die
        Kl&ouml;ster aus, beraubten und bel&auml;stigten die Glaubensboten.
        Eines Tages kam Karl d. Gr. nach Pfelling unterhalb Bogen. Von dort
        f&uuml;hrten drei Wege in den Wald herein, ein Heeresweg &uuml;ber
        Kalteck, Achslach, Gotteszell, ein Saumweg &uuml;ber Berg,
        Oberhirschberg, Voglsang, Gotteszell, ein Pr&uuml;glweg dem Kohlbach
        entlang &uuml;ber Datting, Hochb&uuml;hl, Gotteszell. Da kam bekanntlich
        Karl d. Gr. den Saumweg ben&uuml;tzend, rau dem Einsiedler Utto in
        Uttobrunn (bei Berg). Utto bat den Kaiser um die Erbauung eines festen,
        massiven Klosters in Metten. Karl d. Gr. erf&uuml;llte die Bitte des
        wundert&auml;tigen Einsiedlers und lie&szlig; 792 das Kloster Metten
        bauen. Karl d. Gr. zog dann Waldeinwerts mit seinem Heere gegen die
        heidnischen Slawen, besiegte sie und nahm von Lande, Besitz.
        Gleichzeitig gab er einen Teil dieses Gebietes, Nordwald genannt, dem
        Kloster Metten. Dieses Besitztum erstreckte sich von Voglsang (Der
        Kohlbach war die Grenze.) nach K&ouml;ckersried, L&auml;mmersdorf,
        Fratersdorf (Der Flintsbach war die Grenze.), Neum&uuml;hle,
        Frankenried, Hornwald, Schusterstein, Hirschenstein, Kalteck, Metten.
        Dieses Gebiet stand unter kaiserlichem Schutz. Das Kloster Metten hatte
        in diesem Gebiet seine eigene Gerichtsbarkeit und Immunit&auml;t
        (Steuerfreiheit). Wenn auch das Kloster Metten durch die Erwerbung
        dieses gro&szlig;en Gebietes wirtschaftlich gest&auml;rkt war, so wurde
        ihm durch die Zuweisung dieses St&uuml;ckes Land eine gro&szlig;e
        Arbeitsleistung zugemutet. Denn das Land war mit Urwald Sumpf, See usw.
        bedeckt und musste erst gerodet urbar gemacht werden. Die Rodungsarbeit
        und das Christianisierungswerk waren die beiden Hauptaufgaben des
        Klosters Metten in hiesiger Gegend gleich zu Beginn des 9. Jahrhunderts.
        Die ersten Menschen, die hierher gekommen sind, waren Slawen, die bis
        zur Donau vorgedrungen sind. Die Ortschaft Metten soll eine slawische
        Ansiedlung gewesen sein. Dann kamen die R&ouml;mer, die ihre Vorposten
        bis an den Regen vorschoben. W&auml;re zur damaligen Zeit Ruhmannsfelden
        schon eine Besiedelung gewesen, so w&auml;re der Name dieser Besiedelung
        nicht Ruhmannsfelden, sondern mit einem Eigennamen verbundener
        &quot;ing&quot;-Name.<br>
        Zu Beginn des 9. Jahrhunderts kamen dann hierher die Benediktiner-Patres
        vom Kloster Metten. Damals war schon besiedelt die Kalte Herberge =
        Kalteck (am Heeresweg), Achslach und Dro&szlig;lach (Gotteszell), das im
        Besitz eines ungarischen Grafengeschlechtes war und sp&auml;ter in den
        Besitz der Grafen von Pfelling kam. Die Klosterherren von Metten haben,
        von der Grenze nach der Mitte des Besitztums zu besiedelt. So kam es,
        dass sie sich hier in Ruhmannsfelden nicht festgelegt hatten. Vielmehr
        haben die an der Grenze des Besitztums entlang ihre Ansiedlungen
        angelegt und haben neben den bereits vorhandenen &auml;lteren
        Ansiedlungen (durch Slawen oder R&ouml;mer) neue H&ouml;fe (Villen)
        angelegt, die sie durch einen Meier oder durch eine Familie
        bewirtschaften Iie&szlig;en. Besonders die Villen, deren Namen die
        Zusammensetzung mit Eigennamen zeigen, waren an einzelne Familien zu
        Bewirtschaftung gegen Entrichtung bestimmter Abgaben vergeben. An der
        Grenze des Besitztums entstanden die Ansiedlungen Metten (bei Regen) =
        &ouml;stliche Ansiedlung, March = Mark oder Moar, Geierstal, Markbuchen.
        An der Westseite der Besitzung im Nordwald, also von Voglsang bis zum
        Regen, wurden die Villen in einer Reihe nacheinander angelegt. (Die
        althochdeutsche &Uuml;bersetzung f&uuml;r Villa = Dorf.) Es sind das
        L&auml;mmersdorf, Fratersdorf, Aschersdorf, Hetzelsdorf. An der
        Ostgrenze von Regen bis &Ouml;dwies sind Rannersdorf, Seigersdorf,
        Kammersdorf, Fernsdorf, Allersdorf und Schreindorf. In der Mitte des
        Besitztums liegt Patersdorf. An der Westgrenze haben sich neben den
        Villen (Dorforten) Familien angesiedelt, die sich haupts&auml;chlich bei
        der Rodungsarbeit bet&auml;tigen. Die Namen der Ansiedelungen dieser
        Familien endigen auf &quot;ried.&quot; Bei L&auml;mmersdorf ist
        „Giggenried“, bei Fratersdorf &quot;Kaickenried&quot;, bei
        Patersdorf &quot;Zuckenried&quot;, bei Aschersdorf
        &quot;B&auml;rmannsried.&quot; Auffallenderweise finden wir auf der
        Ostseite der Besitzung und nur hier, sonst nirgends, ganz nahe beisammen
        eine Menge von &quot;ing&quot;-Orten, z. B. Tradweging, Brenning,
        Zottling, Handling, Sintweging, weiter &ouml;stlich gelegen Einweging.<br>
        Was nun das Alter dieser Ansiedlungen betrifft, so k&ouml;nnen
        behaupten, dass diese &quot;dorf&quot; und &quot;ried&quot;-Namen im
        9.Jahrhundert entstanden sind. Denn erstens hat das Kloster Metten diese
        Besitzungen im Nordwald zu Karl d. Gr. Lebzeiten bekommen und hat sich
        882 die betreffenden Schenkungs-Urkunde best&auml;tigen lassen, und
        zweitens sind die Personennamen, die in diesen &quot;dorf&quot; und
        &quot;ried&quot;-Namen enthalten sind, zur damaligen Zeit an das
        Reichenauer Kloster gesandt und in das Verbr&uuml;derungsbuch
        eingetragen worden. L&auml;mmersdorf = Lempferstord = Lantfried,
        Fernsdorf = Fater, Patersdorf = Patto, Hetzelsdorf = Hesse, Aschersdorf
        = Adalschalk, Fernsdorf = Eberwin, Allersdorf = Uodalrat, Schreindorf =
        Schrandorf, war Markt, Schrannen- und Gerichtsplatz. Er wurde nicht von
        einer Familie, sondern von einem Klostermeier verwaltet.<br>
        Die vielen &quot;ried&quot;-Namen weisen auf Rodungsland hin.
        K&ouml;ckersried = Coteschalk, Giggenried = Cundachaz, Kaickenried =
        Hacco, B&auml;rmannsried = Perhtnand, Zuckenried = Sigine, Hasmannsried
        = Hosnod, Lobetsried = Luipolf, Perlesried = Perolf, Triefenried =
        Trunolf.<br>
        Mit den &quot;dorf&quot;- und &quot;ried&quot;-Orten sind zu gleicher
        Zeit auch die mit &quot;berg&quot; zusammengesetzten Ortsnamen
        entstanden. Dietzberg = Theoto, Witzberg = Witolt, Wolfsberg = Volficho, <br>
        Die &quot;dorf&quot;- und &quot;ried&quot;-Orte sind also alle
        &auml;lter als Ruhmannsfelden. Ihre Entstehung geht zur&uuml;ck bis zum
        Jahre 800 und f&auml;llt in die Zeit, in der das Kloster Metten das
        Besitztum im Nordwalde innehatte. Nicht so ist es mit den
        &quot;ing&quot;-Orten, die sich an der Westseite von Ruhmannsfelden
        dicht zusammendr&auml;ngen. Das sind keine echten
        &quot;ing&quot;-Ortsnamen, weil sie nicht in Verbindung mit einem
        Eigennamen auftreten. Sie sind wom&ouml;glich mit Ruhmannsfelden
        entstanden. Da waren vier gro&szlig;e Rodungspl&auml;tze, an denen das
        Holz niedergebrannt wurde. Das ist Voglsang = sengen, Klessing = an der
        Klippe (Klep) sengen, Prenning = brennen und Pr&uuml;nst = Punst.
        Handling = H&auml;ndlern, Einweging = weging = Weihern = ein Weiher,
        Sintweging = sint = drei = drei Weiher, Hartweging = hart = Wald =
        Waldweiher, Zottling = Zeidler, Zadler, Zodler, Zodlbauer.<br>
        In der Umgebung von Ruhmannsfelden wurde demnach vom Anfang des 9.
        Jahrhunderts an gerodet. Es war das Gebiet noch Urwald mit
        Riesenb&auml;umen und mit gro&szlig;em Wildbestand. In den T&auml;lern,
        die fr&uuml;her mit Seen ausgef&uuml;llt waren, befanden sich noch
        S&uuml;mpfe und Auen mit zahllosem Wassergefl&uuml;gel (Auhof, Au bei
        Achslach). In der perlreichen Teisnach musste es gewimmelt haben von
        gro&szlig;en und kleinen Fischen. Hier und da f&uuml;hrte ein Steg
        &uuml;ber das Wasser (Stegm&uuml;hle). In den meisten F&auml;llen fuhr
        man an seichten Stellen (Furten, Furthof) durch das Wasser hindurch.
        Br&uuml;cken wurden erst unter der Mettener Herrschaft gemacht
        (Bruckhof). Zwei Hauptwege f&uuml;hrten durch das Gebiet, der Heeresweg
        von S&uuml;den nach Norden von Kalteck, Gotteszell, &uuml;ber die
        Hochstra&szlig;e &uuml;ber die Starlwiesen Gr&uuml;nbach zu, der zweite
        Weg von Osten nach Westen von Metten (bei Regen) &uuml;ber Fratersdorf
        nach Schreindorf. Der Kreuzungspunkt dieser zwei Stra&szlig;en war hier
        in Ruhmannsfelden. Dieses wurde von Metten nicht beachtet, denn das
        Arbeitsfeld der Mettener Patres lag mehr an der Grenze ihres Besitztums.
        Dieser Stra&szlig;enknotenpunkt gab erst den Grafen von Bogen (die
        sp&auml;ter die Herren dieses Gebietes wurden, nachdem man dem Kloster
        Metten das Besitztum im Nordgau abgenommen hatte) den Anla&szlig;, eine
        Ansiedelung zu errichten. Auf dieser Ansiedelung entstand dann um das
        Jahr 1100 herum, also viel sp&auml;ter als die umliegenden Ortschaften
        mit Ausnahme der &quot;ing&quot;-Orte, die Ortschaft Ruhmannsfelden.</p>
      <h2><b>VII.</b> Pfarrkirche St. Laurentius Ruhmannsfelden,<a name="vii"
  id="vii"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a><br>
        <font size="-1">Viechtacher Tagblatt, 1928/29</font></h2>
      <h3><b>1.</b> Die Pfarrkirche St. Laurentius bis zum Brand 1820<a
  name="vii1" id="vii1"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <p>In den Tagen vom 31. Oktober bis 2. November dieses Jahres kann die
        Pfarrkirche St. Laurentius in Ruhmannsfelden das 100-j&auml;hrige
        Jubil&auml;um seiner Einweihung begehen. In kirchlich feierlicher, aber
        doch in schlichter w&uuml;rdiger Form werden die Herzen der
        gl&auml;ubigen Pfarrangeh&ouml;rigen in dankbarer Liebe zu Gott empor
        schlagen, der ihnen, nachdem am 1. Juli 1820 das fr&uuml;here Gotteshaus
        durch Feuersbrunst in Schutt und Asche verwandelt wurde, wieder eine so
        sch&ouml;ne Kirche hat erstehen lassen.<br>
        Bei dieser Gelegenheit wollen wir erstens, unsere Gedanken in
        fr&uuml;here Zeiten zur&uuml;ck versetzen und dabei eine kurze
        R&uuml;ckschau halten &uuml;ber die Kirchenverh&auml;ltnisse von
        fr&uuml;hester Zeit bis zum Brand am 1. Juli 1820 und zweitens eine
        kurze Betrachtung anstellen &uuml;ber die neu erbaute Pfarrkirche nach
        dem Brande.<br>
        Wir haben schon &ouml;fters gelesen, dass in der Karolingerzeit (nach
        800) die Mettener Patres in hiesiger Gegend christianisiert und gerodet
        haben. Ruhmannsfelden existierte zu damaliger Zeit noch nicht. An der
        Stelle des heutigen unteren Marktes war damals der Kreuzungspunkt zweier
        wichtiger Stra&szlig;enz&uuml;ge. Hier aber lag nicht das Arbeitsfeld
        der Klosterherren, sondern es lag abseits der Heeresstra&szlig;e und dem
        Handelsweg, an der Grenze ihres Besitztums. Erst um das Jahr 1100, als
        der Nordwaldbesitz in die H&auml;nde der Grafen von Bogen kam, da bekam
        auch erst diese Stra&szlig;enkreuzung Bedeutung. Ein Angestellter des
        Grafen Aswin von Bogen, Rumar genannt, der musste diesen Punkt sichern,
        baute, sich einen kastenf&ouml;rmigen Turm und seine Arbeiter,
        Rodungsleute und Handwerker, bauten sich um den Turm herum ihre
        Wohnst&auml;tten. So entstand die Ansiedlung Hrothimarsfeld
        (Rumarsfeld). Die ersten Besiedler waren schon christianisiert. Sie
        brauchten zu ihrer gemeinsamen Andachtsverrichtung auch ein Kapellchen.
        Und dieses bauten sie nicht innerhalb der Besiedlung, sondern erbauten
        ihr Kapellchen oberhalb der Besiedlung, inmitten des B&uuml;hls, dahin,
        wo heute die Pfarrkirche steht. Das k&ouml;nnen wir daraus entnehmen,
        weil der Patron der Pfarrkirche der hl. Laurentius ist. Dieser wurde bei
        allen jenen Kapellen und Kirchen als Patron gew&auml;hlt, die seinerzeit
        au&szlig;erhalb der Besiedlung standen.<br>
        Das Rumargeschlecht hatte aber bald ausgewirtschaftet gehabt in
        Ruhmannsfelden. In der zweiten H&auml;lfte des 13. Jahrhunderts treten
        als Besitzer der Burg Ruhmannsfelden die reich beg&uuml;terten
        Pfellinger Grafen auf. Nach dem Tode Heinrichs von Pfelling fiel die
        Besiedlung den Landesherren zu. Die drei niederbayerischen Herz&ouml;ge
        Otto III., Ludwig III. und Stephan I. verkauften den Markt an das
        Kloster Aldersbach, die inzwischen aus dem Hof
        &quot;Dro&szlig;lach&quot; das Zisterzienserkloster Gotteszell gemacht
        hatten. Nicht blo&szlig; um Material f&uuml;r die Erbauung und
        Erweiterung des Klosters Gotteszell zu gewinnen, sondern auch
        haupts&auml;chlich darum um die Niederlassung irgend einer weltlichen
        Macht in Ruhmannsfelden zu verhindern, wurde der Turm in Ruhmannsfelden
        vollst&auml;ndig abgebrochen. Ruhmannsfelden stand nun unter der
        Herrschaft der Aldersbacher M&ouml;nche. Dabei ging es aber
        Ruhmannsfelden gar nicht schlecht. Im Gegenteil! Da hat sich
        Ruhmannsfelden zum Markt emporgeschwungen.<br>
        Am 10. M&auml;rz 1408 verpflichtete sich Georg der Parsberger, Chorherr
        zu Freising von den Kirchen zu Patersdorf, Geiersthal und Ruhmannsfelden
        dem Kloster Aldersbach j&auml;hrlich 10 Pfund Pfennige zu entrichten.
        Diese drei Kirchen waren ihm vom Abte Heinrich zu Aldersbach auf Bitten
        seines Schwagers, Stephan des Degenbergers zu Altnussberg,
        &uuml;berlassen worden. Die Urkunde ist besiegelt vom genannten
        Degenberger, vom Wernherrn, dem Parsberger und Eberhard, dem Nussberger
        zu Kollnburg (nach Trellinger, Bayerwald).<br>
        1445 fanden Unterhandlungen statt zwischen Aldersbach und Gotteszell die
        &quot;Villa&quot; Ruhmannsfelden zu vertauschen. 1496 verkaufte das
        Kloster Aldersbach den Markt Ruhmannsfelden notgedrungen an die
        Degenberger unter Vorbehalt des Wiedereinl&ouml;sens, was von Seiten des
        Abtes Simon von Aldersbach am Ende des 15. Jahrhunderts geschah. 1503 am
        Freitag nach Maria Himmelfahrt best&auml;tigte Herzog Albrecht der Weise
        einen zwischen den Kl&ouml;stern Aldersbach und Gotteszell vollzogenen
        Tausch, wodurch Ruhmannsfelden an das Kloster Gotteszell kam. Gotteszell
        gab daf&uuml;r mehrere, im Gebiete Georg des Reichen gelegene,
        G&uuml;ter. Aldersbach behielt sich nur den Pfarrhof und die pfarrlichen
        Rechte in Ruhmannsfelden und schickte einen Expositus nach
        Ruhmannsfelden. 1511 begann der dauernde Streit zwischen Kloster und
        Markt. 1519 brachen Unruhen aus, die sogar in offenen Aufruhr
        ausbrachen. Die Ruhmannsfeldener besch&auml;digten die G&uuml;ter des
        Pr&auml;laten. 1522 brach ein neuer Aufstand aus, bei der ganze Markt
        durch Feuersbrunst vernichtet wurde. Die Pfarrkirche stand damals
        wahrscheinlich noch au&szlig;erhalb des Marktes, sonst w&auml;re sie
        sicher auch ein Raub der Flammen geworden. Hiervon ist aber nichts
        bekannt. Im Jahre 1574 brannte das Pfarrgotteshaus vollst&auml;ndig
        nieder. F&uuml;r die Wiederherstellung derselben geschah sehr viel von
        Seiten des Klosters Gotteszell, das auch die Aufsicht &uuml;ber den Bau
        leitete. Die Glocken, die von dem M&uuml;nchner Glockengie&szlig;er Girt
        stammten, wurden erst 1643 auf den Turm gebracht. 1633 wurde die
        Pfarrkirche von den schwedischen Kriegsv&ouml;lkern vollst&auml;ndig
        ausgepl&uuml;ndert und Pfarrvikarhaus und Klostertaverne nach
        geschehener Pl&uuml;nderung in Asche gelegt.<br>
        1658 kam die bisher Kloster Aldersbach'sche Pfarrei Ruhmannsfelden an
        das Kloster Gotteszell. Von dieser Zeit an bis zur Aufhebung des
        Klosters Gotteszell (1803) blieb der Markt Ruhmannsfelden dem Kloster
        Gotteszell mit Grund und niederer Gerichtsbarkeit unterworfen und die
        Pfarrei wurde von da an nicht mehr von Geiersthal aus, sondern vom
        Kloster Gotteszell aus pastoriert.<br>
        1659 wurde ein Pfarrhof gebaut. 1686 spuckte das Luthertum in
        Ruhmannsfelden. 1696 vermachte Paul Huber von Pr&uuml;nst dem Gotteshaus
        zu Ruhmannsfelden sein ganzes, nicht unbedeutendes Verm&ouml;gen. 1745
        im &ouml;sterreichischen Erbfolgekrieg kamen die Panduren nach
        Ruhmannsfelden und pl&uuml;nderten die Kirche aus.<br>
        1803 wurde das Kloster Gotteszell aufgehoben unter Abt Amad&auml;us
        Bauer. Der Pfarrhof und die pfarrlichen Rechte von Ruhmannsfelden, die
        seit 1659 zum Kloster Gotteszell geh&ouml;rt hatten, wurden von der
        Aufhebungskommission bei der S&auml;kularisation verkauft, ebenso die
        Rechte und G&uuml;ter in Ruhmannsfelden, die zum Kloster Gotteszell
        geh&ouml;rten. Im Jahre 1800 versah die hiesige Expositur noch Hr. P.
        Bernhard Kammerer. Zurzeit der Aufhebung des Klosters war Pfarrprovisor
        Hr. P. Joseph Haindl und zwar vom 21.3.1803 bis 1. Oktober 1805.
        Aushilfe leistete ihm vom 21.4. bis Ende Oktober 1803 P. Nivard Sator,
        vom November 1803 bis Ende 1804 P. Marian Triendorfer, vom M&auml;rz
        1804 bis Ende September 1805 P. Guido Berger. Am 14. August 1805 starb
        in Ruhmannsfelden P. Xaver S&auml;mer. P. Marian Triendorfer ist dann
        nach Viechtach &uuml;bergesiedelt, kehrte aber bald wieder nach
        Ruhmannsfelden zur&uuml;ck. 1805 wurde dann in Ruhmannsfelden ein
        Pfarrer definitiv angestellt. 1806 wurde die Auspfarrung vollzogen.<br>
        Eine Beschreibung vom Jahre 1819 sagt, dass die damalige Pfarrkirche von
        Ruhmannsfelden aus rauhen Steinen erbaut war, Langhaus und Chorhaus
        waren mit Taschen gedeckt. Der Turm war durchaus von rauhen Steinen
        erbaut. Die Turmkuppel war mit Schneideschindeln eingedeckt. Im
        Kirchenturm hingen drei Glocken und in der oberen, kleineren Kuppel, die
        Laterne genannt wurde, ein kleines Sterbegl&ouml;cklein. Die gro&szlig;e
        Glocke wog 12 Zentner, die kleinere 2 ½  Zentner und das
        Sterbegl&ouml;cklein ½  Zentner. Die drei Glocken hatten einen Wert
        von 300 fl. Im Gotteshaus waren vier Alt&auml;re und eine Orgel mit 8
        Registern von einem ungewissen Meister. N&auml;chst dem Hochaltar befand
        sich ein Seitenaltar, auf welchem der hl. Leib des M&auml;rtyrers St.
        Martin ruhte. Dieser Leib wurde vom Kloster Gotteszell erkauft und war
        Eigentum des hiesigen Bierbrauers Martin Lukas. Der Bruderschafts-Altar
        war Eigentum der Corporis-Christi-Bruderschaft und musste von dieser
        unterhalten werden. Die Kirche war gut versehen mit Paramenten. Sogar
        der Chor war gut ausger&uuml;stet mit Instrumenten. Das Eigentumsrecht
        von den Trompeten hatten die Wolfgangi-Br&uuml;der und die Pauken
        geh&ouml;rten der Bruderschaft.<br>
        Am 1. Juli 1820 brach im Hofraum der Berger'schen Bierbrauerei
        (Amberger) ein Brand aus. Der damals herrschende Wind entfachte den im
        Hof aufgestapelten und mit Tannenreisig zugedeckten Aschenhaufen. Im Nu
        standen die Berger'schen Geb&auml;ulichkeiten in hellen Flammen und die
        halbe Marktseite samt der Pfarrkirche wurde &uuml;ber Nacht in Schutt
        und Asche verwandelt.</p>
      <h3><b>2.</b> Die Pfarrkirche St. Laurentius bis zur Einweihung 1828<a
