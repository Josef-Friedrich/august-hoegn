        Dietrich wurde um 3000 Markt angekauft und damit der Friedhof erweitert.
        Seit dieser Zeit existiert auch die Friedhofsmauer.<br>
        Am 20. November 1890 fuhr der erste Zug von Viechtach nach
        Ruhmannsfelden. Dabei gab es einen Dammrutsch bei Mariental, sodass
