        1729 wurde ein Wilderer vom Klosterrichter abgeurteilt, weil er auf dem
        Vogelsang ein Wildschwein erlegt hatte. 1745 kam General B&auml;renklau
        mit den Panduren aus Richtung Grafling hierher. 1762 herrschte hier
        wiederum die Viehseuche und im gleichen Jahre vernichtete ein starker
        Reif alles auf dem Felde und Wiese. Bei der Hungersnot im Jahre 1770/71
        wurde in den k&ouml;niglichen Jagden im Gebirge das Wild abgeschossen
        und das Wildpret im M&uuml;nchen kostenlos an die hungernde
        Bev&ouml;lkerung abgegeben. Im Auslande wurde Getreide aufgekauft und zu
        verbilligtem Preise verteilt. So wechselten im Laufe der verflossenen
        Jahrhunderte gute und schlimme Zeiten ab. Die Bev&ouml;lkerung von
        Ruhmannsfelden verlor aber trotz der h&auml;rtesten Bedr&auml;ngnisse
        den Glauben und das Vertrauen auf sein K&ouml;nnen und seinen
        Flei&szlig; nicht.<br>
        Hat es in damaliger Zeit auch schon Schulen gegeben? Ja! Klosterschulen,
        Lateinschulen, in denen talentierte Knaben zu Geistlichen herangebildet
        wurden. Aber es gab auch damals noch nicht die Schulh&auml;user, wie
        jetzt &uuml;berall. Lehrer, damals Schulmeister genannt, gab es wohl.
        Wanderlehrer und sesshafte. Urkunden aus dem 16. Jahrhundert weisen
        nach, dass es solche damals auch schon in Ruhmannsfelden gegeben hat. In
        einer Urkunde von 1550 kommt als Zeuge vor ein Andr&auml;
        Wei&szlig;pech, Schulmeister in Ruhmannsfelden und auch im 17. und 18.
        Jahrhundert berichten Urkunden von Schulmeistern in Ruhmannsfelden, die
        zugleich auch Mesner waren. Diese Schulmeister hatten meist selbst ein
        Besitztum und unterrichteten die wenigen Kinder, die freiwillig lesen
        und schreiben und rechnen lernen wollten, in ihrer Behausung. So lesen
        wir in einer Urkunde: <i>„Unterm 5.11.1658 verkaufte Georg Pitter,
        B&uuml;rger und gewester Schulmeister zu Ruhmannsfelden und dessen Frau
