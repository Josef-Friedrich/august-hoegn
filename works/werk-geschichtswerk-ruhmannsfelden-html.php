        Markt Ruhmannsfelden das Selbstverwaltungsrecht &uuml;bertragen. Der
        erste B&uuml;rgermeister war Josef Liebl, b&uuml;rgerlicher Bierbrauer.<br>
        1805 wurden auch ein Pfarrer, ein Kaplan und ein Schullehrer angestellt.
        Das Schulzimmer und die Lehrerwohnung waren aber in einem so schlechten
        Zustande, dass die Schulkinder wieder lieber nach Gotteszell zur Schule
        gingen. Am 15. August 1806 wollte die in Ruhmannsfelden liegende
        franz&ouml;sische Besatzung ein gro&szlig;es Fest abhalten. Auf dem
        Marktplatze war ein gro&szlig;es Transparent aufgestellt. Als abends die
        Lichter angez&uuml;ndet waren, warf jemand aus der Mitte der vielen
        Zuschauer heraus einen Stein gegen das Transparent, sodass es in Flammen
        aufging. Die betreffende Person blieb bis heute unbekannt.<br>
        Im Jahre 1812 geh&ouml;rten zur Pfarrei Ruhmannsfelden 9 D&ouml;rfer, 6
        Weiler, 13 Ein&ouml;den und 3 Neusiedlungen. Die Bev&ouml;lkerung der
        Pfarrei setzte sich zusammen aus 79 Inleuten und Tagl&ouml;hnern, 29
        Ganzbauern, 53 Halbbauern, 20 Viertelbauern, 2 Achtelbauern und 3
        Sechzehntelbauern. Unter den Namen der einzelnen Ortschaften steht auch
        ein „Hutweging“. Nicht weniger als 94 Handwerker und
        Gewerbetreibende waren damals hier ans&auml;ssig , darunter 6 Brauer, 5
        B&auml;cker, 5 Schmied, 5 Schuhmacher, 4 Schneider, 2 Tischler, 3
