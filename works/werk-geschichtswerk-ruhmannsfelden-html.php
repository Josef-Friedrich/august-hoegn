  src="images/geschichts_klein_oben.png"></a></h4>
      <p>Vor 1803 stand Ruhmannsfelden unter der Jurisdiktion (niedere
        Gerichtsbarkeit) des Zisterzienserklosters Gotteszell, das auch das
        amtliche Schreibwesen der Marktgemeinde Ruhmannsfelden und damit auch
        das Siegel f&uuml;hrte. Erst 1804 wurde dem Markte Ruhmannsfelden das
        Selbstverwaltungsrecht &uuml;bertragen. Der 1. B&uuml;rgermeister in
        Ruhmannsfelden war der b&uuml;rgerliche Bierbrauer Josef Liebl (1804 –
        1806). Ihm folgten bis heute 41 B&uuml;rgermeister. Von allen diesen
        hatte der B&uuml;rgermeister Lorenz Schreiner, Konditor, mit 12
        Dienstjahren als amtierender B&uuml;rgermeister. 1945 war sogar ein
        geb&uuml;rtiger Holl&auml;nder kurze Zeit Marktb&uuml;rgermeister.</p>
      <h4>d.) Gemeindeschreiber<a name="ii3d"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h4>
      <p>Nach Aufhebung des Klosters Gotteszell erscheint in den Urkunden und
        Protokollb&uuml;chern der Gemeindeverwaltung Ruhmannsfelden ein
        Schullehrer, namens Andr&auml; Stern, als Gemeindeschreiber, der dann
        vom Schullehrer Tauschek von Gotteszell abgel&ouml;st wurde. Nach diesem
        wurde Andr&auml; Voerge aus Vilshofen durch Beschluss des Magistrates
        vom 23.3.1823 gegen eine j&auml;hrliche Besoldung von 325 Gulden als
        Marktschreiber aufgenommen. Er starb aber schon ein Jahr darauf. Es gab
        dann einen h&auml;ufigen Wechsel bei den Gemeindeschreibern bis im Juni
        1834 der Schullehrer Georg Lippl das Amt des Marktschreibers
        &uuml;bernahm, der es bis 15.4.1866 f&uuml;hrte. Nach diesem gab es auch
        wieder viele Ver&auml;nderungen bei den Marktschreibern, bis im November
        1878 der Schullehrer Max Weig Marktschreiber wurde, der es bis zu seinem
        Tode am 16.3.1895 war. Der Bezirksoberlehrer Alois Auer versah das Amt
        das Marktgemeindeschreiberei Ruhmannsfelden vom 26.5.1899 bis zum
        1.9.1920. Nach ihm folgten B&uuml;ttner, Brunner, Klingseisen, Neueder,
        Schroll, Achatz, Kilger und Linsmeier.</p>
      <h4>e.) Polizei<a name="ii3e"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h4>
      <p>In fr&uuml;hester Zeit versah den Polizeidienst der Scherge,
        Schergenkerl, Scharnkarl genannt (schergen, sch&uuml;rgen,
        versch&uuml;rgen = verklagen). Es war das in der Regel ein bewaffneter,
        himmellanger, kr&auml;ftig gebauter Kerl, der stets einen gro&szlig;en
        Fanghund mit sich f&uuml;hrte. Das Schergenhaus befand sich zuletzt im
        Meindl-Haus (Winkler). Das jetzige Ellmann-Haus in der Bachgasse war das
        fr&uuml;here Klostergerichtshaus. Dort befanden sich auch die zwei
        Arreste, ein ganz kleiner, niederer und finsterer Raum, in dem man nur
        liegen, aber nicht stehen konnte und ein klein wenig gr&ouml;&szlig;erer
        Raum, in den nur durch eine ganz schmale Mauerscharte wenig Luft und
        Licht Zutritt hatten. Wer in einem solchen Arrest eingesperrt war, der
        hatte wirklich nichts zu lachen. Die Schergen in Ruhmannsfelden standen
        im Dienste des Klosterrichters von Gotteszell. Nach der Aufhebung des
        Klosters Gotteszell erhielt Ruhmannsfelden das Selbstverwaltungsrecht
        und damit auch eine eigene Ortspolizeibeh&ouml;rde, bestehend aus dem
        jeweiligen B&uuml;rgermeister und dem Polizeimann, der zugleich auch das
        Amt des Gemeindedieners versah. Au&szlig;er der Ortspolizei besteht in
        Ruhmannsfelden auch eine staatliche Gendarmeriestation.
        Urspr&uuml;nglich waren die Gendarmen (Schandarmen) Edelleute, die
        Dienst machten als Leibgarde am Hofe der franz&ouml;sischen K&ouml;nige.<br>
        In Deutschland waren es die K&uuml;rassiere, die diesen Dienst an den
        deutschen F&uuml;rstenh&ouml;fen versahen. Der Name Gendarm
        &uuml;bertrag sich dann in Deutschland auf die milit&auml;risch
        organisierte Polizeitruppe, welche wir mit dem Namen Gendarmerie
        (Schandarmerie) bezeichneten. Seit 1945 f&uuml;hrt sie den Namen
        Landpolizei, das ist eine auf Schutz der &ouml;ffentlichen Ordnung und
        Sicherheit gerichtete Gewalt der inneren Verwaltung. Stationsf&uuml;hrer
        der hiesigen Gendarmerie waren: Bogenreither, Bauer, Gmeiner, Adrian,
        Baumgartner, Hetzenecker, Sponrast, Wallner und Schindlbeck. Den
        Landpolizeiposten Ruhmannsfelden f&uuml;hrten: Schaffer und zurzeit
        Oberkommiss&auml;r Piehler.</p>
      <h4>f.) Post<a name="ii3f"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h4>
      <p>Die Post ist eine Einrichtung zur regelm&auml;&szlig;igen
        Bef&ouml;rderung von Sendungen und Personen. Sie wurde von Thurn und
        Taxis, einem italienischen Adelsgeschlecht, das sein Schloss in
        Regensburg hat, eingef&uuml;hrt und das seit 1516 alle Hoheitsrechte
        &uuml;ber die Post besa&szlig;. Die letzten Postgerechtsamen verlor das
        F&uuml;rstenhaus Thurn und Taxis im Jahre 1867. Seit dieser Zeit ist die
        Post eine &ouml;ffentliche Staatsanstalt. Die deutsche Reichspost
        besteht seit 1871. Die bayerische Posterverwaltung aber wurde erst am
        1.4.1920 vom Reich &uuml;bernommen. Postsendungen und Personen wurden in
        fr&uuml;heren Zeiten mit der gelben Postkutsche bef&ouml;rdert. Der
        Postillon war bekleidet mit hohen Stiefeln, wei&szlig;er Lederhose,
        blauem Frack mit Silberkn&ouml;pfen, Zylinder mit hohem,
        wei&szlig;blauem B&uuml;schl und f&uuml;hrte das Posthorn an
        wei&szlig;blauer Schnur mit sich. Da die Strecke von Viechtach bis nach
        Deggendorf t&auml;glich hin und zur&uuml;ck f&uuml;r die Postpferde zu
        anstrengend gewesen w&auml;re, wurden die Pferde in Ruhmannsfelden
        gewechselt. Der Poststall war damals bei Kaufmann Probst. Am 20. Oktober
        1883 war hier die Er&ouml;ffnung der Posttelegraphenstation. Nachdem im
        November 1890 die neu erbaute Regentalbahn Viechtach-Gotteszell dem
        &ouml;ffentlichen Verkehr &uuml;bergeben wurde, &uuml;bernahm diese Bahn
        neben G&uuml;ter- und Personentransport auch die Bef&ouml;rderung der
        Postsendungen.<br>
        1906 wurde hier eine &ouml;ffentliche Telefonstelle errichtet.
        Fr&uuml;her war das Postlokal in der Brauerei Amberger. Seit 1939
        befindet sich dasselbe im Haus des Hr. Professors Hieke. Als Leiter der
        Postagentur Ruhmannsfelden sich noch in Erinnerung: Hr. Alois
        Sagstetter, Frl. Laufenbeck und Hr. R&ouml;dl. Seitdem die Postagentur
        in eine Postanstalt umgewandelt wurde, versahen den Verwaltungsdienst
        der hiesigen Postanstalt: Frl. Mathilde Sagstetter, Frl. Wirthensohn und
        Hr. Pilch. Zurzeit ist damit Hr. Brummer betraut.</p>
      <h4>g.) Aufschlageinnehmerei<a name="ii3g"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h4>
      <p>Fr&uuml;her war in Ruhmannsfelden auch eine k&ouml;nigliche
        Aufschlageinnehmerei. Sie war eine Nebenstelle des Zollamtes Zwiesel.
        Der Aufschl&auml;ger hatte die Kontrolle und die Verrechnung &uuml;ber
        die aufschlag- und zollpflichtigen Waren. Davon waren in Ruhmannsfelden
        haupts&auml;chlich die Brauereien betroffen (Bier- und Malzaufschlag).
        Als Aufschl&auml;ger fungierten hier: Wimmer, Eisenreich, Strobl, Sturm
        und H&ouml;rner. 1924 wurde die hiesige Aufschlageinnehmerei aufgehoben
        und dem Zollamte Zwiesel zugeteilt. In fr&uuml;hester Zeit hie&szlig;
        der Aufschlag oder Zoll: „die Maut.“ Schiffe mussten von ihrer
        Schiffsladung die Maut entrichten (M&auml;useturm oder Mautturm). Von
        den schwer beladenen W&auml;gen der Kaufleute, die auf den gro&szlig;en
        Handelstra&szlig;en von S&uuml;d nach Nord oder von West nach Ost ihre
        Waren transportierten (Eisenbahnen gab es damals nicht), wurde die Maut
        heruntergeholt. Im Landkreis Wolfstein im unter bayerischen Wald
        f&uuml;hrt eine Grenzortschaft den Namen Mauth.</p>
