        den vielen Felsdurchbr&uuml;chen auf dieses Bahnstrecke nicht ohne
        Unf&auml;lle abgehen konnte, ist leicht erkl&auml;rlich. Bei einem
        Sprengschuss im Tunnel bei Ulrichsberg wurden 3 Arbeiter get&ouml;tet.
        Bei einer Dynamitexplosion in der N&auml;he von Zwiesel wurden gleich
        f&uuml;nf Bahnarbeiter get&ouml;tet. Im Ruhmannsfeldner Friedhof liegen
        viele Italiener begraben. Am 1. Februar 1877 war die erste Probefahrt
        von Plattling &uuml;ber die neue Eisenbahnbr&uuml;cke Deggendorf nach
        Eisenstein. Am 15. November 1877 wurde diese Bahnstrecke dem
        &ouml;ffentlichen Verkehr &uuml;bergeben.<br>
        1874 ging w&auml;hrend des 2. Evangeliums bei der
        Fronleichnamsprozession pl&ouml;tzlich ein Haus in Flammen auf. Am
        Dreifaltigkeitssonntag des Jahres 1875 wurde in Perlesried ein
        Raubmordversuch durch die Hilfe der Nachbarsleute von Sintweging
        verhindert. 1878 erbaute die Sepulturgemeinde Ruhmannsfelden den neuen
        Friedhof bei der Pfarrkirche. Leider hat man dabei die alten Grabsteine
        vollst&auml;ndig beseitigt, die uns vielen Aufschluss &uuml;ber die alte
        Geschichte von Ruhmannsfelden h&auml;tten geben k&ouml;nnen. Am
        17.3.1879 lie&szlig; sich der ledige Eisenrichter von hier in die Kirche
        abends einsperren und beging dann auf den Stufen des Hochaltars einen
        Selbstmordversuch. Da er mit dem Messgl&ouml;cklein l&auml;utete, wurde
        er vom Mesner geh&ouml;rt und aus seiner peinlichen Lage befreit. Die
        Kirche wurde darauf wieder eigens konsekriert. Das 700-j&auml;hrige
        Wittelsbacher Jubil&auml;um wurde auch hier feierlich begangen im August
        1880.<br>
