        Ruhmannsfelden. Wie schon erw&auml;hnt, wurde fr&uuml;her das Holz nicht
        verkauft, sondern im Walde verbrannt und daf&uuml;r die daraus gewonnene
        Asche verkauft an Seidensieder, usw. Der Aufk&auml;ufer hatte schon
        einen ziemlich hohen Haufen Asche im Hofe beim Berger Br&auml;u
        (Amberger) aufgestapelt und mit Tannen- und Fichtenzweigen zugedeckt. In
        der Nacht zum 1. Juli 1820 ging ein heftiger Wind, entfachte die
        gl&uuml;hende Asche zum lodernden Feuer und schon brannte die
        Pfarrkirche. 11 H&auml;user samt dem Broth&auml;usl wurden ein Raub der
        Flammen, ebenso auch die zwei kleinen h&ouml;lzernen Feuerspritzen und
        die <i>„me&szlig;ingerne wurde verdorben</i>.<i>“</i> Leider
        verbrannte auch das sch&ouml;ne Marienbild, das 1814 vom
        Osterbr&uuml;nnl hierher verbracht wurde. Da an einen raschen
        Wiederaufbau der Pfarrkirche nicht gedacht werden konnte, ging man
        daran, die Osterbr&uuml;nnl-Kapelle als Filialkirche auszubauen, was
        auch mit Hilfe aller Pfarrangeh&ouml;rigen bald gelang, sodass die neu
        erbaute, aber etwas tiefer herabgesetzte Osterbr&uuml;nnl-Kapelle schon
        im Jahre 1821 eingeweiht werden konnte.<br>
        Fr&uuml;her stand diese Kapelle auf der Anh&ouml;he beim hohen Kreuz, zu
        dem die 14 Stationen des Kreuzweges hinf&uuml;hren. Auch ein
        Muttergottesbild, wie das fr&uuml;here, wurde wieder neu angeschafft.
        Die 1820 abgebrannte Pfarrkirche konnte erst 1828 fertig gestellt und
        seiner Bestimmung &uuml;bergeben werden. Das Hochaltarbild ist ein sehr
        wertvolles Gem&auml;lde von Josef von Lens. Die beiden Seitenaltarbilder
        stammen von M&uuml;nchner K&uuml;nstlern und wurden in der Hauptsache
        ausgef&uuml;hrt von Martin Dorner, der ein armer, bed&uuml;rftiger, aber
        hervorragend t&uuml;chtiger Sch&uuml;ler der beiden Hofmaler Schraudolpf
        und Hauber von M&uuml;nchen war. Sehr wertvoll sind auch die
        Madonnenstatur &uuml;ber dem Taufstein und der Kreuzweg aus
        Messinggewebe gemalt von Leopold Baumann, einem geb&uuml;rtigen
        Ruhmannsfeldener. 1831 bekam die Pfarrkirche auch wieder eine Orgel. Am
        21.4.1821 starb H. Hr. Pfarrer Blaim von hier an Lungenentz&uuml;ndung
        im 49. Lebensjahr.<br>
