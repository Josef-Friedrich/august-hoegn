        Kirchen am B&uuml;hl, item die B&auml;cker im Markt. Salzh&auml;fe,.
        Eisen, Eisengeschir,. Lederer, Schuhmacher, Seiler sollen zwischen
        Hofmanns, Weningers Enzlens, Lorenz Segens und Achazens Behausung
        feilgehalten werden. Item der Schweinemarkt vor des Penzkofers und
        Michael Gr&uuml;ters Behausung feilgehalten werden. Item die Fremden
        B&auml;cker vor dem Pfarrhof, Item die Tuechschneider und Huterer vorn
        am Platz am Pranger. Doch steht es in des Pr&auml;laten Willen, solche
        Ordnung zu &auml;ndern und zu verbessern.&quot;</i></p>
      <h3>4. Ruhmannsfelden unter der Herrschaft des Klosters Gotteszell<a
  name="i4"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <p>Der Ausbau des Klosters Gotteszell mit der Kirche, den
        Konventsh&auml;usern und der Wirtschaftsgeb&auml;uden ging rasch vor
        sich. 1297 wurde das Kloster Gotteszell zu einem Aldersbacher Priorat
        erhoben und beherbergte schon 13 Klosterherren. Im Jahre 1320 wurde
        Gotteszell selbstst&auml;ndige Abtei mit 20 M&ouml;nchen. Der erste Abt
        in Gotteszell hie&szlig; Berthold.<br>
        Da sich aber die Ausgaben des Klosters seiner raschen
        Aufw&auml;rtsentwicklung entsprechend erh&ouml;hten, musste auch
        f&uuml;r die Erh&ouml;hung seiner Einnahmen gesorgt werden, was aber
        gro&szlig;e Schwierigkeiten machte. Deswegen fanden fortgesetzt
        Unterhandlungen zwischen Aldersbach und Ruhmannsfelden statt, die sich
        viele Jahre hindurch zogen. 1445 fanden solche Unterhandlungen statt
        zwischen Aldersbach und Gotteszell die „Villa Ruebmannsfelden“ zu
        vertauschen. <br>
        1496 verkaufte das Kloster Aldersbach den Markt Ruedmannsfelden
        notgedrungen an die Degenberger unter Vorbehalt des
        Wiedereinl&ouml;sens, was von Seiten des Abtes Simon von Aldersbach Ende
        des 15. Jahrhunderts auch geschah. Erst nach 50 Jahren gelang eine
        Einigung in diesen Verhandlungen. Am Freitag nach Maria Himmelfahrt im
        Jahre 1503 best&auml;tigte Herzog Albrecht der Weise einen zwischen den
        Kl&ouml;stern Aldersbach und Gotteszell vollzogenen Tausch, nach welchen
        das Kloster Gotteszell den Markt Ruhmannsfelden bekam mit Ausnahme des
        Pfarrhofes und der pfarrlichen Rechte, die vom Expositus in Geiersthal
        ausge&uuml;bt wurden, da Geiersthal noch zu Aldersbach geh&ouml;rte.
        Ruhmannsfelden stand nun vollst&auml;ndig unter der Herrschaft und der
        Gerichtsbarkeit des Klosters Gotteszell.<br>
        Bald tr&uuml;bte sich aber das gute Einvernehmen zwischen dem
        B&uuml;rgern von Ruhmannsfelden und den Klosterherren von Gotteszell.
        Die Ruhmannsfeldener B&uuml;rger wollten selbst&auml;ndig sein wie unter
        der Aldersbacher Herrschaft und 1511 schon brach der Streit aus wegen
        der Grenzen, beim H&uuml;ten, wegen der Abgaben an das Kloster, wegen
        Unterhalt des Pr&auml;latenhauses, usw. Von den Straubinger R&auml;ten
        sollte dieser Streit geschlichtet werden, was zwar auf kurze Zeit
        gelang. Aber der gleiche Streit ging bald wieder los, weil sich die
        Ruhmannsfeldener der Klosterherrschaft von Gotteszell einfach nicht
        f&uuml;gen wollten, weil sie auf ihrer eigenen Verwaltung und der
        F&uuml;hrung ihres eigenen Siegels hartn&auml;ckig bestanden. Bei diesen
        Streitigkeiten ging es auch nicht ohne gro&szlig;e Unruhen ab, die sogar
        in offenen Aufruhr ausarteten, bei denen das Pr&auml;latenhaus in
        Ruhmannsfelden in Flammen aufging (1519).<br>
        Die Ruhmannsfeldner hatten einen unabh&auml;ngigen Rat gew&auml;hlt,
        brachten die Landsteuer eigenm&auml;chtig ein. Das Gericht in Viechtach
        musste dagegen einschreiten. Der Markt mit seinen B&uuml;rgern wurde
        hart bestraft. Die Ruhmannsfeldner B&uuml;rger mussten an das Kloster
        Gotteszell Entsch&auml;digung zahlen, die B&uuml;rger durften auf keinem
        Landtag mehr erscheinen und die Leibesstrafen wurden ihnen angedroht.
        Dessen ungeachtet brachen drei Jahre darauf (1522) schon wieder Unruhen
        aus, bei denen der Markt durch Br&auml;nde einen bedeutenden Schaden
        erlitt. Auch die Kirchentrennung und Glaubensspaltung, die 1517 ihren
        Anfang nahm, machte sich mit allen ihren Folgen in Ruhmannsfelden
        bemerkbar, sodass die Unruhen und Streitigkeiten nicht mehr auszugehen
        schienen. 1540 brach in Ruhmannsfelden eine neue Revolution aus. Die
        Leibesstrafen mussten erh&ouml;ht werden. Trotzdem gingen Handel und
        Wandel bei den Ruhmannsfeldnern ihren gewohnten Weg weiter und in einer
        friedlichen Zwischenzeit wurde der Markt sogar mit neuen, festen
        Turmwerken versehen und zugleich entstand (1566) ein neues Marktviertel,
        das Kalteck, das damals aus 12 H&auml;usern bestand. Mit der Zeit haben
        sich die Ruhmannsfeldner B&uuml;rger auch etwas mehr Recht und Freiheit
        erworben. Freilich fehlte noch viel zur Erreichung ihres ersehnten
        Zieles. In der 2. H&auml;lfe des 1. Jahrhunderts brannte des
        Pfarrgotteshaus St. Laurentius zum gr&ouml;&szlig;ten Schmerz der
        Ruhmannsfeldener zum wiederholten Male ab. F&uuml;r die
        Wiederherstellung des abgebrannten Gotteshauses geschah viel von Seiten
        des Kloster Gotteszell. Die Glocken, die von einem M&uuml;nchner
        Glockengie&szlig;er stammten, konnten erst 60 Jahre sp&auml;ter auf den
        Turm angebracht werden. Das leider im letzten Weltkrieg zu Verlust
        gegangene Osterbr&uuml;nnl-Gl&ouml;cklein mit seinem herrlich reinen Ton
        stammte aus dieser Zeit. Es trug die Aufschrift: <i>„hans durnknopf
        Regenspurg 1550.“</i> Von dem gleichen Glockengie&szlig;er waren solch
        tonreine Glocken noch auf 7 niederbayerischen Pfarrt&uuml;rmen und dazu
        die Feuerglocke auf dem Stadtturm in Straubing. Ob diese herrlichen
        Glocken nicht auch die Kriegfurie von den T&uuml;rmen herab gerissen hat
        wie das Osterbr&uuml;nnl-Gl&ouml;cklein, ist uns nicht bekannt.<br>
        Vieles hatte sich in Ruhmannsfelden seit seiner Entstehung (um 950) und
        seiner Erhebung zum Markt (um 1400) ereignet. Aber in der nun folgenden
        Zeit des 30-j&auml;hrigen Krieges (1618 - 1648) sollte Ruhmannsfelden
        unendlich mehr zu erdulden bekommen. Schwedische Kriegsv&ouml;lker kamen
        1633 auf ihren Durchz&uuml;gen von Deggendorf &uuml;ber Viechtach nach
        Cham und sp&auml;ter auf ihren R&uuml;ckm&auml;rschen auch nach
        Ruhmannsfelden und Umgebung (Schwedentrunk). Die Pfarrkirche wurde nebst
        den &uuml;brigen H&auml;usern im Markte ausgepl&uuml;ndert und das
        Pfarrvikarhaus und die Klostertaverne in Schutt und Asche gelegt.<br>
        Genau so erging es dem Kloster Gotteszell, das w&auml;hrend dieser Zeit
        dreimal gebrandschatzt wurde. Selbst als der Westf&auml;lische Friede
        (1648) schon abgeschlossen war, r&uuml;ckte eine Abteilung schwedischer
        Soldaten von B&ouml;hmen her in Bayern ein. Auf ihrem Durchzug in
        hiesiger Gegend blieb eine Kompanie in der Gegend von Achslach
        zur&uuml;ck und lie&szlig; sich in Wolfertsried h&auml;uslich nieder und
        machte von da aus die Streifz&uuml;ge. Die Bewohner von Achslach
        schlossen sich aber zusammen und vernichteten die letzten Schweden in
        dortiger Gegend in einer Nacht. Diese sollen im sogenannten Schwedenloch
        bei Wolfertsried beerdigt sein. Der Ort Ruhmannsfelden erlitt in der
        Zeit von 1633 bis 1643 den f&uuml;r jene Zeit ungeheuren Schaden, nach
        damaligen Sch&auml;tzungen, von 50 000 Gulden, wenn man unter anderem in
        Betracht zieht, dass unmittelbar vor dieser ungl&uuml;cklichen
        Kriegszeit Ruhmannsfelden heimgesucht wurde von der Pest (1613), dem
        Viehfall (1620), und anderem mehr. Von der Bev&ouml;lkerung waren nur
        mehr wenige am Leben, das Geld und die Wertsachen waren gestohlen, das
        Vieh tot, die Felder und Wiesen 30 Jahre nicht mehr bewirtschaftet. Dass
        es da in unserem Vaterland und auch hier sehr traurig ausgesehen haben
        muss, kann man sich leicht vorstellen. Die Leute mussten die Arbeit
        wieder von vorne anfangen und Verdienst und Einnahmequellen suchen.<br>
        Zur dauernden Erinnerung an den 30-j&auml;hrigen Krieg und zum Gedenken
        an all die vielen Gefallen auf den Kriegsschaupl&auml;tzen und die
        vielen Toten in der Heimat w&auml;hren dieses unseligen Krieges haben
        die Ruhmannsfeldner im Jahre 1687 am &ouml;stlichen Marktausgang einen
        Gedenkstein errichtet (Stegm&uuml;hle).<br>
        Damit das Kloster Gotteszell mehr Einnahmen erhielt, um die erlittenen
        Kriegssch&auml;den wieder gut machen zu k&ouml;nnen, wurde 4 Jahre nach
        Beendigung des 30-j&auml;hrigen Krieges unter Abt Gerhard die
        pfarramtliche Seelsorge in Ruhmannsfelden, die bisher von der Pfarrei
        Geiersthal ausge&uuml;bt wurde, auf das Kloster Gotteszell
        &uuml;bertragen (1652). Von dieser Zeit an bis zur Aufhebung des
        Klosters Gotteszell blieb der Markt Ruhmannsfelden dem Kloster
        Gotteszell unterstellt. Allm&auml;hlich traten auch an Stelle von
        Unglauben, Aberglauben, Zauberei und Hexerei wieder religi&ouml;se
        Fr&ouml;mmigkeit und echter Gottesglaube. Besonders pflegten aber damals
        die Leute in ihrer seelischen Not und in ihrem wirtschaftlichen Elend
        die Marien-Verehrung. An vielen Orten wurden in der Nachkriegszeit der
        30-j&auml;hrigen Krieges Marien-Kapellen erbaut oder schon bestehende
        Kapellen in Marien-Kapellen umbenannt. So entstanden in damaliger Zeit
        auch die Osterbr&uuml;nnl-Kapellen. Ob das hiesige
        Osterbr&uuml;nnlkirchlein in dieser Zeit entstanden ist oder schon
        fr&uuml;her, dar&uuml;ber berichten uns Urkunden nichts. Es k&ouml;nnte
        sein, dass das Osterbr&uuml;nnlkirchlein schon fr&uuml;her erbaut wurde
        als Notkirchlein in einer Zeit, da die Pfarrkirche in Schutt und Asche
        lag und das war ja &ouml;fters der Fall. F&uuml;r alle F&auml;lle ist es
        aber nicht die herabgesetzte Schlosskapelle, weil es ein Schloss auf der
        dortigen Anh&ouml;he niemals gab.<br>
        Nachdem die Errichtung der Osterbr&uuml;nnl-Kapelle und gleichzeitig die
        vielen wundert&auml;tigen Heilungen an diesem Gnadenort weit und breit
        bekannt wurden, setzte ein so gro&szlig;er Besuch dieser St&auml;tte
        ein, sogar bis aus B&ouml;hmen und &Ouml;sterreich kamen Wallfahrer
        hierher, dass das Kloster Gotteszell sich durch den geringer Besuch
        seiner kirchlichen Veranstaltungen und die Gesch&auml;ftsleute der
        Ortschaft Gotteszell sich durch den dadurch entstanden Einnahmeausfall
        benachteiligt f&uuml;hlten. Urkundlich erscheint das Osterbr&uuml;nnl im
        Jahre 1724.<br>
        Dort kann man folgendes lesen: <i>„Da ist die h&ouml;lzerne Kapelle
        beim Osterbr&uuml;nnl mit den Votivtafeln verbrannt. Das darin sich
        befindliche Mutter Gottesbild von Alten&ouml;tting wurde in das Kloster
        Gotteszell abgeliefert - auch die bei der Kapelle gestandene, steinerne
        Marters&auml;ule mit dem Bildnis der allerheiligsten Dreifaltigkeit und
        statt derselben wurde ein Schaup Stroh aufgestellt.“</i> Der Abt
        Wilhelm II. von Gotteszell berichtet, dass das Bild der Muttergottes von <i>„Alten&ouml;tting“</i> an einem Baum ohne seine Erlaubnis
        aufgestellt wurde und dass dazu eine Kapelle aus Holz erbaut wurde und
        ein Opferstock aufgestellt wurde. Er habe das Bild abnehmen und die
        Kapelle verbrennen lassen. Die B&uuml;rger von Ruhmannsfelden hetzten
        dagegen eben einquartierte Reiter auf, welche wieder ein Bild
        aufstellten. Darauf lie&szlig; der Abt den Baum umhauen. Die B&uuml;rger
        h&auml;ngten aber wieder Tafeln auf, welche der Abt immer abnehmen und
        die bereffenden B&auml;ume umhauen lie&szlig; und ihnen unter Strafe
        verbat, diese Osterandacht weiter zu treiben. Die Ruhmannsfeldener
        erkl&auml;rten, sie lie&szlig;en sich das Beten nicht nehmen. Der Abt
        erkl&auml;rte ihre Andacht als Baum- und Stockverehrung. Das
        Bisch&ouml;fliche Konsistorium verf&uuml;gte, dass die Kapelle im
        Osterbr&uuml;nnl nicht mehr errichtet werden d&uuml;rfe. Erst nach dem
        Brand des Marktes im Jahre 1820 ging man notgedrungen an die Erbauung
        des Osterbr&uuml;nnl-Kirchleins.<br>
        Heute ist das Osterbr&uuml;nnl-Kirchlein, ganz neu renoviert und mit
        einer neuen Glocke versehen, die von Hr. J. Ederer appr. Bader von
        Ruhmannsfelden gestiftet wurde, das Ziel vieler, vieler Wallfahrer aus
        nah und fern, die alle mit ihren Bitten und Dankgebeten zu dem in
        jetziger Nachkriegszeit gestohlenen und wieder zur&uuml;ckgebrachten
        Gnadenbilde im stillen Heiligtume der Osterbr&uuml;nnl-Kapelle
        wallfahren.<br>
        Kaum waren die Sch&auml;den des 30-j&auml;hrigen Krieges
        einigerma&szlig;en behoben, erfolgte der mit argen Quartierlasten
        verbundene R&uuml;ckmarsch von &ouml;sterreichischen Truppen aus
        Richtung Cham (1704). Am Weihnachtstag 1705 war die Sendlinger
        Bauernschlacht und am 8. Januar 1706 wurden die niederbayerischen Bauern
        von den &Ouml;sterreichern bei Aidenbach unweit Vilshofen besiegt. 2000
        bis 3000 niederbayerische Bauern verloren dabei ihr Leben, darunter
        viele Bauern aus dem Bezirke Viechtach und auch von Ruhmannsfelden. Zum
        ehrenden Gedenken an die bei Aidenbach 1706 gefallenen Ruhmannsfeldener
