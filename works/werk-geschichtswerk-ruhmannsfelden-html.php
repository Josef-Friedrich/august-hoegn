        Schlosser, Bartscherer. Das Gewerbe der Leinweber und Zeugmacher war in
        hiesiger Gegend besonders vertreten. Im Markt Ruhmannsfelden stand ein
        eigenes Zunfthaus.<br>
        1813 kam die Wallfahrt zur Osterbr&uuml;nnl-Gnadenst&auml;tte in
        verst&auml;rktem Ma&szlig;e wieder auf. Da lie&szlig;en Josef Baumann
        und Anton Schl&ouml;gl von Ruhmannsfelden eine neue h&ouml;lzerne
        Kapelle errichten. Au&szlig;erdem lie&szlig;en sie ein gro&szlig;es
        Marienbild anfertigen, das in feierlicher Prozession von der Pfarrkirche
        in die neue Kapelle gebracht wurde.<br>
        In dem Opferstock dortselbst wurde viel Geld eingelegt. Als Landrichter
        Beyerlein von Viechtach dies h&ouml;rte, lie&szlig; er die Kapelle im
        Oktober 1814 niederrei&szlig;en. Das Muttergottesbild wurde dann in der
        Pfarrkirche unter der Kanzel aufgestellt.<br>
        Im Jahre 1817 herrschte gro&szlig;e Not. Das Getreide, das nur sehr
        wenig war, hatte furchtbar hohe Preise. In dieser Notlage mussten sich
        die einen Lins und die anderen blo&szlig; Scho&szlig;w&uuml;rze oder
        Moos, das man zum Einstreuen nicht gut brauchen konnte, mahlen lassen.
        Das gab sicher kein gutes Brot. Aber als das beste Brot in dieser Zeit
        galt noch das Kleinenbrot.<br>
