        am 14.10.1825 eine „Marktgemeinde“ unter dem Landgerichte Viechtach.
        Da die damaligen kleinen Zimmer, die f&uuml;r Unterrichtszwecke
        ben&uuml;tzt wurden (im Haus des Bielmeier Eierh&auml;ndlers), den
        Erfordernissen nicht mehr entsprachen, wurde 1834 ein neuer Schulhaus
        aus Bruch- und Ziegelsteinen an der Stra&szlig;e nach Gotteszell erbaut
        1835 er&ouml;ffnet, Dieses Schulhaus ist das erste und damit das alte
        Schulhaus in Ruhmannsfelden. In diesem Schulhaus waren zwei Schulzimmer,
        die Wohnung der Lehrers und Hilfslehrers (Schulgehilfen).<br>
        1841/44 wurde der jetzige Pfarrhof gebaut. 1842 bekam Ruhmannsfelden
        eine Feuerspritze, die sich dann darauf bei dem Brande des
        Moosm&uuml;ller-Zeugweberhauses bestens bew&auml;hrte. Eine Feuerwehr
        gab es damals noch nicht. Diese wurde erst im August 1867
        gegr&uuml;ndet. Josef Fromholzer b&uuml;rgerlicher F&auml;rberssohn von
        hier erlitt bei einem Theaterbrande in Karlsruhe (Baden) derartige
        Brandwunden, dass er daran sein junges Leben einb&uuml;&szlig;en musste.
        Er wurde in Karlsruhe feierlichst beerdigt. Im Februar 1849 trug sich
        beim Steinbauer in Haberleuthen ein ganz seltener Fall zu. Da kam
        n&auml;mlich ein angeschossener Hirsch auf den Stadelfirst hinauf,
        st&uuml;rzte auf der anderen Seite des Daches herunter auf einen
        Apfelbaum und dort zu Boden, wo er dann von dem Sohn der Steinbauern mit
        dem Haust&uuml;rriegel erschlagen wurde. In einer Nacht des Jahres 1851
        hat man den 37-jahrigen Gemeindediener Josef Moosm&uuml;ller ermordet. 3
        Jahre sp&auml;ter hat man den verheirateten Bauern Achatz von Perlesried
        tot (infolge eines Sturzes von einem Baume) aufgefunden.<br>
        In der Mitte des Marktplatzes stand fr&uuml;her eine
        &uuml;berlebensgro&szlig;e barocke Statue des Johannes von Nepomuk
        inmitten von 4 Alleb&auml;umen. Diese Statue wurde im Jahre 1885 von
        diesem Platz entfernt und zun&auml;chst an das Haus des Hr. Rauch
        gestellt. Hier blieb sie bis Hr. Rauch die L&auml;den in sein Haus
        einbaute. Dann kam die Statue an das Haus des Hr. Fromholzer, wo sie
        heute noch steht. Die 4 Alleb&auml;ume kaufte Bierbrauer Sagstetter und
        in die Mitte des Marktplatzes kam ein sch&ouml;ner Marktkorbbrunnen aus
        Granit mit der Statue der Patrona Bavaria 1859. Alle Marktb&uuml;rger
        steuerten bei zur Deckung der Kosten dieses Marktbrunnens und Hr. Alois
        Fromholzer stellte seinen B&uuml;rgermeisterjahresgehalt gleich f&uuml;r
        2 Jahre zu diesem Zwecke zur Verf&uuml;gung.<br>
        Im Jahre 1862 wurden am bauf&auml;lligen Glockenturm der hiesigen
        Pfarrkirche gr&ouml;&szlig;ere Reparaturen vorgenommen und der Turm mit
        Wei&szlig;blech eingedeckt. Im gleichen Jahre wurde aus dem Tabernakel
        der Pfarrkirche eine wertvolle Monstranz gestohlen.<br>
        1867 wurde aus Krankenhaus hier (jetzige Kinderbewahranstalt) gebaut,
        was sich gleich als sehr n&uuml;tzlich erwiesen hat, da im gleichen Jahr
        der Typhus herrschte und von 9 Todesf&auml;llen dabei berichtet wird.<br>
