        Rosina Hinderholzer, verwitwete Schulmeisterin zu Ruhmannsfelden ihre
        Markbehausung am Kalteck an ihren Tochtermann Martin Staudenberger,
        B&uuml;rger und Schneider in Ruhmannsfelden. 1784 wirkte hier der
        Schulmeister Herrmann und der gepr&uuml;fte Eremit Franz Pitsch von
        Gotteszell. Dieser hatte auf dem Kalvarienberg bei Gotteszell ein
        kleines H&auml;uschen. In diesem unterrichtete er die Gotteszeller und
        Ruhmannsfeldner Jugend. Da aber im Laufe der Zeit den Ruhmannsfeldner
        Kindern der Weg nach Gotteszell, zumal bei schlechtem Wetter, zu
        beschwerlich war, ging der Eremit Pitsch nach Ruhmannsfelden und
        erteilte hier mit Herrmann den Unterricht. Nach Wegzug des Eremiten
        Pitsch von Gotteszell hat Amad&auml;us in Gotteszell (1803) hat man die
        Wohnung des Pfarrvikars und die des Schulmeisters und Mesners in
        Ruhmannsfelden verkauft und daf&uuml;r ein einziges, zwar
        gr&ouml;&szlig;eres, aber sehr bauf&auml;lliges Haus angekauft und darin
        den Pfarrer und Kooperator, samt Lehrer und Gehilfen und das Schullokal
        f&uuml;r 200 Schulkinder und den Mesner untergebracht. Das war aber nur
        ein Notbehelf und die unhaltbaren Zust&auml;nde, die sich im Laufe der
        Zeit in diesem Gemeinschaftshaus f&uuml;r Pfarrer, Lehrer und Schule
        ergaben, mussten zu einer &Auml;nderung dieser Verh&auml;ltnisse
        f&uuml;hren.</p>
      <h3>5. Ruhmannsfelden wird selbst&auml;ndig<a name="i5"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h3>
      <p>Kaum hatte sich das Kloster Gotteszell nach all den Wirrnissen,
        Drangsalen und Kriegssch&auml;den wieder einigerma&szlig;en erholt, kam
        pl&ouml;tzlich die Aufhebung des Klosters im Jahre 1803. Die
        Klostergeistlichen mussten sich um Unterkunft oder Wiederverwendung als
        Geistliche in den umliegenden Pfarreien umsehen. Die
        Klostergeb&auml;ulichkeiten, die &Auml;cker, Wiesen und W&auml;lder
