      <h3>HAUPTTEIL</h3>
      <h2>I. Geschichtliches<a name="i"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h2>
      <h3>1. Wie hat es vor seiner Entstehung ausgesehen?<a name="i1"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h3>
      <p>Wo unser Heimatort Ruhmannsfelden ist, da schaute es fr&uuml;her ganz
        anders aus als heute. Es gab keine Ortschaften, keine H&auml;user,
        Stra&szlig;en und Wege, keine Br&uuml;cken oder gar Eisenbahnen. In den
        undurchdringlichen W&auml;ldern mit den Riesenb&auml;umen von Eiben,
        Buchen und Eichen, mit den hohen Str&auml;uchern, Gift- und
        Heilkr&auml;utern und den dichten Mooren und Moosen, da hausten Tiere
        der verschiedensten Art und Gr&ouml;&szlig;e, Tiere, die schon
        l&auml;ngst ausgestorben sind. In den Felsh&ouml;hlen und Steinriegeln
        lebten die B&auml;ren, W&ouml;lfe, Luchse und F&uuml;chse, die
        unheimlichen Salamander, Eidechsen und Nattern. In den Gew&auml;ssern
        schwammen unz&auml;hlige Fische und Wassertiere. In hohlen B&auml;umen
        wohnten die Wildkatzen und Marder und gro&szlig;m&auml;chtige Adler,
        Bussarde, Habichte und Sperber hatten ihre Nester in den dichten Kronen
        der hohen B&auml;ume, Menschen gab es in dieser Wildnis anf&auml;nglich
        noch nicht. Es fehlten ja alle notwendigen Lebensbedingungen f&uuml;r
        sie und die ersten Menschen, die aus B&ouml;hmen &uuml;ber den Arber
        her&uuml;ber kamen und in der Richtung zur Donau und zur Donauebene
        unser Gebiet hier durchzogen, die mussten sich erst m&uuml;hsam einen
        Weg durch diesen Urwald suchen und nur da, wo es sich um eine von Natur
        aus lichte Waldstelle handelte und wo sie einigerma&szlig;en von den
        wilden Tieren sicher waren, konnten sie ihre Rast- und Ruhepl&auml;tze
        anlegen, die dann von ihren gro&szlig;en und kr&auml;ftigen Hunden
        bewacht wurden.</p>
      <h3>2. Der Nordgau kommt in den Besitz des Klosters Metten.<a name="i2"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h3>
      <p>Diesen Urwald lernte auch der damalige Kaiser Karl der Gro&szlig;e
        kennen bei seinen Jadgstreifen in den Vorbergen des bayerischen Waldes
        der Donau entlang. Bei einem solchen Jagdausflug in die wildreichen
        W&auml;lder traf er bei der Ortschaft Berg in der N&auml;he von
        Deggendorf den frommen und wundert&auml;tigen Einsiedler Utto, dem er
        versprach, ein Kloster in dieser Gegend erbauen zu wollen. Durch die
        t&auml;tige und verst&auml;ndnisvolle Arbeit des Einsiedlers Utto und
        mit Hilfe der kaiserlichen Unterst&uuml;tzung entstand das Kloster
        Metten, das sich immer der Gunst h&ouml;chster und weitester Kreise
        erfreuen und sich durch alle St&uuml;rme und Drangsale des fehdereichen
        Mittelalters und der kriegerischen Jahrhunderte der Neuzeit hindurch bis
        auf den heutigen Tag erhalten konnte und in allen L&auml;ndern und auf
        allen Erdteilen als erstklassige Pflanzst&auml;tte der geistigen Bildung
        der Jugend bekannt ist.<br>
        Da Kaiser Karl der Gro&szlig;e Kirche und Kl&ouml;ster reichlich
        beschenkte, bekam auch das neu entstandene Kloster Metten von ihm das
        ganze Gebiet rund um unseren Heimatort herum, das damals „Nordgau“
        genannt wurde. Dieser Nordgau umfasste das Gebiet innerhalb der Grenze:
        Metten, Edenstetten, Fa&szlig;lehen, Voglsang, Kohlbach,
        K&ouml;ckersried, Zachenberg, Klessing, Eckersberg, Unterauerkiel,
        Asbachm&uuml;ndung, Altnussberg, Seigersdorf, Fernsdorf, Frankenried,
        Hornberg, Einweging, Schusterstein, &Ouml;dwies, Hirschenstein, Kalteck,
        Edenstetten, Metten.<br>
        Innerhalb dieser Gebiets, das nun dem Kloster Metten geh&ouml;rte
        errichteten die damaligen Klosterherren von Metten gro&szlig;e
        H&ouml;fe, welche Sie bewirtschafteten mit Hilfe von Arbeitern, die sie
        von Metten und der Donauebene mitgebracht hatten. Ein solcher Hof wurde
        benannt nach dem Namen des Hofverwalters. Da es damals nur Taufnamen gab
        und der Hof &quot;Villa d. h. Dorf&quot; genannt wurde, so hie&szlig;
        der Hof Patos der Patoshof = Patersdorf, der Hof Fatos der Fatoshof =
        Fratersdorf, der Hof Lempfers (Landfrit) Lempfershof = L&auml;mmersdorf.
        Bei diesen H&ouml;fen fehlten auch nicht Obst-, Gem&uuml;se-
        Hopfeng&auml;rten, auch nicht die Weinberge (Patersdorf). Allerdings
        konnten sich die Mettener Klosterherren nicht lange des Besitzes vom
        Nordgau, wie das hiesige Gebiet fr&uuml;her gehei&szlig;en hat,
        erfreuen. Alles, was sie hier gesessen haben, wurde ihnen von Arnulf dem
        B&ouml;sen (911 - 937) enteignet und dem Grafen Hartwig von Bogen
        zugesprochen f&uuml;r seine dem Arnulf geleisteten Hilfsdienste.
        &Uuml;berdies konnte Graf Hartwig dieses Gebiet notwendig brauchen, da
        er sehr viele Kinder hatten (angeblich 38) und jedem von seinen
        S&ouml;hnen ein m&ouml;glichst gro&szlig;es Besitztum geben und eine
        ertragreiche und einflussreiche Stellung mit Amt, Macht und W&uuml;rde
        (Grafensitz, Bischofsitz und dergleichen) sichern wollte.</p>
      <h4>a.) Um 950 unter Graf Aswin von Bogen entstanden Siedlungen<a
  name="i2a"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h4>
      <p>Auf diese Weise gelangte nun die Landschaft um unseren Heimatort
        herum in den Besitz von Aswin, einem Sohn des Grafen Hartwig von Bogen.
        Dieser schickte sofort Arbeiter hierher, die das Land urbar machen
        mussten. Dabei musste vor allem der Urwald aus dem Wege ger&auml;umt
        werden. Mit S&auml;ge und Hacke h&auml;tte man aber die kolossal dicken
        und riesig hohen B&auml;ume nicht f&auml;llen k&ouml;nnen. Infolgedessen
        musste der Wald niedergebrannt werden. Das Holz war ja damals
        vollst&auml;ndig wertlos, weil es ohnehin zu viel gab. Nur die Asche
        konnte sp&auml;ter verkauft werden an die Seifensieder.<br>
        Die Namen der Orte Pr&uuml;nst, Brenning, Klessing, usw. und alle -sang,
        -seng und -sing Namen deuten heute noch hin auf die Pl&auml;tze der
        damaligen gro&szlig;en Waldbr&auml;nde.<br>
        Der Waldboden wurde dann von den Baumst&ouml;cken und -wurzeln gerodet
        und anbauf&auml;higer Boden daraus gemacht. Zugleich wurden an diesen
        Orten Wohnst&auml;tten f&uuml;r die Rodungsarbeiter errichtet. So
        entstanden zu dieser Zeit die &quot;ried&quot;-Namen, z. B. Giggenried
        bei L&auml;mmersdorf, Kaikenried bei Fratersdorf, Zuckenried bei
        Patersdorf. Diese &quot;ried&quot;-Namen sind wie die Dorfnamen auch mit
        dem Taufnamen des Rodungsvorarbeiters verbunden, z. B.:</p>
      <table>
        <tr>
          <td>K&ouml;ckersried= Choteschalk (ried)<br>
            Kaikenried = Hacco<br>
            Giggenried = Chundahar<br>
            Zuckenried = Sigine </td>
          <td><p align=left style='text-align: left'>Hasmannsried = Hasnolf<br>
              Lobetsried = Luipolf<br>
              Perlesried = Perolf<br>
              Triefenried = Trunolf</p></td>
        </tr>
      </table>
      <p>Die &quot;dorf&quot;-Namen (Mettener Gr&uuml;ndungen) sind also die
        &auml;ltesten. Dann kommen die &quot;ried&quot;-Namen (Bogener
        Gr&uuml;ndungen). Gn&auml;nried, auch Gnadenried genannt = Kleinried,
        Gollersried oder K&ouml;hlersried = Gottlesried, usw. z&auml;hlen nicht
        zu den echten &quot;ried&quot;-Namen. Zu gleicher Zeit erscheinen auch
        die &quot;berg&quot;-Namen, die aber auch mit Taufnamen verbunden sein
        m&uuml;ssen, z. B. Dietsberg = Theotosberg, Wolfsberg = Wolfichosberg,
        usw. Harnberg = Heidenberg z&auml;hlt nicht zu den echten
        &quot;berg&quot;-Namen. Dieser Name sagt uns, dass dort Harn oder Flachs
        gebaut wurde. Die an der Westseite von Ruhmannsfelden dicht
        zusammengedr&auml;ngten &quot;ing&quot;-Namen sind keine echten
        &quot;ing&quot;-Namen, weil sie nicht in Verbindung stehen mit einem
        Taufnamen, z. B. Einweging = wag, wog, w&ouml;ge, weging = Wasser in
        einem Graben oder Weiher - Sintweging = viel Wasser, Tradweging = drate
        = eilig, schnell, rei&szlig;end = Stelle des rei&szlig;enden Wassers.
        Dazu geh&ouml;ren auch Handling, Zottling (Zeidler- oder Imkerort).<br>
        Wie bereits erw&auml;hnt, lie&szlig; Graf Aswin den Waldboden in
        hiesiger Gegend roden. Ein Vorarbeiter leitete die Rodungsarbeiten. Der
        Mann, der diese Arbeiten hier leitete, war ein Bediensteter
        (ministeriale, also kein Adeliger) des Grafen Aswin von Bogen und
        f&uuml;hrte den sch&ouml;nen Namen &quot;Hrothimar oder Hrothmar oder
        Rumar.&quot; Hrot(i) = Ruhm, Sieg und mar(u) = ber&uuml;hmt, also Rumar
        = der Siegber&uuml;hmte.<br>
        Dieser Rumar errichtete sich eine Siedlung, bestehend aus Wohnhaus,
        Stall und Stadel. Seine Dienstmannen bauten sich anschlie&szlig;end an
        diesen Hof (Villa oder Dorf) in sch&ouml;nem Viereck (siehe
        Vierecksanlage der heutigen Bachgasse) ihre damals noch &uuml;blichen
        niederen Wohnh&auml;user. Zum Zeichen der Wehrhaftigkeit wurde dieser
        Siedlung ein viereckiger Turm aus Findlingssteinen, Erdreich und Lehm
        errichtet (siehe Turmruine in Linden). Die ganze Siedlung erhielt den
        Namen der Vorarbeiters, wie bei den &quot;dorf&quot;- und
        &quot;ried&quot;-Namen, also den Namen Rumars und wurde deshalb
        Ruhmarsfeld, Rumarsfelden genannt. („felden“ nicht im Sinne eines
        einzigen Feldes, sondern im Sinne eines gr&ouml;&szlig;eren Gebietes).<br>
        Der Name &quot;Rumarsfelden&quot; wird verschiedentlich geschrieben. In
        einer Oberaltteicher Urkunde von 1184 steht noch
        &quot;Rumarsfelden&quot;. In einer sp&auml;teren Niederalteicher Urkunde
        hei&szlig;t es schon &quot;Rudmarsfelden&quot; und 1394 schrieb man
        &quot;Rumatzfelden&quot;. In einer Urkunde von 1448 erscheint der Name
        &quot;Ruebmannsfelden&quot;. R&uuml;be im Trutzwappen) und auf der
        Fink´schen Karte aus der 2. H&auml;lfte des 17. Jahrhunderts kann man
        &quot;Ruemannsfelden&quot; lesen. Nach dieser Zeit hat man an die Stelle
        des &quot;e&quot; in diesem Worte ein &quot;h&quot; gesetzt und seit
        dieser Zeit schreibt man Ruhmannsfelden. Die Formen
        &quot;Rumarsfelden&quot; oder &quot;Ruhmarsfelden&quot;, so wie sie das
        Volk hier noch heute ausspricht, die tragen den Stempel der
        Originalit&auml;t.</p>
      <h4>b.) Ruhmannsfelden unter den Nachfolgern Rumars.<a name="i2b"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h4>
      <p>Rumars Nachfolger waren flei&szlig;ige Leute, sodass bald um die
        Siedlung herum sch&ouml;ne Felder und Wiesen entstanden. Wege gebaut,
        Stege (Stegm&uuml;hle) und Br&uuml;cken (Bruckhof und Bruckm&uuml;hle)
        errichtet und Weiher (Weiherwiesen) angelegt wurden. Sie waren auch
        t&uuml;chtige J&auml;ger und gingen in Spiel und Wettkampf
        (Alkovenschl&uuml;sselrennen, H&uuml;rdenspringen, Steinheben und
        Steinwerfen, Armbrustschie&szlig;en, Speerwerfen, Lanzen- und
        Ringelstechen zu Fu&szlig; und auf dem Pferd, mit und ohne Sattel)
        meistens als Sieger hervor. Arnold von Rumarsfelden wird gefeierter
        Sieger bei einem Ritterspiel in Z&uuml;rich in der Schweiz. Aber die
        Nachfolger Rumars waren nicht lange hier. Das Geschlecht der Rumar starb
        bald aus. Die Siedlung Ruhmannsfelden kam in der 2. H&auml;lfte des 13.
        Jahrhunderts in den Besitz der an der Donau zwischen Plattling und Bogen
        reichbeg&uuml;terten Grafen von Pfelling. Nach dem Tode Heinrichs von
        Pfelling, der neben Burg und Ortschaft Ruhmannsfelden auch einige
        Ortschaften um Ruhmannsfelden herum sein Eigen nannte, fiel dies alles
        dem Landesherren Heinrich XIII. zu. Nach dessen Tode kam Ruhmannsfelden
        in den Besitz seiner S&ouml;hne Otto III., Ludwig III. und Stefan I.
        (1290). Diese verkauften und vertauschten Grundst&uuml;cke und
        H&ouml;fe, die zur Siedlung Ruhmannsfelden geh&ouml;rten, an reiche
        Grafen z. B. an die Degenberger, Parsberger usw. Der Wehrturm wurde
        abgebrochen.<br>
        Erst sp&auml;ter gelangte die Siedlung Rumarsfelden in den Besitz des
        Zisterzienserklosters Aldersbach durch Kauf (1295). Lauf Brief vom 28.
        April 1294, ausgestellt in Regensburg, verkaufte Herzog Otto III. mit
        Zustimmung seiner beiden Br&uuml;der Ludwig und Stefan die Ortschaft
        Ruhmannsfelden &quot;um die Last ihrer Schulden zu erleichtern&quot; und
        aus Anh&auml;nglichkeit an den Zisterzienserorden mit allen
        W&auml;ldern, Fischwassern, Wegen und Stegen, Viehweiden, M&uuml;hlen,
        dem Bruckhof mit der M&uuml;hle, dem Dorfe Arnetsried, dem Weiler
        Labertsried, der Ortschaft Weichselsried, dem Hofe zu Lemersbach und zu
        Zierbach, einschlie&szlig;lich Gerichtsbarkeit (mit Ausnahme &uuml;ber
        Stra&szlig;enraub, Notzucht und Totschlag) und Grundbarkeit um 400 Pfund
        Regensburger Pfennig an das Kloster Aldersbach. Im Jahre 1292 am 4. Mai
        wurde der Verkauf abgeschlossen.</p>
      <h3>3. Ruhmannsfelden unter den Zisterziensern von Aldersbach<a
  name="i3"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <h4>a.) Unter der Herrschaft des Klosters Aldersbach ging es
        Ruhmannsfelden gut.<a name="i3a"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h4>
      <p>Unter dieser Herrschaft bl&uuml;hte Ruhmannsfelden auf. Am B&uuml;hl
        stand eine kleine h&ouml;lzerne Kapelle zu Ehren des hl. Laurentius. Die
        Sebastiani-, Laurentius- und Magdalenen-Kapelle standen &uuml;berall
        au&szlig;erhalb der eigentlichen Siedlung. Bei dieser Laurentius-Kapelle
        am B&uuml;hl war hie und da eine Andacht oder ein Gottesdienst, zu dem
        ein Geistlicher von Geiersthal kommen musste. Vermutlich war bei der
        Laurentius-Kapelle auch die erste Begr&auml;bnisst&auml;tte.<br>
        Nachdem die Siedlung dem Kloster Alderbach geh&ouml;rte, wurde aus der
        Kapelle ein sch&ouml;nes Kirchlein gemacht, wenn auch anf&auml;nglich
        noch ein kleines und es fanden nun &ouml;fters und regelm&auml;&szlig;ig
        hier Gottesdienste statt. Da kamen auch H&auml;ndler und Kr&auml;mer von
        Viechtach, Regen und Deggendorf. Diese schlugen zu beiden Seiten des
        Weges von der Siedlung zum Kirchlein hinauf und um das Kirchlein herum
        ihre Verkaufsst&auml;nde und Buden auf. Sp&auml;ter bauten sich die
        H&auml;ndler und Kr&auml;mer feste Wohnsitze hier und auf diese Weise
        entstand anschlie&szlig;end an die Siedlung, in der sich die Lederer,
        Gerber, Stricker usw. bereits vorher sesshaft gemacht hatten, der untere
        Markt mit den B&auml;ckereien, Metzgereien, Brauereien und Fragnereien
        und zwar wieder in Vierecksanlage, was f&uuml;r das 13. und 14.
        Jahrhundert bezeichnend ist.<br>
        Die Ortschaft Ruhmannsfelden hat sich von unten nach oben, von West nach
        Ost, also von der urspr&uuml;nglichen Siedlung (der heutigen Bachgasse)
        aus zum Laurentius-Kirchlein am B&uuml;hl hin entwickelt. Von einem
        Schloss auf der Anh&ouml;he steht weder in den Urkunden etwas, noch sind
        auch nur die sp&auml;rlichsten &Uuml;berreste oder Spuren von einem
        solchen Schloss oder einer Ansiedlung um ein solches herum auf der
        Anh&ouml;he zu finden. Wohl sprechen die Urkunden von einer Villa und
        von einer Burg Ruhmannsfelden. Darunter ist aber wohl nur die
        anf&auml;ngliche Siedlung mit oder ohne Wehrturm und den dazu
        geh&ouml;rigen Grundst&uuml;cken, H&ouml;fen und Rechten zu verstehen.</p>
      <h4>b.) Die Gr&uuml;ndung des Klosters Gotteszell 1295<a name="i3b"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h4>
      <p>Wo heute die Ortschaft Gotteszell steht, war fr&uuml;her nur ein
        einziger gro&szlig;er Hof, der einem ungarischen Grafen, namens
        &quot;Drozza&quot; geh&ouml;rte und &quot;Drozzalach&quot;, kurz
        &quot;Dro&szlig;lach&quot; hei&szlig;. Dieser Hof kam in den Besitz des
        Grafen von Pfelling bei Bogen, dem auch fr&uuml;her die Siedlung
        Rumarsfelden geh&ouml;rte. Dieser Graf schenkte dieses
        &quot;Dro&szlig;lach&quot; im Einvernehmen mit seiner Gemahlin
        Mechthildis, Kinder hatten sie nicht, dem Kloster Aldersbach
        (Zisterzienserklosters) mit der Absicht, aus diesem Hof ein sch&ouml;nes
        Kloster zu machen, was auch geschah. Anf&auml;nglich waren nur zwei
        Klosterherren in Gotteszell. Bischof Heinrich II. von Regensburg, der
        mit dem Grafen von Pfelling verwandt war (Mechthildis war eine Schwester
        zu diesem Bischof), genehmigte die neue Klosterniederlassung in
        Gotteszell, machte sie selbstst&auml;ndig, indem der sie von
        Pfarrverbande Geiersthal l&ouml;ste und gab ihr den sch&ouml;nen Namen
        &quot;Cella Dei&quot; = „Zelle Gottes“ oder Gotteszell. Das war in
        der gleichen Zeit, als das Zisterzienserkloster Aldersbach die Siedlung
        Rumarsfelden von den damaligen Landesherren k&auml;uflich erwarb (1295).</p>
      <h4>c.) Die Pest in Ruhmannsfelden (1354 - 1357 nach H. Geheimrat
        Eberl).<a name="i3c"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h4>
      <p>&quot;Unter dem Krumstab des Klosters Aldersbach war gut zu
        leben&quot;, berichten die Urkunden aus damaliger Zeit. Die Siedlung
        Ruhmannsfelden lag am Kreuzungspunkt der Stra&szlig;e von Cham,
        Viechtach und Deggendorf, also von Nord nach S&uuml;d und der
        Stra&szlig;e von Regen, Fratersdorf, Kalteck, Bogen und Straubing, also
        von Ost nach West. Da es damals Eisenbahnen noch nicht gab, war auf
        diesen Stra&szlig;en ein reger Verkehr von Fu&szlig;g&auml;ngern und
        Fuhrwerken, was sich auf das Gesch&auml;ftsleben und die Entwicklung von
        Ruhmannsfelden nur g&uuml;nstig auswirken konnte. Die bevorzugten Waren
        der t&uuml;chtigen Handwerker von dort wurden gerne gekauft und der
        gro&szlig;e Durchgangsverkehr brachte den Gewerbetreibenden ein
        bl&uuml;hendes Gesch&auml;ft. Ruhmannsfelden wurde weit &uuml;ber den
        bayerischen Wald hinaus bekannt. Unter der Herrschaft der Klosters
        Aldersbach brach f&uuml;r die Ruhmannsfeldner wirklich eine
        gl&uuml;ckliche, hoffnungsvolle Zeit an. Aber! Mit des Schicksals
        M&auml;chten ist kein ew`ger Bund zu flechten. Nur ein halbes
        Jahrhundert dauerte diese Zeit ungest&ouml;rten Schaffens und belohnten
        Flei&szlig;es.<br>
        Pl&ouml;tzlich kam f&uuml;r Ruhmannsfelden in der Mitte des 14.
        Jahrhunderts etwas ganz Schreckliches. Die indische Beulenpost, die von
        Italien hierher eingeschleppt wurde, suchte auch in Ruhmannsfelden und
        Umgebung ihre Opfer und kein Haus und keine Familie blieben davon
        verschont. Ganze Familien wurden von dieser Seuche hinweggerafft. Den
        Pestkranken reichte man die Nahrung mit Hilfe von langen Stangen an ihre
        Fenster, um ja nicht mit ihnen in Ber&uuml;hrung zu kommen. Jede
        M&ouml;glichkeit einer Ansteckung wurde vermieden. Der Friedhof wurde
        zur Aufnahme der Toten zu klein. Es mussten zwei neue
        Begr&auml;bnispl&auml;tze mit Massengr&auml;bern errichtet werden,
        n&auml;mlich &quot;das Grab&quot; auf der Anh&ouml;he und &quot;das
        Sichet&quot; von der Ziegelei bis zum Mittelzellner Holz. Nur ganz
        wenige Einwohner blieben von dieser furchtbaren Krankheit verschont.<br>
        In dieser Zeit kamen von ausw&auml;rts b&ouml;se Menschen, welche die
        Not und das Ungl&uuml;ck, das die Pest &uuml;ber die Einwohner von
        Ruhmannsfelden gebracht hatte, auszun&uuml;tzen suchten. Sie machten den
        noch &Uuml;briggebliebenen allen m&ouml;glichen Schwindel vor, raubten
        und pl&uuml;nderten in den menschenleeren Wohnungen, wobei es auch zu
        Streit und Raufereien kam. Dabei wurde auch das sch&ouml;ne
        Laurentius-Kirchlein am B&uuml;hl ein Raub der Flammen.</p>
      <h4>d.) Ruhmannsfelden wird um das Jahr 1400 herum &quot;Markt&quot;.<a
  name="i3d"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h4>
      <p>Aber trotz Post und Krieg entwickelte sich in einem Zeitraum von 100
        Jahren wieder ein reger Handel und Verkehr. Auch das Handwerk kam wieder
        in gro&szlig;en Aufschwung. Handwerker, die bis dahin in Ruhmannsfelden
        nicht vertreten waren (Nagelschmied, Seiler, Konditor, Chirurgen und
        Binder), bauten sich ihre kleineren H&auml;user anschlie&szlig;end an
        den unteren Markt in die unmittelbare N&auml;he der Kirche, woraus dann
        der obere Markt entstand. Die T&uuml;chtigkeit der Ruhmannsfeldner
        Handwerker und der Gewerbeflei&szlig; seiner B&uuml;rger brachten es
        schlie&szlig;lich so weit, dass Ruhmannsfelden zum Markt erhoben wurde.<br>
        Wann die Markttitelverleihung erfolgte und von wem sie vollzogen wurden,
        ist urkundlich nicht nachzuweisen, wie eben an anderen Pl&auml;tzen
        auch. Jakob der Rueerer stellte am 26. April 1416 eine Urkunde aus in
        welcher er sich <i>„Dn czeitt Richter des Markehtz zue
        Ruedmansfelden“</i> nennt. Es d&uuml;rfte kaum ein Zweifel bestehen,
        dass der Urkundenaussteller als landesherrlicher Richter &uuml;ber die
        Markteigenschaft seines Wirkungsortes Bescheid wusste. Es ist deshalb
        berechtigt, die Markteigenschaft zu Ruhmannsfelden schon seit dem
        Beginne des 15. Jahrhunderts in Anspruch zu nehmen. Am 15. Februar 1431
        bekannte der Jakob der Degenberger von Altnu&szlig;berg <i>&quot;vielleicht
        derselbe, der 1418 bis 1431 Bachvogt von Ruhmannsfelden war&quot;</i> dass ihn das Gotteshaus zu Aldersbach zum Vogte &uuml;ber seine armen
        Leute im <i>&quot;Markte&quot;</i> zu Ruhmannsfelden und den G&uuml;tern
        in Viechtreich eingesetzt habe. In einem Aldersbacher Codex vom Jahres
        1452 ist die Rede von dem <i>„Forum Rudmannsfelden“</i>, also Markt,
        w&auml;hrend in einer Urkunde vom 2. April 1475 des „Opidum
        Rudmannsfelden“ erscheint, was mehr an die befestigte Siedlung als an
        den Markt gemahnt. Auf Bitten der <i>&quot;Burger unnsers Margkts zu
        Rudmannsfellden&quot;</i> tut ihnen Herzog Albrecht IV. von
        Bayern-M&uuml;nchen die Gnade: <i>&quot;Freyen sie auch wissenlich in
        crafft des Briefs, Also das sy vund all Ir nachkommen, sich aller der
        gnaden vnd freihait</i> geprauchen vnd nyessen m&uuml;gen vnd die haben
        sollen, In allerma&szlig; als vnnder vnser M&auml;rkt, in Nider Baiern
        von vnnsern vordern gefreyt sein.&quot;<br>
        Dieses Privileg ist nur in Abschrift erhalten und undatiert, steht aber
        zwischen zwei Urkunden desselben Jahres 1469 und darf daher als aus
        diesem Jahre stammend angenommen werden. In einem vom Jahres 1803
        hei&szlig;t es: <i>&quot;Es erhelle aus den &auml;lteren Umst&auml;nden
        Literale des Klosters Gotteszell vom Jahre 1566 bis 1602 kommen vor die
        &quot;Geschworenen des Rats und ganze B&uuml;rgerschaft des
        &quot;Markts&quot; Ruhmannsfelden, wie &uuml;berhaupt seit der
        Begnadigung von 1469 keinerlei Zweifel an dem Markrecht Ruhmannsfelden
        mehr aufkommen kann. In der Rechnung des Marktes Ruemannsfelden sehr
        deutlich, da&szlig; der Markt Ruemannsfelden eine vollst&auml;ndige
        Marktgerechtigkeit mit der angemessen Jurisdiktion besessen habe. Es war
        derselbe schon in der Jahren 1500 mit einer f&ouml;rmlichen und
        gegenw&auml;rtig noch vorhandenen, die Wahrheit des Altertums
        bezeigenden Raths Glocke ersehen.&quot;</i><br>
        Ruhmannsfelden war zurzeit der Markttitelverleihung mit Toren versehen
        und die Tapferkeit seiner B&uuml;rger war r&uuml;hmlichst bekannt. Das
        waren ja die zwei Voraussetzungen f&uuml;r die Marktitelverleihung
        einerseits und f&uuml;r die wei&szlig;blauen Rauten im Wappen
        andererseits. Ruhmannsfelden war wieder auf seiner fr&uuml;heren
        wirtschaftlichen H&ouml;he angelangt, auf die es unter der Herrschaft
        der Aldersbacher Klosters gekommen ist.<br>
        Besonders zeigte sich dieser Aufschwung bei den M&auml;rkten. So
        str&ouml;mten z. B. zum Laurentius-Markte von weit und breit die Leute
        herbei und kaum konnte der Marktplatz und der Kirchenplatz die
        zahlreichen Leute, St&auml;nde und Buden fassen. Dass auch damals schon
        bei solchen Gelegenheiten Ordnung und Disziplin herrschten, ersehen wir
        aus einer Verordnung f&uuml;r das Feilbieten von Waren. Die
        diesbez&uuml;gliche Marktst&auml;ndeordnung vom Freitag nach den
        Pfingstfeiertagen unseres lieben Herrn Christo anno 1503 lautet:<i> &quot;- - es soll gehalten werden, wie hernach folgt: Schmalz,
        K&auml;se, Garn, Inschlit, Schmier, Wildpret, das Gehstuhl und das
        Rauchleder soll man vor dem Schenkhaus, zwischen dem Kasten und des
