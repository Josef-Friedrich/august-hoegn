        Die Leute des Marktes waren zum gr&ouml;&szlig;ten Teil auf den Feldern.
        Ob nun Kinder, die mit Z&uuml;ndh&ouml;lzern ihr Spiel trieben, schuld
        waren oder ob eine &auml;ltere Frauensperson absichtlich angez&uuml;ndet
        hat, dar&uuml;ber lie&szlig; sich bis heute nichts Genaues feststellen.
