      </ul>
      <h3>1860<a name="1860"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Die Unterbringung der Feuerl&ouml;schspritzen betreffend: <i>„Die
          zwei Gemeinde L&ouml;schspritzen befanden sich seit einiger Zeit in
          der Wagenschupfe des Lukas´schen Br&auml;uanwesens. Da der jetzige
          Anwesensbesitzer Peter Schr&ouml;tter die Unterkunft in eine andere
          Stelle w&uuml;nscht, da ihm selbst wegen vielen Wagengeschirr der
          Platz mangelt und man die &uuml;berzeugung machte, da&szlig; sich
          derartige Unterkunft ohnedies nicht gut eignet, da diese oft mit
          Rauzen dergestalt verrammelt waren, da&szlig; man selbe oft nur mit
          zuviel Zeitverlust hervorholen konnte, hat man den &Ouml;konom Johann
          Pfeffer, der einen ger&auml;umigen Platz in seinem Stadel durch einen
          Anbau gewonnen, contrahiert, denselben die Unterbringung der Spritzen
          gegen eine j&auml;hrl. Verg&uuml;tung von 2 Gulden verst&auml;ndigt,
          aus Gemeindemitteln zu berichten.“</i> (Sp&auml;ter dann zu
          G&ouml;stl, bis das Feuerhaus gebaut wurde.)</li>
      </ul>
      <h3>1863<a name="1863"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>am 4. Oktober wurde eine Feuerl&ouml;schordnung produziert, die in
          ihren 10 enthaltenen Artikeln als durchwegs gut und ohne alle
          Ab&auml;nderung anzunehmen sei.</li>
        <li>bekam der Markt 14 St&uuml;ck Feuereimer von den Magirus-Werken in
          Ulm unter Nachnahme und durch Vermittlung des Herrn Kaufmann
          Schwaighofer in Deggendorf laut Schein 17 fl. 55 kr.</li>
      </ul>
      <h3>1866/67<a name="186667"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Ein Beschluss der Gesamtgemeinde Ruhmannsfelden vom 14.7.1867
          lautet: <i>„Es wurde aus Communalmitteln 12 St&uuml;ck zwilchene
          Wassereimer angeschafft. Jedes neu aufgenommene Gemeindemitglied (auch
          bei Insassen) hat einen desgleichen Eimer anzukaufen und an den
          Spritzenmeister abzuliefern.“</i></li>
        <li>Laut Rechnung von diesem Jahr wurde verausgabt an den
          Wagnermeister H&ouml;lzl f&uuml;r neue Deichsel zur Feuerspritze 2.
          fl. 26 kr., dem Joseph Baumann, Schmied, f&uuml;r geleistete Arbeit
          zur Feuerspritze 12 fl. 6 kr., dem Anton Prieglmayer von Viechtach
          f&uuml;r Reparaturarbeiten an der Feuerspritze 7 fl. 24 kr.</li>
      </ul>
      <h2><b>III. </b> Die Gr&uuml;ndung der freiwilligen Feuerwehr
        Ruhmannsfelden<a name="iii" id="iii"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h2>
      <p>Nachdem bereits im Bezirke Viechtach um jene Zeit eine freiwillige
        Feuerwehr bestand und zwar die freiwillige Feuerwehr Viechtach
        (gegr&uuml;ndet 15.8.1863) und in Ruhmannsfelden lebhaft von der
        Gr&uuml;ndung einer Freiwilligen Feuerwehr debattiert wurde, ging am 13.
        August 1867 im ganzen Markt von Haus zu Haus folgendes Schreiben herum:</p>
      <p><i>„Cirkular.<br>
        </i><i>Wir haben beschlossen im Markte Ruhmannsfelden eine Feuerwehr zu
