          Die freiwillige Feuerwehr Ruhmannsfelden &uuml;bernahm in der Nacht
          vom 22. auf 23.1. von 8 Uhr abends bis 6 Uhr morgens mit 5 Mann und 1
          F&uuml;hrer die Wache, und ebenso vom 23. auf 24. Januar von 7 Uhr
          abends bis 7 Uhr morgens. Am 24.1 traf milit&auml;rische Bewachung
          ein.</li>
        <li>Die Besichtigung der HJ-Feuerwehrschar Ruhmannsfelden erfolgte am
          19.10.1941, vormittags 9 Uhr im Schulhof in Ruhmannsfelden. Zur
          Besichtigung erschienen: Der Kreisfeuerwehrf&uuml;hrer, Hr.
          Kramh&ouml;ller, der Stammf&uuml;hrer der HJ, Hr. Meierh&ouml;fer, der
          B&uuml;rgermeister, der Ortsgruppenleiter Hr. Karl, die Gendarmerie,
          sowie der F&uuml;hrer der hiesigen Feuerwehr Hr. W&uuml;hr und dessen
          Stellvertreter Hr. Linsmeier. Nach einer kurzen Begr&uuml;&szlig;ung
          durch den Kreisfeuerwehrf&uuml;hrer begann die Besichtigung der
          HJ-Feuerwehrschar durch einige exakte Fu&szlig;&uuml;bungen unter der
          F&uuml;hrung des HJ-Feuerwehrscharf&uuml;hrers Linsmeiers.
          Anschlie&szlig;end ging die HJ-Feuerwehrschar zur &uuml;bung
          &uuml;ber. Alle &uuml;bungen wurden sehr gut durchgef&uuml;hrt. Durch
          die Abschlussmeldung des Gruppenf&uuml;hrers wurde die &uuml;bung
          beendet. Der Kreisfeuerwehrf&uuml;hrer Hr. Kramheller schloss die
          Besichtigung des HJ-Feuerwehrschar Ruhmannsfelden mit einer kurzen
          Ansprache und wies auf die Wichtigkeit der HJ-Feuerwehrscharen hin.
          Der Stammf&uuml;hrer des Stammes II Viechtach Hr. Meierh&ouml;fer
          &uuml;berreichte dann den HJ-Feuerwehrjungen das HJ-Feuerwehr
          Ehrenzeichen. Er sprach im Namen des Landrates Viechtach der
          HJ-Feuerwehrschar Ruhmannsfelden die vollste Anerkennung aus.</li>
      </ul>
