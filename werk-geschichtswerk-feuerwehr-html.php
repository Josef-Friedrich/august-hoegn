      <h3>1874<a name="1874"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>In einem Gemeindebeschluss vom 28.1. betreffend Anschaffung einer
          Feuerspritze im Verein der Marktsgemeinde Ruhmannsfelden und der
          Gemeinde Zachenberg hei&szlig;t es, dass eine neue Feuerspritze auf
          gemeinschaftlich zu bezahlende Weise anzuschaffen sei und wird die
          Beschaffung derselben dem B&uuml;rgermeister Lukas von Ruhmannsfelden
          beauftragt und bevollm&auml;chtigt. Es sei schon <i>„eine solche
          Spritze anzuschaffen, da&szlig; dieselbe auf die D&ouml;rfer hinaus
          leicht zu transportieren ist und in Erw&auml;gung, da&szlig; die
          Gemeinde Zachenberg in ihrer Lage sich zun&auml;chst neben
          Ruhmannsfelden hinzieht und von da aus dieselbe am leichtest
          fortgeschafft werden kann, auch auf die entferntesten Punkte auch
          bez&uuml;glich der Zugpferde sichere und schleunigere Hilfe
          allenthalben &uuml;berall hin geboten ist, so hat diese neue
          anzuschaffende Feuerspritze im Markte Ruhmannsfelden seine
          Aufbewahrungsstelle, wird auch die Aufsicht dem B&uuml;rgermeister von
          Ruhmannsfelden &uuml;bertragen.“</i> Der anwesende gegenw&auml;rtige
          B&uuml;rgermeister der Gemeinde Patersdorf lehnte den Beitritt zur
          gemeinschaftlichen Anschaffung obigen Spritze im Namen seiner Gemeinde
          ab. (Siehe Gemeindebeschluss vom 9.11.05 und vom 6.10.06)</li>
        <li>Am 4. Juni bei der Fronleichnamsprozession brannte der Bruckhof
          ab. Im Gemeindebeschluss vom 5.6.1874 hei&szlig;t es: <i>„Nachdem
          sich beim Brande im Hause des Joseph Bauer in Bruckhof gezeigt hat,
          da&szlig; die vorhandenen Feuereimer sich als sehr praktisch und viel
          zu wenig erwiesen haben, so wird beschlossen, da&szlig; deren mehrere
          Feuereimer anzuschaffen sind und zur leichteren Bezweckung der
          Geldmittel hiezu hat ein jeder sich zu Verehelichende, der
          Marktgemeinde Ruhmannsfelden je f&uuml;r einen das Geld im Betrage von
          1 fl. 30 kr. pro St&uuml;ck bei der Gemeinde zu erlegen und ist in der
          Taxnote der Heimat- und B&uuml;rgeraufnahmegeb&uuml;hr zu bezeichnen
          und zu berechnen.“</i></li>
        <li>Am 27.Mai erging eine Aufruf lauf Zirkular an alle Mitglieder der
          freiwilligen Feuerwehr Ruhmannsfelden, da sich bei der letzten
          Versammlung wenige Mitglieder bet&auml;tigt haben, unausbleiblich am
          Sonntag den 30. Mai Mittags 12 Uhr im Lokal (Post) mit s&auml;mtlichen
          Ausr&uuml;stungsgegenst&auml;nden zu erscheinen.</li>
        <li>Der Verwaltungsrat des freiwilligen Feuerwehrkorps Vilshofen dankt
          in einem Schreiben vom 20. August 1874 f&uuml;r die freundliche
          Einladung zur Fahnenweihe der freiwilligen Feuerwehr Ruhmannsfelden
          mit dem Beif&uuml;gen, dass <i>„der Einladung keine Folge geleistet
          werden kann, da zu gleicher Zeit in Vilshofen das Landwirtschaftsfest
          abgehalten wird, zu welchen das Festkomite unsere T&auml;tigkeit in
          Anspruch nimmt. Der Commandant des freiwilligen Feuerwehrkorps der
          Stadt Vilshofen.“</i></li>
        <li>Am 13. September fand die Fahnenweihe des freiwilligen
          Feuerwehrkorps Ruhmannsfelden statt. Fahnenmutter war die
          Metzgermeistersgattin Holler (+ 18.7.1931), die auch beim
          40-j&auml;hrigen Gr&uuml;ndungsfest der freiwilligen Feuerwehr
          Ruhmannsfelden im Jahre 1907 der Einladung folgend sich mit ihrer
          Gegenwart und Beteiligung zu unserer aller Freude beteiligte. Nach den
          &uuml;blichen Festesfeiern am Vor- und Haupttage war an diesem
          Nachmittag 2 Uhr Signal zur Zugaufstellung, denn Auszug in die
          Romantische Schloss Leuthen, wo selbst gesellige Unterhaltung mit
          Musik, Feuerwerk etc. stattfindet. Abends war R&uuml;ckzug ins
          Feuerwehrlokal und Festball.</li>
      </ul>
      <h3>1875<a name="1875"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>wurde eine Gemeindeumlage in H&ouml;he von 450 fl. erhoben mit der
          Bemerkung: <i>&quot;Ist eine Gemeindeumlage in diesem Ma&szlig;stabe
          bez&uuml;glich Anschaffung einer neuen Feuerl&ouml;schmaschine
          nothwendig und zwar nach bisheriger Festsetzung durch
          Gemeindebeschlu&szlig; vom Grundsteuergulden 24 kr. vom
          Gewerbesteuergulden 6 kr.“</i></li>
        <li>ergeht vom Vororte der niederbayerischen Feuerwehren Passau an das
          Feuerwehrkorps Ruhmannsfelden die Anfrage, ob der niederbayerische
          Feuerwehrtag vor oder nach dem bayerischen Feuerwehrtag stattfinden
          solle, was dahin entschieden wurde, dass der niederbayerische
          Feuerwehrtag in Passau am 29. August, also vor dem bayerischen
          Feuerwehrtag in Kempten abgehalten werden solle.</li>
      </ul>
      <h3>1876<a name="1876"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Der Bezirksvertreter Hr. Hauptmann Schmid von Viechtach beruft
          Feuerwehren im Bezirk zu einer Versammlung nach Viechtach behufs
          Gr&uuml;ndung eines Bezirksverbandes der Feuerwehren des Bezirkes
          Viechtach. Diesem schlossen sich an: Viechtach, Ruhmannsfelden
          (vertreten durch Bielmeier und Mayer), Arnbruck, Wiesing, Kollnburg,
          Sch&ouml;nau, Kirchaitnach, Blossersberg, Allersdorf, Wiesing,
          Prackenbach. Bezirksvertreter wurde Hr. Hauptmann Schmid von
          Viechtach, Ersatzvertreter wurde Hr. Anton Kasparbauer von Viechtach.</li>
        <li>Die Anschaffung der neuen Feuerspritze f&uuml;r Ruhmannsfelden und
          Zachenberg gemeinschaftlich zieht sich sehr in die L&auml;nge, denn
          erst ein Gemeindebeschluss vom 14.9.76, also nach mehr als 2 ½
          Jahren, befasst sich wieder mit der Anschaffung dieser Feuerspritze
          und lautet: <i>„In Anbetracht des kgl. bay. Amtes Viechtach, Auftrag
          vom 9.9.74 wurde Beschlu&szlig; gefasst bez&uuml;glich den zu
          handhabenden zwei Projekten zur Anschaffung einer
          Feuerl&ouml;schmaschine f&uuml;r die Gemeinden Ruhmannsfelden und
          Zachenberg entschlie&szlig;t sich der Ausschlu&szlig; dahin, f&uuml;r
          die Maschine des Braun in N&uuml;rnberg im Betrage von 730 fl,. jedoch
          aber mit der Bedingung, da&szlig; auf zwei Jahre Garantie geleistet
          werde, dann in Hinsicht der Sicherheit und Ausdauer derselben in
          Ben&uuml;tzung in den Hohlwegen und schlechter Wegen, voll
          gro&szlig;er Steine, der Maschine wie auch der Wagen an derselben
          aushalten und nicht brechen und brauchbar sind beim etwaigen
          Gebrauche, ferners da&szlig; dieselbe, da mit demselben im Markte
          besonders die B&auml;che in ihrem Wassergehalt wegen der Entfernung u.
          der H&ouml;he der Steigung nicht gen&uuml;tzt werden k&ouml;nnen, das
          Wasser aus den im Markte reichlich mit Wasser versehenen Brunnen in
          einer Tiefe von 30 Fu&szlig; = 9 Meter herausgehoben werden k&ouml;nne
          mit dieser Maschine, da eine solche Maschine, die das nicht im Stande
          ist, hierorts f&uuml;r nichts ist, da wir ohnehin eine gute Spritze
          ohne Sauger haben.“</i></li>
        <li>wurde an Johann Nepomuk Baumann f&uuml;r Reparatur einer Spritze
          verausgabt 1 fl. 45 kr.</li>
        <li>an das k&ouml;nigliche Bezirksamt Viechtach den treffenden
          Lastenanteil f&uuml;r Anschaffung einer L&ouml;schmaschine 292 fl.</li>
        <li>an Johann Sagstetter f&uuml;r Beif&uuml;hrung der
          L&ouml;schmaschine von Viechtach nach Ruhmannsfelden 7 fl. 30 kr.</li>
      </ul>
      <h3>1877<a name="1877"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Die neue Feuerspritze sollte nun auch einen geeigneten
          Unterkunftsplatz bekommen, Zu diesem Zwecke sollte ein eigenes
          Spritzenhaus gebaut werden. Am 11.1.1877 wurde der Gemeindebeschluss
          gefasst betreffend <i>„Erbauung eines Feuerrequisitenhauses“</i>,
          dass <i>„mit Eintritt der g&uuml;nstigen Jahreszeit mit dem Bau des
          Requisitenhauses begonnen werden mu&szlig; und da&szlig; die Kosten
          teils durch Umlagen, teils durch den bis dahin anfallenden
          Bierpfenning gedeckt werden soll“.</i> Au&szlig;erdem wurde am
          23.2.1877 beschossen, dass <i>„zur Erbauung des neuen Feuerhauses
          von s&auml;mtlichen Fehrwerksbesitzern die ben&ouml;tigten Bausteine
          unentgeltlich beigefahren werden sollen.“ </i>Am 24.4.1877 wurde die
          Maurer- und Zimmermannsarbeiten zu dem neuen Feuerhause verakkordiert.
          Den Zuschlag f&uuml;r die Mauererarbeiten erhielt Johann Pl&ouml;tz,
          Maurermeister mit 390.- M und f&uuml;r die Zimmermannsarbeiten Hr.
          Anton Bielmeier, Schreinermeister mit 325.- M.</li>
        <li>Am 19.3. war die Beerdigung des verstorbenen
          Gr&uuml;ndungsmitgliedes und Kassiers der freiwilligen Feuerwehr
          Ruhmannsfelden Hr. Joseph Moosm&uuml;ller.</li>
        <li>Am Dienstag, den 22. Mai, feierte die freiwillige Feuerwehr
          Ruhmannsfelden ihr zehnj&auml;hriges Gr&uuml;ndungsfest. Tags vorher
          war schon Empfang ausw&auml;rtiger Vereine und Zapfenstreich. Am
          Festtage selbst war Kirchenzug, Feldmesse und nachmittags Ausmarsch
          s&auml;mtlicher Vereine zur Sch&auml;ffer Markendentenh&uuml;tte beim
          Zachenberger Eisenbahneinschnitt, dann R&uuml;ckmarsch zu Hr.
          Gastgeber M&uuml;nch, Gartenmusik und Festball.</li>
      </ul>
