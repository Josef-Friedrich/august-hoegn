      </ul>
      <h3>1856/57<a name="185657"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Laut Gemeinderechnung wurde f&uuml;r Reparatur an Feuerhaken an
