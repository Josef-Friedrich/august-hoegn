      <h2><b>V. </b> Ereignisse seit Gr&uuml;ndung der freiwilligen Feuerwehr
        Ruhmannsfelden<a name="v" id="v"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h2>
      <h3>1867<a name="1867"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Die freiwillige Feuerwehr Ruhmannsfelden will eine Steigleiter
          nach dem Muster der Viechtacher Steigleiter.</li>
        <li>erbaute die freiwillige Feuerwehr Ruhmannsfelden ein Steighaus
          (cirka 15 m hoch, mit 3 Etagen, Einsteigt&uuml;ren, Steigbaum).</li>
      </ul>
      <h3>1868<a name="1868"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Im Juli war hier Feuerwehrprobe, bei der die zwei
          Feuerwehrm&auml;nner Blessing und Baumer von Viechtach anwesend waren.</li>
        <li>Im August feierte die freiwillige Feuerwehr Ruhmannsfelden
          Gr&uuml;ndungsfest.</li>
        <li>erhielt laut Gemeinderechnung Schlosser Holtmeier f&uuml;r
          Reparatur an der Feuerspritze laut Schein 3 fl. 27 kr.</li>
        <li>f&uuml;r Reparatur an der kleinen Feuerspritze Karl Prieglmeier
          laut Schein 17 fl. 24 kr.</li>
        <li>ausgezahlt dem Wagnermeister H&ouml;lzl und Schmiedmeister Rauch
          Arbeitslohn an der Feuerspritze laut Schein 22 fl. 3 kr.</li>
        <li>dem Leopold Baumann Maler f&uuml;r Reparaturarbeit an der
          Feuerspritze laut Schein 42 kr.</li>
        <li>dem Sebastian Rauch, Schmiedmeister f&uuml;r gefertigte Arbeit an
          der Feuerspritze laut Schein 42 kr.</li>
        <li>dem Anton Schmid f&uuml;r &uuml;berbringung der Feuerspritze von
          Viechtach nach Ruhmannsfelden laut Schein 1 fl. 36 kr.</li>
        <li>an Magirus in Ulm f&uuml;r gelieferte Feuereimer laut Schein 25
          fl. 39 kr.</li>
        <li>erfolgte eine Einladung der freiwilligen Feuerwehr Viechtach an
          die freiwillige Feuerwehr Ruhmannsfelden zwecks Pr&uuml;fung des neuen
          Requisitenwagens anl&auml;sslich einer Fahrt von Viechtach nach
          Ruhmannsfelden.</li>
      </ul>
      <h3>1869<a name="1869"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Am 20. Juni erhielt die freiwillige Feuerwehr in Ruhmannsfelden
          eine Einladung zur Fahnenweihe der freiwilligen Feuerwehr Cham.</li>
        <li>Im Dezember selbigen Jahres traf von Deggendorf ein Brief
          folgendes Inhalts ein:<br>
          <i>„Sehr verehrtester Herr Hauptmann! Anliegend &uuml;bersende ich
          Ihnen das allen Feuerwehren so beliebte Feuerwehrlied nebst Melodram,
          das wirket so sehr auf die Mitglieder und spornt den Geist, das
          Gem&uuml;th jedes Feuerwehrmannes an, es ist recht h&uuml;bsch und
          leicht zum auff&uuml;hren. Einer spricht das Melodram, dann vier
          M&auml;nnerstimmen singen die Lieder und 1 oder 2 Signalisten blasen
          die angegebenen bei Ihnen &uuml;blichen Signale. 1 schl&auml;gt auf
          der Glocke 12 Uhr und trommelt – und das ist das ganze Personal. Ich
          lasse Ihnen Melodram und Singstimmen um den billigen Preis von lf. 24
          kr. ab und K&ouml;nnen Sie selben gerade dem Postillon mitgeben, haben
          Sie noch keine Signale und Ordonanzm&auml;rsche f&uuml;r 2
          Signalisten, so schicke ich selbe um ein ganz geringes Honorar. Ich
          hoffe, da&szlig; ich eine Ehre aufhebe Ihnen wie bei dem Corps und
          gr&uuml;&szlig;e Sie Hochachtungsvollst! Ihr ergebener Eduard Grill
          Musiker“.</i></li>
      </ul>
      <h3>1870<a name="1870"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>ergeht eine Einladung an die freiwillige Feuerwehr Ruhmannsfelden
          zur Teilnahme an der 2. Landesversammlung der bayerischen Feuerwehren
          in Regensburg am 29. bis 31. Mai 1870, an welcher sich laut
          Unterschrift auf der Einladung Jakob Bielmeier beteiligte.</li>
        <li>erging vom Feuerwehrkorps Deggendorf an das Feuerwehrkorps in
          Ruhmannsfelden eine Anfrage, ab noch in diesem Jahre ein
          niederbayerischer Feuerwehrtag abgehalten oder bis zum Friedensschluss
          gewartet werden sollte und ob das freiwillige Feuerwehrkorps zum
          bayerischen Kreisverband beitreten wolle.</li>
        <li>fand die Fahnenweihe der freiwilligen Feuerwehr Viechtach statt.
          Die Lokalbahn existierte damals noch nicht. Der Weg von Ruhmannsfelden
          nach Viechtach musste zu Fu&szlig; zur&uuml;ckgelegt werden. F&uuml;r
          die Teilnehmer an dieser Fahnenweihe fand am Sonntag, den 3. Juli ein
          Reisemarsch statt von Ruhmannsfelden nach Gotteszell auf den Keller
          bei Hr. Kilger Br&auml;u.</li>
      </ul>
      <h3>1871<a name="1871"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>erh&auml;lt das Feuerwehrkommando Ruhmannsfelden aus der
          Gemeindekasse f&uuml;r Anschaffung von 10 Feuereimern 1 fl.</li>
        <li>Laut Zirkular von 8.3.1871 wurden s&auml;mtliche Mitglieder der
          hiesigen Feuerwehr aufgefordert zu einer Abend 7 Uhr im Gastlokale des
          B&uuml;rgermeisters Lukas anberaumten Feuerwehrversammlung in Uniform
          respektive M&uuml;tze zu erscheinen zu einer Besprechung
          bez&uuml;glich der Friedensfeier.</li>
        <li>lief beim k&ouml;niglichen Bezirksamt Viechtach eine Klage ein
          bez&uuml;glich der Feuereimer. Infolge dessen erging von der
          Marktverwaltung Ruhmannsfelden an das Feuerwehrkorps Ruhmannsfelden
          folgendes Schreiben:<br>
          <i>„Nach Auftrag des kgl. Bezirksamtes Viechtach wird in rubr.
          Betreff das Feuerwehrkorps Ruhmannsfelden beauftragt am 17. Juni
          Samstag laufendes Jahres s&auml;mtliche Feuereimer vorzuweisen und zu
          diesem Behufe dieselben in die Behausung des B&uuml;rgermeisters
          bringen zu lassen. Das Weitere wird sich schon herausstellen.
          Achtungsvoll Lukas Bgst.“</i></li>
        <li>Am 27. August 1871 fand die Fahnenweihe des freiwilligen
          Feuerwehrkorps in Zwiesel statt, an der sich 16 Feuerwehrm&auml;nner
          von Ruhmannsfelden beteiligten.</li>
      </ul>
