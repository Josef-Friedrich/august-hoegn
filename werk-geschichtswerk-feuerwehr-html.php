      </ul>
      <h3>1850/51<a name="185051"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>erhielt Schmied Wolfgang Stegmeier f&uuml;r 2 Steften und
