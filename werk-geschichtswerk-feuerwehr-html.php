        <li>Am 12.12.1948 fand eine Generalversammlung der freiwilligen
          Feuerwehr Ruhmannsfelden statt.</li>
      </ul>
      <h3>1949<a name="1949"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Am 8.1.1949 war der herk&ouml;mmliche Feuerwehrball mit Fackelzug
          und am 23.1. eine gro&szlig;e Tanzunterhaltung im neuen Saal der
          Brauerei Vornehm.</li>
        <li>Am 3.2.1949 brach bei Edenhofer Hochstra&szlig;e ein
          Werkst&auml;ttenbrand aus, der aber rasch gel&ouml;scht werden konnte.
          Maschinen und Werkzeuge konnten in Sicherheit gebracht werden.</li>
        <li>Am 29.6.1949 fand der Jahrestag mit Kirchenzug, mit Ehrung der
          gefallenen und verstorbenen Feuerwehrkameraden, mit Generalversammlung
          und anschlie&szlig;ender Tanzveranstaltung statt.</li>
        <li>Am 8.7.1949 wurde in der Brauerei Schaffer ein Feuerwehrball
          abgehalten.</li>
        <li>Am 9. Juli 1949 beteiligten sich die jungen aktiven
          Feuerwehrkameraden an einem Maschinistenlehrgang der hiesigen
          Motorspritze.</li>
        <li>Bei der am 10.7.1949 stattgefundenen Fahnenweihe der freiwilligen
          Feuerwehr Pirka beteiligten sich 15 Mann der hiesigen Feuerwehr.</li>
        <li>Am 18.9.1949 war unangemeldeter Generalappell s&auml;mtlicher
          Feuerwehren des Bezirkes Viechtach mittags 1 Uhr in Teisnach.</li>
        <li>Am 2. Oktober 1949 brach bei dem Bauern Wittenzellner in
          Pr&uuml;nst mittags 1 Uhr ein Brand aus, dem der Stadel und das
          Stallgeb&auml;ude zum Opfer fielen. Das Wasser musste von der weit
          entfernten Teisnach hergeleitet werden, weil der nahe gelegene Weiher
          wasserleer war.</li>
      </ul>
      <h3>1950<a name="1950"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Zu Gunsten der Feuerwehrkasse fand am 5.6.1950 ein Heimatabend der
          freiwilligen Feuerwehr Ruhmannsfelden unter Mitwirkung des
          Trachtenvereins in der Turnhalle statt.</li>
        <li>Am 7.1.1950 fand der herk&ouml;mmliche Feuerwehrball mit Fackelzug
          statt.</li>
        <li>Am 12.3.1950 war der Jahrestag mit Kirchenzug, mit Ehrung der
          Gefallenen und verstorbenen Feuerwehrkameraden und mit
          Generalversammlung.</li>
        <li>Am 14.3.1950 wurde beim Kriegsentsch&auml;digungsamt Passau ein
          Entsch&auml;digungsantrag in H&ouml;he von 32 192.- DM f&uuml;r
          kriegsbesch&auml;digtes Feuerhaus und f&uuml;r restlos zerst&ouml;rte
          Feuerwehrger&auml;te und Utensilien eingereicht.</li>
        <li>Am 5.6.1950 brach oberhalb Lindenau im Vornehmwald ein Waldbrand
          aus. Es waren erschienen die Feuerwehren von Achslach, Ruhmannsfelden,
          Gotteszell und Viechtach. Das Feueranmachen auf der
          Waldbl&ouml;&szlig;e bei sengender Hitze war die Brandursache.</li>
        <li>Ebenso war am 13.6.1950 ein Waldbrand in Bergern im Zitzelsberger
          Wald, der aber im Entstehen gel&ouml;scht werden konnte. Die
          freiwillige Feuerwehr Ruhmannsfelden brauchte nicht mehr einzugreifen.</li>
        <li>Am 16. Juli 1950 fand eine au&szlig;erordentliche
          Generalversammlung wegen Neuwahlen statt. Als Vorstand wurde Hr. Josef
          Brem und als 1. Kommandant Hr. Johann Linsmeier gew&auml;hlt.</li>
        <li>Am 13.8.1950 fand anl&auml;sslich des Volksfestes in
          Ruhmannsfelden ein „Feuerwehrtag“ statt. Um ½ 11 Uhr gab die
          Sirene das Signal zur Schau&uuml;bung. Als Brandobjekt wurde zuerst
          das Anwesen des Hr. Ludwig Helmbrecht und dann das Anwesen der Hr.
          Karl Raster angenommen. Hr. Kreisbrandinspektor Kramheller Teisnach,
          wohnte der Schau&uuml;bung bei. Beim Festzug mittags 13 Uhr
          beteiligten sich die Feuerwehren Ruhmannsfelden, Zachenberg,
          Gotteszell und Patersdorf. Ihnen voraus fuhr der Kreisbrandinspektor
          Hr. Kramheller in einer sch&ouml;n geschm&uuml;ckten Equipage. Nach
          dem Testzug versammelten sich die Feuerwehrm&auml;nner im
          Schafferkeller, wo ein Gartenkonzert stattfand.</li>
        <li>Am 1. Oktober fand der herk&ouml;mmliche Jahrtag der freiwilligen
          Feuerwehr Ruhmannsfelden statt. Um ½ 10 Uhr war der Kirchenzug und
          dann Gottesdienst. Nach dem Gottesdienst war Gedenken und Ehrung der
          verstorbenen und gefallenen Feuerwehrkameraden am Kriegerdenkmal.
          Anschlie&szlig;end fand im Saal der Brauerei Schaffer Ehrung von 72
          verdienten Jubilaren der Feuerwehr f&uuml;r 25. 40. und
          50-j&auml;hrige treue Dienstzeit durch Kreisbrandinspektor Kramheller
          Teisnach statt. In seiner Festansprache &uuml;bermittelte er den
          verdienten Veteranen der Wehr den Dank und die Anerkennung des
          Landrates, um sodann auf die Bedeutung des Jahrtages einzugehen. Unter
          anderem wies er darauf hin, dass die erfolgreiche T&auml;tigkeit des
          fr&uuml;heren Kommandanten Hr. Hinkofer nicht vergessen werden
          d&uuml;rfe und ermahnte zum treuen Zusammenstehen. Auch der hiesige
          Kommandant, Hr. Linsmeier Johann sprach den Jubilaren seine
          Gl&uuml;ckw&uuml;nsche aus. Hr. B&uuml;rgermeister Muhr rief die
          jungen M&auml;nner aus den Reihen der Neub&uuml;rger zur Mitarbeit auf
          und stattete ebenfalls dem fr&uuml;heren Kommandanten Hinkofer
          f&uuml;r seine 14-j&auml;hrige T&auml;tigkeit im Dienste der
          Allgemeinheit seinen Dank aus. Im Namen der Jubilare dankte Hr. Rektor
          H&ouml;gn f&uuml;r die zuteil gewordene Ehrung und betonte, dass die
          Alten stets der Feuerwehr die Treue halten werden.</li>
        <li>Am 8.12.1950 wurde ein Kameradschaftsabend der aktiven Mitglieder
          der freiwilligen Feuerwehr Ruhmannsfelden im Herbergslokal
          veranstaltet.</li>
        <li>Die 83. Generalversammlung der freiwilligen Feuerwehr
          Ruhmannsfelden fand am 26.12.1950 im Herbergslokal der Brauerei
          Schaffer statt. Nachdem der bisherige Schriftf&uuml;hrer Rektor August
          H&ouml;gn aus gesundheitlichen Gr&uuml;nden und infolge eines hohen
          Alters den Schriftf&uuml;hrerposten niederlegte, wurde eine Neuwahl
          notwendig. Dabei wurde die Vorstandschaft der freiwilligen Feuerwehr
          erg&auml;nzt mit dem neuen Schriftf&uuml;hrer Hr. Johann Freisinger
          und dem Vorstandsmitglied Hr. Michl Wurzer. Gleichzeitig wurde der
          fr&uuml;here Schriftf&uuml;hrer Rektor August H&ouml;gn von der
          Versammlung in Anerkennung seiner Verdienste f&uuml;r die freiwillige
          Feuerwehr Ruhmannsfelden w&auml;hrend seiner 40-j&auml;hrigen
          Dienstzeit als Schriftf&uuml;hrer der freiwilligen Feuerwehr
          Ruhmannsfelden zum Ehrenschriftf&uuml;hrer ernannt.</li>
      </ul>
      <h3>1951<a name="1951"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Am 8.1.1951 fand der herk&ouml;mmliche Feuerwehrball mit Fackelzug
          statt. Es spielte die Kapelle Heinrich.</li>
        <li>Wegen Aussprache &uuml;ber Ankauf eines LKW zum Transport der
          Motorspritze und der Mannschaft fand am 23.2.1951 eine
          Verwaltungsratssitzung statt. Der Verwaltungsrat beschloss dabei, den
          Ehrenabend f&uuml;r Rektor H&ouml;gn am 11. M&auml;rz zu veranstalten.
          Weitere 11 verdiente Mitglieder wurden zur Ehrung durch das
          Staatsministerium vorgeschlagen. Ebenso wurde der &uuml;bungsplan
          f&uuml;r 1951 aufgestellt.</li>
        <li>Am 11. M&auml;rz fand der durch den Verwaltungsrat beschlossene
          Ehrenabend f&uuml;r Rektor H&ouml;gn statt. Mitten unter seinen
          Feuerwehrkameraden und Marktsb&uuml;rgern feierte das langj&auml;hrige
          Mitglied der freiwilligen Feuerwehr Ruhmannsfelden, deren
          Schriftf&uuml;hrer und Chronist er seit 1910 ist, das 40-j&auml;hrige
          Dienstjubil&auml;um. Kreisbrandinspektor Kramheller war ebenfalls
          anwesend. Die Musikkapelle Heinrich sorgte kostenlos f&uuml;r
          musikalische Umrahmung der Feier. Besonderes Bepr&auml;ge erhielt die
          Feier durch die Anwesenheit der „Alten“, einer Vereinigung der
          alten B&uuml;rger, Handwerker und Pensionisten von Ruhmannsfelden, der
          auch der Jubilar angeh&ouml;rte. Die Feier wurde um 17 Uhr leider
          durch einen Sirenenalarm unterbrochen. In der Gastwirtschaft Wilhelm
          in Auerbach brach um diese Zeit ein Zimmerbrand aus, der aber rasch
          gel&ouml;scht werden konnte.</li>
        <li>Am 1.4.1951 war die erste Feuerwehr&uuml;bung f&uuml;r das Jahr
          1951. Es handelte sich bei dieser &uuml;bung um die Ausprobierung der
          s&auml;mtlichen Oberflurhydranten im Markte. Bei dieser
          &uuml;berpr&uuml;fung der 32 Oberflurhydranten wurden Frostrisse
          festgestellt und bei 11 Hydranten sind die Kopfdichtungen defekt. Ihre
          Ausbesserung kann sofort ausgef&uuml;hrt werden. &uuml;berpr&uuml;fung
          der restlichen 10 Hydranten soll bei der n&auml;chsten &uuml;bung
          erfolgen.</li>
      </ul>
