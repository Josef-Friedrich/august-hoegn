      <h3>1943<a name="1943"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Vom 7. bis 14.2.1943 besuchte Feuerwehrf&uuml;hrer Hr. Josef
          Hinkofer die Feuerwehrschule Regensburg.</li>
        <li>Am 12.21943 war Planspielbesprechung des Luftschutzes f&uuml;r
          Ruhmannsfelden. Diese war von der Regierung von Niederbayern/Oberpfalz
          angeordnet und von Luftschutzoffizier der Regierung, Hr. Leutnant
          Ditsch geleitet worden. Sie war besucht von den Vertretern des
          Landrates Viechtach unter anderem Hr. Landrat Seufert von Deggendorf,
          dem Kreissicherheitskommiss&auml;r, dem Kreisgruppenf&uuml;hrer des
          RLB, dem Kreisfeuerwehrf&uuml;hrer und dem B&uuml;rgermeister von
          Ruhmannsfelden. Au&szlig;erdem waren erschienen alle
          B&uuml;rgermeister und Feuerwehrf&uuml;hrer der 15 km-Zone. F&uuml;r
          den B&uuml;rgermeister und f&uuml;r den RLG sprach Rektor H&ouml;gn,
          f&uuml;r die Feuerwehr der Kreisfeuerwehrf&uuml;hrer Hr.
          Kramh&ouml;ller und der Feuerwehrf&uuml;hrerstellvertreter Hr.
          Linsmeier, f&uuml;r die Luftschutzwarte Hr. Zadler, f&uuml;r die Post
          Hr. Brummer, f&uuml;r die Bahn Hr. Widmann, f&uuml;r NSV Hr. Ernst und
          f&uuml;r die Gendarmerie Hr. Gendarmeriemeister Hr. Schindlbeck und
          f&uuml;r die Glaserinnung Hr. Geiger. Die Besprechung war nicht
          &ouml;ffentlich. Die Aussprache hat gezeigt, dass die Feuerwehr
          Ruhmannsfelden im Falle eines feindlichen Fliegerangriffes ihrer
          Aufgabe bewusst und gewachsen ist.</li>
        <li>Am 28.3.1943 fand der Generalappell f&uuml;r die Feuerwehr
          Ruhmannsfelden statt. Bei diesem waren auch anwesend der
          Kreisfeuerwehrf&uuml;hrer Hr. Kramheller, der in l&auml;ngeren
          Ausf&uuml;hrungen referiert &uuml;ber alle wichtigen Fragen, die
          zurzeit die Feuerwehr ber&uuml;hren und der
          Unterkreisfeuerwehrf&uuml;hrer Kronner, der seine Beobachtungen und
          Eindr&uuml;cke bei den Visitationen der einzelnen Feuerwehren
          schilderte. Hr. Feuerwehrf&uuml;hrer Hinkofer leitete den
          Generalappell und schloss ihn nach 2 ½ st&uuml;ndiger Dauer mit dem
          3-fachen Sieg-Heil auf den F&uuml;hrer.</li>
        <li>Am 5.9.1943 fand lauf Anordnung der Kreisfeuerwehrf&uuml;hrers
          eine Dringlichkeitswehr&uuml;bung statt, zu der 29 Feuerwehrkameraden
          vorgeladen waren. Der Kreisfeuerwehrf&uuml;hrer erteilte denselben
          besondere Instruktionen. Anwesend war dabei auch Hr.
          B&uuml;rgermeister.</li>
        <li>Am 17.9.1943 mittags 11 Uhr brach bei Schr&ouml;tter in der
          Grabsiedlung hier ein Dachbodenbrand aus. Die rasch herbeigeeilte
          Feuerwehr l&ouml;schte in kurzer Zeit den Brand, wobei sich
          insbesonders die HJ-Feuerwehr auszeichnete. Auch die Brandwache wurde
          von 2 Jungen der HJ-Feuerwehr gestellt. Der F&uuml;hrer der
          HJ-Feuerwehr Hr. Johann Linsmeier erhielt eine Kopfverletzung, sodass
          er sich in &auml;rztliche Behandlung begeben musste.</li>
      </ul>
      <h3>1944<a name="1944"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Bei der am Sonntag, den 30.1.1944 nachmittags 3 Uhr
          stattgefundenen Gro&szlig;kundgebung zum Tage der nationalen Erhebung
          in hiesiger Turnhalle beteiligte sich die Feuerwehr sehr zahlreich.</li>
        <li>Am Freitag, den 11.8.1944 fand in Ruhmannsfelden eine von der
          Regierung von Niederbayern/Oberpfalz festgesetzte Luftschutz&uuml;bung
          statt, bei der sich auch die freiwillige Feuerwehr Ruhmannsfelden zu
          beteiligen hatte. Der anwesende Kreisfeuerwehrf&uuml;hrer Hr.
          Kramheller von Teisnach sprach sich nach der Luftschutz&uuml;bung sehr
          lobenswert &uuml;ber das schnelle und exakte Eingreifen der Feuerwehr
          aus und spendete besonders der tadellos arbeitenden HJ-Feuerwehr ein
          ganz besonderes Lob.</li>
        <li>Am 23.8.1944 nachmittags 3 Uhr brach im Stadel eines kleinen
          Landanwesens in Lindenau, Gemeinde Achslach ein Brand aus, bei dem die
          freiwillige Feuerwehr Ruhmannsfelden mit der Motorspritze raschestens
          erschienen war, aber nicht mehr in Hilfeleistung zu treten hatte, da
          der Brand teils von den eigenen Leuten, teils von der erschienen
          Nachbarsfeuerwehr rasch gel&ouml;scht werden konnte.</li>
      </ul>
