      <h3>HAUPTTEIL</h3>
      <h3><b>1.&nbsp; </b> Vorwort<a name="o1" id="o1"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h3>
      <p>Die vorliegende Chronik der freiwilligen Feuerwehr Ruhmannsfelden
        macht nicht den Anspruch auf Vollst&auml;ndigkeit. Es wurde nur
        zusammengetragen, was erreichbar war.<br>
        Es w&auml;chst daraus die Aufgabe Altes zu ergr&uuml;nden und
        nachzutragen, Neues sofort einzutragen. Dann wird die Chronik einmal
        etwas Wertvolles f&uuml;r die freiwillige Feuerwehr Ruhmannsfelden.</p>
      <p>August H&ouml;gn, Oberlehrer</p>
      <h2><b>I.&nbsp; </b> Vor der Gr&uuml;ndung der freiwilligen Feuerwehr
        Ruhmannsfelden<a name="i" id="i"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h2>
      <p>Der Markt Ruhmannsfelden wurde des &Ouml;fteren von
        Schicksalsschweren Br&auml;nden heimgesucht. 1522 wurde der ganze Markt
        durch eine Feuersbrunst vernichtet. 1574 brannte das Pfarrgotteshaus
        vollst&auml;ndig nieder. 1633 wurden das Pfarrvikarhaus und die
        Klostertaverne in Asche gelegt. Es sei erinnert an die
        gr&ouml;&szlig;eren Br&auml;nde in den Jahren 1820, 1889 und 1894. Durch
        alle Jahrhunderte und Jahrzehnte hindurch hat sich in Ruhmannsfelden die
        gegenseitige Hilfsbereitschaft bei Feuersbr&auml;nden erwiesen und durch
        alle Zeiten hindurch galt den durch viele Br&auml;nde schwer
        heimgesuchten Bewohnern des Marktes Ruhmannsfelden der Wahlspruch:
        „Einer f&uuml;r alle, alle f&uuml;r Einen.“<br>
        Fr&uuml;her gab es weder Pflichtfeuerwehren, noch freiwillige
        Feuerwehren. Es galt bei Ausbruch eines Brandes f&uuml;r Mann und Frau,
        f&uuml;r Alt und Jung als Selbstverst&auml;ndlichkeit zu rennen, zu
        retten, zu l&ouml;schen mitzuhelfen, wo es ging. Da wurde unaufgefordert
        gepumpt an den Spritzen und wenn die M&auml;nner anderweitig sich am
        Rettungswerke beteiligen mussten, so traten die Frauen an die Pumpstange
        und Feuereimer flogen von Hand zu Hand. In den St&auml;dten wurden
        eigene Feuerwachen gebildet aus den Reihen der B&uuml;rger und
        B&uuml;rgerss&ouml;hne. Diese Feuerwachen, die zuerst nur zeitweise
        aufgestellt wurden, die wurden sp&auml;ter zu einer dauernden
        Einrichtung. Aus diesen haben sich zun&auml;chst in den St&auml;dten die
        freiwilligen Feuerwehren gebildet. Da man deren
        Zweckm&auml;&szlig;igkeit und N&uuml;tzlichkeit alsbald erkannte, haben
        sich in M&auml;rkten und dann in D&ouml;rfern solche freiwilligen
        Feuerwehren gebildet. Der Kreis, die Bezirke und Gemeinden zeigten stets
        ihr wohlwollendes Interesse f&uuml;r die Bed&uuml;rfnisse einer
        hilfsbereiten und schlagfertigen Feuerwehr und f&ouml;rderten und
        unterst&uuml;tzten die Feuerwehren und damit das Feuerl&ouml;schwesen in
        hervorragendem Ma&szlig;e.<br>
        Bitten, Gesuche, W&uuml;nsche, Antr&auml;ge f&uuml;r F&ouml;rderung des
        Feuerl&ouml;schwesens wurden sowohl von Bezirks- und Kreisvertretungen
        als auch von den Bezirks&auml;mtern und Regierungen w&auml;rmstens
        bef&uuml;rwortete und genehmigt. Wenn wir in den Akten vor Jahrzehnten
        oder Jahrhunderten nachlesen, immer wieder lesen wir, dass f&uuml;r
        „Sicherheit“, in diesem Falle also f&uuml;r Feuerl&ouml;schwesen und
        Feuerwehr keine Mittel gescheut wurden und keine Ausgaben zu hoch
        erschienen.</p>
      <h2><b>II.&nbsp; </b> Ausz&uuml;ge aus den &auml;ltesten
        Ruhmannsfeldener Akten<a name="ii" id="ii"></a>&nbsp;&nbsp;<a
  href="#oben"><img src="images/geschichts_klein_oben.png"></a></h2>
      <p>Aus den diesbez&uuml;glichen noch vorgefundenen &auml;ltesten
        Ruhmannsfeldener Akten lesen wir:</p>
      <h3>1814:<a name="1814"></a>&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Die Gemeinderechnung vom Jahre 1814 weist unter Rubrik
          „Inventarium“ aus: 1. Alte h&ouml;lzerne Feuerspritzen, 2. alte
          me&szlig;ingere Feuerspritzen. (Die unter 1. aufgef&uuml;hrten
          h&ouml;lzernen Feuerspritzen sich 1820 verbrannt.)</li>
      </ul>
      <h3>1817/18<a name="181718"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Dem Joseph Stoiber, Schlossermeister ist f&uuml;r Beschlagung der
          gro&szlig;en, me&szlig;ernen Feuerspritzen 7 fl., f&uuml;r Beschlagung
          der h&ouml;lzernen 5 fl. 42 kr. bezahlt worden.</li>
        <li>Von der Schmiedearbeit hiezu ist dem Andr&auml; Baumann bezahlt 36
          kr., dem Lorenz Tafner, Zimmermann ist f&uuml;r das Zusammenrichten
          der drei Feuerspritzen mit drei neuen R&ouml;hren, dann f&uuml;r
          Lein&ouml;l, Farb bezahlt lt. Schein 5 fl. 50 kr.</li>
      </ul>
      <h3>1820<a name="1820"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Da am 1. Juli 1820 durch eine ungl&uuml;cklichen entstandenen
          Brand 12 b&uuml;rgerliche Hausungen samt der Kirche in Asche gelegt
          wurden, dabey selbst die kleinen zwey h&ouml;lzernen Feuerspritzen
          verbrannten und die eine me&szlig;ingere verdorben wurde, so ist
          letztere durch den Schlossermeister Joseph Stoiber wieder in
          brauchbaren Zustand hergestellt worden.</li>
      </ul>
      <h3>1821<a name="1821"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>den beiden Zimmerleuten Tafner und Zitzelsberger wurde an
          Herstellung der 2 Feuerwehrleitern 3 fl. Dem Schied Joseph Baumann
          f&uuml;r Beschlagung dieser zwei Feuerwehrleitern 48 kr. bezahlt.</li>
      </ul>
      <h3>1826/27<a name="182627"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Anton Priglmeier Kupferschmied erhielt f&uuml;r eine neue
          Feuerl&ouml;schspritzen mit Ausnahme des Rohrs, welches schon
          vorhanden war 56 fl. 9 kr.</li>
      </ul>
      <h3>1835/36<a name="183536"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Laut Rechnung musste bei einer damaligen B&uuml;rgeraufnahme <i>„ein
          bestimmter Beytrag“</i> zu den Feuerl&ouml;sch Requisiten geleistet
          werden (meistens 1 fl.).</li>
      </ul>
      <h3>1842/43<a name="184243"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Schmied Joseph Baumann erhielt f&uuml;r Reparatur der kleinen
          Feuerspritze laut Schein 36. kr.</li>
        <li>Vorstehender erhielt f&uuml;r Reisen:<br>
          a.)&nbsp; nach Deggendorf<br>
          b.)&nbsp; nach Passau<br>
          betr. Anschaffung einer neuen Feuerspritzen 2. fl. 48 kr.</li>
        <li>Zeugschmied Joseph Stoiber erhielt f&uuml;r Mitreise nach
          Deggendorf, um die neue Feuerspritze zu probieren 48 kr.</li>
      </ul>
      <h3>1843/44<a name="184344"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Der Pferdeknecht Michael Baumgartner erhielt zur Belohnung seiner
          flei&szlig;igen Wasserzufuhr bei dem Brande des Mossm&uuml;ller
          Zeugweberhause in der Nacht vom 7. Mai 1843 laut Schein 36 kr.</li>
        <li>F&uuml;r das vom Glockengie&szlig;er Samasa in Passau
          &uuml;berschickte messingenes Sch&ouml;pfwerk zur neuen Feuerspritze
          an Porto bezahlt 1 fl. 27 kr.</li>
        <li>Dem Rotgerber Gerhard Lukas f&uuml;r ein abgegebenes St&uuml;ck
          Leder zur Feuerspritze 42 kr.</li>
        <li>Dem Glockengie&szlig;er Samasa in Passau f&uuml;r das abgelieferte
          neue messingene Druckwerk zur Feuerspritze den Gesamtbetrag laut
          Quittung &uuml;bersandt 225 fl. Porto 6 kr.</li>
        <li>Dem Schmied Joseph Baumann von hier f&uuml;r Herstellung des
          Wagens samt Kasten zur Feuerspritze mit vollst&auml;ndigen
          Eisenbeschl&auml;gen und gefertigten Schrauben hieran laut Schein 55
          fl. Rest 177 fl.</li>
      </ul>
      <h3>1844/45<a name="184445"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>Zur Bezahlung der neuen Feuerspritze an den Schmied Joseph Baumann
          von hier musste eine neue Umlage nach dem Steuerfu&szlig; erhoben
