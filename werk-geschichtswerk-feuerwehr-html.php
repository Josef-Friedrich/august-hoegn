      </ul>
      <h3>1857/58<a name="185758"></a>:&nbsp;&nbsp;<a href="#oben"><img
  src="images/geschichts_klein_oben.png"></a></h3>
      <ul>
        <li>erhielt Kupferschmied Pr&uuml;glmeier von Viechtach f&uuml;r die
