                        <p>August H&ouml;gn wurde am 2. August 1878 als mittleres von f&uuml;nf Kindern   des Buchbinderehepaares Andreas und Helene H&ouml;gn in Deggendorf geboren.   Wie alle Kinder des sp&auml;teren Magistratsrats, Landrats und   Landtagsabgeordneten Andreas H&ouml;gn erlernte auch August das   Klavierspielen. Seine musikalischen F&auml;higkeiten konnte er in der   Ausbildung zum Lehrer an der <a href="http://de.wikipedia.org/w/index.php?title=Pr%C3%A4parandenschule&amp;action=edit&amp;redlink=1" title="Pr&auml;parandenschule (Seite nicht vorhanden)">Pr&auml;parandenschule</a> in Deggendorf (1891 – 1894) und an der <a href="http://de.wikipedia.org/wiki/Lehrerbildungsanstalt" title="Lehrerbildungsanstalt">Lehrerbildungsanstalt</a> in <a href="http://de.wikipedia.org/wiki/Straubing" title="Straubing">Straubing</a> (1894 – 1896) weiterbilden. Als Junglehrer wurde H&ouml;gn an mehrere Orte in Niederbayern versetzt (<a href="http://de.wikipedia.org/wiki/Deggendorf" title="Deggendorf">Deggendorf</a>, <a href="http://de.wikipedia.org/wiki/Neukirchen_%28Niederbayern%29" title="Neukirchen (Niederbayern)">Neukirchen (Niederbayern)</a>, <a href="http://de.wikipedia.org/wiki/Schaufling" title="Schaufling">Schaufling</a>, <a href="http://de.wikipedia.org/wiki/Geratskirchen" title="Geratskirchen">Geratskirchen</a>, <a href="http://de.wikipedia.org/wiki/Zeilarn" title="Zeilarn">Zeilarn</a>, <a href="http://de.wikipedia.org/wiki/Wallersdorf" title="Wallersdorf">Wallersdorf</a>, <a href="http://de.wikipedia.org/wiki/Eberhardsreuth" title="Eberhardsreuth">Eberhardsreuth</a>).   Am 20. Juni 1904 heiratete H&ouml;gn in Wallersdorf seine Ehefrau Emma   Gerstl. Aus dieser Ehe gingen zwei Kinder hervor (14. August 1906   Elfriede, 17. Januar 1912 August). Am 1. Januar 1910 wurde H&ouml;gn nach   Ruhmannsfelden versetzt, wo er bis zu seinem Tod wohnhaft blieb. In   seinen ersten Ruhmannsfeldener Jahren trat er als Vorstand des   Turnvereins (21. Mai 1910 – 27. Dezember 1913), Schriftf&uuml;hrer der <a href="http://de.wikipedia.org/wiki/Freiwillige_Feuerwehr" title="Freiwillige Feuerwehr">Freiwilligen Feuerwehr</a> Ruhmannsfelden (26. Dezember 1910 – 26. Dezember 1950) und   Gemeindeschreiber der benachbarten Gemeinde Zachenberg (1913 – 1920)   hervor. Am 26. Juni 1926 starb H&ouml;gns Ehefrau Emma. In der Folgezeit   stelle H&ouml;gn seine Haush&auml;lterin Rosa Beischmied ein, die bis an sein   Lebensende bei ihm wohnte und arbeitete.</p>
                        <p>Im <a href="http://de.wikipedia.org/wiki/Drittes_Reich" title="Drittes Reich">Dritten Reich</a> war H&ouml;gn in die politischen Geschehnisse als Gemeindegruppenf&uuml;hrer,   Untergruppenf&uuml;hrer und Beauftragter f&uuml;r „Schutz des Volksguts“   eingebunden. Von nicht zu untersch&auml;tzenden Einfluss auf die politische   Gesinnung von H&ouml;gn im Dritten Reich d&uuml;rfte die als sehr gut beschriebene   Beziehung zu seinem Schwiegersohn und <a href="http://de.wikipedia.org/wiki/NSDAP" title="NSDAP">Nazi</a>-Oberb&uuml;rgermeister von Bayreuth, <a href="http://de.wikipedia.org/wiki/Karl_Schlumprecht" title="Karl Schlumprecht">Karl Schlumprecht</a>,   gewesen sein. 1945 wurde H&ouml;gn, der seit 1921 Schulleiter, seit 1929   Oberlehrer und seit 1940 Rektor der Volksschule Ruhmannsfelden war, in   Folge der <a href="http://de.wikipedia.org/wiki/Entnazifizierung" title="Entnazifizierung">Entnazifizierung</a> vom Schuldienst suspendiert. Nach einem kurzen Schuleinsatz 1947 ging   er im selben Jahr in den Ruhestand. 1953 erlitt er einen Schlaganfall,   der eine halbseitige L&auml;hmung zur Folge hatte. August H&ouml;gn starb am 13.   Dezember 1961 in Ruhmannsfelden und wurde am 15. Dezember 1961 in   Deggendorf beerdigt.</p>
                        <p>Von 1926 bis 1929 erschienen mehrere von H&ouml;gn verfasste   heimatkundliche Zeitungsartikel. Im Ruhestand verfasste er drei gro&szlig;e   heimatkundliche Abhandlungen („Geschichte von Ruhmannsfelden“ 1949,   „Geschichte und Chronik der Freiwilligen Feuerwehr Ruhmannsfelden“ 1951,   „Heimat-Geschichte der Gemeinde Zachenberg“ 1953).</p>
                        <p>Ab 1919 engagierte sich H&ouml;gn als Leiter der S&auml;nger- und   Orchesterriege des Turnvereins Ruhmannsfelden. Im M&auml;rz 1923 kam unter   H&ouml;gns Leitung das <a href="http://de.wikipedia.org/wiki/Singspiel" title="Singspiel">Singspiel</a> „Der Holledauer Fidel“ von <a href="http://de.wikipedia.org/wiki/Erhard_Kutschenreuter" title="Erhard Kutschenreuter">Erhard Kutschenreuter</a> zur Auff&uuml;hrung, die wohl gr&ouml;&szlig;te und erfolgreichste Darbietung des Turnvereins.</p>
                        <p>Von seinem 20. bis zum seinem 76. Lebensjahr war H&ouml;gn als <a href="http://de.wikipedia.org/wiki/Gesang" title="Gesang">S&auml;nger</a>, <a href="http://de.wikipedia.org/wiki/Organist" title="Organist">Organist</a> und <a href="http://de.wikipedia.org/wiki/Chorregent" title="Chorregent">Chorregent</a> bei mehreren Kirchench&ouml;ren der beiden <a href="http://de.wikipedia.org/wiki/Bistum_Passau" title="Bistum Passau">Di&ouml;zesen Passau</a> und <a href="http://de.wikipedia.org/wiki/Bistum_Regensburg" title="Bistum Regensburg">Regensburg</a> t&auml;tig. In Ruhmannsfelden wirkte er 43 Jahre lang vor allem als Organist   und in drei Phasen auch als Chorregent (1921 bis 1924, 1927 bis 1929,   und 1940 bis 1953). Seine zahlreichen geistlichen Werke, darunter drei <a href="http://de.wikipedia.org/wiki/Messe_%28Musik%29" title="Messe (Musik)">Messen</a> und 13 <a href="http://de.wikipedia.org/w/index.php?title=Marienlied&amp;action=edit&amp;redlink=1" title="Marienlied (Seite nicht vorhanden)">Marienlieder</a>,   sind &uuml;berwiegend in den Zeiten entstanden, als er Chorregent in   Ruhmannsfelden war. 1898 entstand seine erste (Veni creator spiritus   B-Dur) und 1960 seine letzte (Marienlied Nr. 13 Es-Dur) erhaltene <a href="http://de.wikipedia.org/wiki/Komposition_%28Musik%29" title="Komposition (Musik)">Komposition</a>. 1905 wurde sein <a href="http://de.wikipedia.org/wiki/Marschmusik" title="Marschmusik">Marsch</a> „In Treue fest!“ verlegt.</p>
